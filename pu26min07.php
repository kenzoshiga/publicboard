          <p class="h6 ind0">1.　使者の意義</p>
          <p class="p6 ind1">本人以外の者で、書面等を届ける等、本人の意思表示をそのまま伝達する者（伝達機関としての使者）や、口頭で伝える等、本人の決定した意思を示してその意思表示を完成させる者（表示機関としての使者）をいう。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 代理との違い</p>
          <p class="p6 ind2">代理では、代理人の意思表示の効果が、直接に本人に帰属する。これに対し、使者とは、本人の決定した効果意思を表示し（表示機関）、又は、完成した意思表示を伝達する者（伝達機関）である。すなわち、効果意思を決定するのは本人であり、使者はそれを表示・伝達するにすぎない。</p>
