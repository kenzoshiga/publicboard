      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">代理人が自己又は第三者の利益を図る目的で代理権の範囲内の行為をした場　合に代理行為の効力はどうなるか。代理権が濫用された場合でも、代理人は代理権の範囲内で行動しているため問題となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　代理権の濫用の要件（107条）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 代理人が自己又は第三者の利益を図る目的で代理権の範囲内の行為をしたこと</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 相手方がその目的を知り、又は知ることができたこと</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　効果</p>
          <p class="p6 ind1">代理権の濫用の要件を充足した場合には、その代理行為は無権代理とみなされる（107条）。したがって、代理行為の効果は本人に帰属することがないのが原則である（113条）。</p>
        </div>
