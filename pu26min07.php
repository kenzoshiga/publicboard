        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　代理人が直接本人の名だけを示した場合も顕名ありといえるか。</p>
          <p class="p6 ind1">　ex. Ａから代理権を与えられたＢが直接「Ａ」と表示してＣと取引する場合</p><br>
          <p class="p6 ind1">→　肯定（最判昭39.９.15）</p>
          <p class="p6 ind1">（理由）</p>
          <p class="p6 ind1">効果帰属主体を明らかにするという顕名の趣旨は満たされる。</p>
