      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">自己契約：同一の法律行為について当事者の一方が相手方の代理人となること</p>
          <p class="p6 ind1">双方代理：同一人が同一法律行為の当事者双方の代理人となること</p>
