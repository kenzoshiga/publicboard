      <h3 id="A01-07-02">第２節　代理の効力が問題となる場合</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-07-02-0-01">１　顕名のない代理行為</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　非顕名のときの効果（代理人が本人のためにすることを示さないでした意思表示の効果）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 原則</p>
          <p class="p6 ind2">代理人が自己のためにした意思表示とみなされる（100条本文）。すなわち、代理人が錯誤による取消し（95条）を主張することは許されない。取引の相手が代理人自身であるという相手方の信頼を保護するためである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 例外</p>
