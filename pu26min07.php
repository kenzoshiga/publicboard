          <p class="p6 ind1">代理権を授与する契約によって決まるのが原則である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　「権限の定めのない代理人」</p>
          <p class="p6 ind1">代理権があることは明らかであるが、その範囲が不明である場合、又は、特に範囲を決めていない場合の補充規定として、103条は、代理人は、保存行為・利用行為・改良行為をすることができるとする。ただし、利用行為及び改良行為は、物又は権利の性質を変えない範囲内でのみ許されるとしている。</p>
