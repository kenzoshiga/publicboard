          <p class="h6 ind1">(2) 利用行為（103条２号）</p>
          <p class="p6 ind2">：財産について収益を図る行為</p>
          <p class="p6 ind2">代理人は、目的物又は権利の性質を変えない範囲での利用行為が出来る。</p>
          <p class="p6 ind2">ex. 現金の預金、物の賃貸（無償で貸し付けることは利用に当たらない）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 改良行為（103条２号）</p>
          <p class="p6 ind2">：財産の経済的価値を増加させる行為</p>
          <p class="p6 ind2">代理人は、目的物又は権利の性質を変えない範囲での改良行為ができる。</p>
          <p class="p6 ind2">ex. 家屋に電気・ガスの設備を施す、無利息貸金を利息付にする</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　共同代理</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">数人が同一事項につき代理権を有し、すべての代理人が共同してのみ代理行為をすることができる場合を、共同代理という（818条３項本文など）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 趣旨</p>
          <p class="p6 ind2">代理権の濫用を防止し、本人を保護する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 効果</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 全員の共同によらない代理は、完全な代理にはならず、その意味で、共同代理は各代理人にとって代理権の制限となる。</p>
          <p class="h6 ind2 nobold">(b) この制限に反して単独でなされた代理行為については、無権代理となると解される。もっとも、一般的には表見代理の適用が考えられる。</p>
