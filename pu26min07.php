        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 原則</p>
          <p class="p6 ind3">代理人が相手方に対してした意思表示の効力が意思の不存在、錯誤、詐欺、強迫又はある事情を知っていたこと若しくは知らなかったことにつき過失があったことによって影響を受けるべき場合には、その事実の有無は、代理人について決するものとする（101条１項）。</p>
          <p class="p6 ind3">つまり、代理行為の瑕疵の有無は、実際に代理行為をした代理人を基準として決せられる（101条１項）。</p>
        </div>
