        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　趣旨</p>
          <p class="p6 ind1">自己契約・双方代理は、事実上、代理人が１人で契約を締結することになり、本人や当事者の一方の利益が不当に害されるおそれがあるため、これを禁止した。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　効果</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 原則－無権代理（108条１項本文）</p>
          <p class="p6 ind2">自己契約、及び本人と代理人との利益相反行為は無権代理とみなされる（108条１項、２項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 例外－有効な代理行為となる場合（108条１項ただし書）</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 債務を履行する場合</p>
          <p class="p6 ind3">本人の利益を害するおそれがないからである。</p>
          <p class="p6 ind3">債務の履行であっても、本人を害するおそれがある場合、たとえば新たな契約である代物弁済や期限未到来の債務・争いのある債務の弁済については、本条ただし書は適用されない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 本人があらかじめ許諾した場合</p>
        </div>
