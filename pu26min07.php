        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">代理：本人と一定の関係にある他人（代理人）が本人のためにすることを示して意思表示をなし、その法律効果が全面的に本人に帰属することを認める制度</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 代理権の存在</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 顕名があること</p>
          <p class="p6 ind2">顕名：代理人がその行為の法律効果を本人に帰属させようとする意思（代理意思）を有していることを相手方に知らせること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 代理行為：代理人と相手方との間の有効な法律行為</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　効果</p>
          <p class="p6 ind1">代理人がその権限内において本人のためにすることを示してした意思表示は、物権的効果、債権的効果を問わず、本人に対して直接にその効力を生ずる（99条1項）。本来の法律効果の他、法律行為の当事者としての地位（取消権、解除権など）も本人に帰属する。</p>
          <p class="p6 ind1">また、第三者が代理人に対してした意思表示についても、本人に対して直接にその効力を生ずる（99条2項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　代理の類型（法定代理と任意代理）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">本人の意思又は信任による代理を任意代理といい、そうでない代理を法定代理という。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 区別の実益</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 任意代理は普通、個別代理であるが、法定代理は包括的代理であることが多い。</p>
          <p class="h6 ind3 nobold">② 復任権（104条、106条）</p>
          <p class="h6 ind3 nobold">③ 代理権の消滅事由（111条１項、２項）</p>
        </div>
        </div>
        </div>
        <div class="p-card" id="min07-q1">
          <p class="h6 ind0">5.　代理人の能力</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 代理人の行為能力の要否</p>
          <p class="p6 ind2">制限行為能力者が代理人としてした行為は、行為能力の制限によっては取り消すことができない（102条本文）。したがって、代理人及び本人は、代理人が制限行為能力者であることを理由として、代理行為を取り消すことができない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 趣旨</p>
          <p class="p6 ind2">代理行為の効果は直接本人に帰属するから（99条）、代理人に不利益が及ぶことはなく、制限行為能力者制度によって代理人を保護する必要はない。</p>
          <p class="p6 ind2">また、制限行為能力者を代理人とした本人が後になって代理人が制限行為能力者であることを理由に取消権を行使するのを許すべきではない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 例外</p>
          <p class="p6 ind2">ただし、制限行為能力者が他の制限行為能力者の法定代理人としてした行為については、この限りでない（102条ただし書）。</p>
          <p class="p6 ind2">その趣旨は、制限行為能力者が他の制限行為能力者の法定代理人としてした行為について、代理行為を取り消すことができないとすると、① 本人の保護という行為能力制度の目的が十分に達成されないおそれがあること、② 本人が代理人の選任に直接関与するわけではないから、代理人が制限行為能力者であることのリスクを本人に負担させるのは妥当でないこと、から、例外的に行為能力の制限を根拠とする代理行為の取消しを認める点にある。</p>
          <p class="p6 ind2">ex. 被保佐人Ｂが未成年者Ａの親権者である場合</p>
          <p class="p6 ind2">　　被保佐人Ｂが他の被保佐人Ａの保佐人である場合</p>
