        <div class="p-card">
          <p class="h6 ind0">4.　盗品・遺失物に関する特則－即時取得の制限</p>
          <p class="p6 ind1">目的物が原権利者の意思に反し、又は意思によらずその占有を離れた盗品・遺失物である場合に、原権利者の保護を図り、２年間は取戻しを可能とする。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 「盗品又は遺失物」</p>
          <p class="p6 ind2">所有者の意思によらないで占有を失った場合に限り適用される。</p>
          <p class="p6 ind2">詐欺・恐喝・横領の場合には適用なし（瑕疵はあるが意思に基づくので）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 原権利者の回復請求権</p>
          <p class="p6 ind2">盗難又は遺失の時から２年間（除斥期間・通説）、原則として無償で回復請求できる（193条）。ただし、例外的に194条により代価を弁償する場合がある。</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 回復請求権者</p>
          <p class="p6 ind3">「被害者又は遺失者」</p>
          <p class="p6 ind3">原所有者が回復請求できることはもちろん、賃借人・受寄者も請求できる。</p>
          <p class="p6 ind3">質権者は回復請求できない。占有回収の訴えによることになる（353条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 相手方</p>
          <p class="p6 ind3">現に占有している者である。</p>
          <p class="p6 ind3">即時取得者からの特定承継人も含む（判例・通説）。</p>
          <p class="p6 ind3">目的物が変形されたり、相手方の手中にないときには、返還請求権もなく、物に代わる損害賠償の請求もできない（最判昭26.11.27）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 所有権の帰属</p><br>
          <p class="p6 ind3">Ｑ　回復請求をすることができる期間、動産の所有権は原所有者・即時取得者のいずれに属するか。</p><br>
          <p class="p6 ind3">Ａ　原所有者説（大判昭４.12.11）</p>
          <p class="p6 ind3">回復請求をすることができる期間、その動産の所有権は原所有者に帰属する。</p>
          <p class="p6 ind3">（理由）</p>
          <p class="p6 ind3">「回復」とは、占有の回復の意味である。</p><br>
          <p class="p6 ind3">Ｂ　即時取得者説</p>
          <p class="p6 ind3">即時取得（192条）の原則を重視して、即時取得者にあるとする。</p>
          <p class="p6 ind3">（理由）</p>
          <p class="p6 ind3">「回復」とは、所有権の回復を意味する。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 代価の弁償（194条）</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 意義</p>
          <p class="p6 ind3">占有者が競売、公の市場、又は目的物と同種の物を販売する商人から買い受けた場合、回復者は、占有者が支払った代価を弁償しなければ、回復請求できない。</p>
          <p class="p6 ind3">一般の動産譲渡の場合に比べ、はるかにその取引安全を保護する必要があるからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 代価弁償の返還時期</p><br>
          <p class="p6 ind3">Ｑ　物の返還後も代価弁償を請求できるか。</p>
          <p class="p6 ind3">→　肯定説（最判平12.６.27）</p>
          <p class="p6 ind3">（理由）</p>
          <p class="h6 ind3 nobold">①占有者の保護という194条の立法趣旨。</p>
          <p class="h6 ind3 nobold">②194条を取得者に抗弁を認めただけの規定とすると、盗品だと言われて素直にこれを原所有者に引き渡した者が代価の弁償を受けられず、かえってどこまでも返還を拒んだ者が弁償を受けることができるという不均衡が生じる。</p>
