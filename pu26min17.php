      <div class="outer-title" id="A02-01-03-3-03">３　混同（179条）</div>
      <p>混同：併存させておく必要のない２個の法律上の地位が同一人に帰属することにより、一方が消滅すること</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　所有権と制限物権の混同（179条１項）</p>
          <p class="p6 ind1">ex. 地上権者が土地の所有権を取得した場合には、地上権は消滅する（179条１項本文）。</p>
          <p class="p6 ind1">例外－以下の場合には混同により消滅しない（179条１項ただし書）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①制限物権が第三者の権利の目的となっている場合</p>
          <p class="h6 ind3 nobold">②物権の目的物が第三者の権利の目的となっている場合</p>
          <p class="h6 ind3 nobold">③自己借地権</p>
          <p class="p6 ind4">借地借家法は、借地権（建物所有を目的とする地上権又は土地の賃借権。借地借家法２条１号）を設定する場合において、「他の者と共に有することとなるときに限り」、自己借地権を設定できるとする（借地借家法15条１項）。また、借地権が、借地権設定者に帰した場合であっても、「他の者と共にその借地権を有するときは」、借地権は消滅しないとする（借地借家法15条２項）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　制限物権とこれを目的とする物権の混同（179条２項）</p>
          <p class="p6 ind1">ex. 地上権者が地上権を目的として設定されている抵当権を取得した場合には、当該抵当権は消滅する（179条２項前段）。</p>
          <p class="p6 ind1">例外－以下の場合には混同により消滅しない（179条２項後段、１項ただし書）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①制限物権が第三者の権利の目的となっている場合</p>
