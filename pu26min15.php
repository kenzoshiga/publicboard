<div class="kakomon"><a href="pu26min15q.php#q-r05-07-ah06-18-oh04-14-u" title="R5-7-ア">過去問</a></div>
        </div>
        <div class="p-card" id="min15-q8">
          <p class="h6 ind2 nobold">(c) 相続による権利の承継と第三者</p>
          <p class="p6 ind3">相続による権利の承継は、遺産の分割によるものかどうかにかかわらず、法定相続分を超える部分については、登記、登録その他の対抗要件を備えなければ、第三者に対抗することができない（民法899条の２第1項）。</p>
          <p class="p6 ind3">特定の相続財産を特定の相続人に「相続させる」という遺言は、遺贈ではなく、遺産分割の方法の指定と解されており（最判平３.４.19）、遺産分割の手続を要せず、被相続人から当該相続人への移転登記ができるというのが判例である。しかし、特定の遺産を特定の相続人に「相続させる」趣旨の特定財産承継遺言（民法1014条2項）によって不動産を取得した者は、法定相続分を上回る部分については、登記なくしてその権利を第三者に対抗することはできない（民法899条の２第1項）。</p>
