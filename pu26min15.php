          <p class="h6 ind1">(2) 共同相続人の登記冒用</p>
          <p class="p6 ind2">Ｑ　特定の相続不動産につき、共同相続人の１人が単独相続の登記をしてこれを第三者に譲渡した場合、他の共同相続人は自己の相続分を登記なしにこの第三者に対抗することができるか。</p>
          <p class="p6 ind2">→　登記不要説（最判昭38.２.22・通説）</p>
          <p class="p6 ind2">（理由）</p>
