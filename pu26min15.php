          <p class="h6 ind2 nobold">(a) 遺産分割前の第三者</p>
          <p class="p6 ind3">特定の相続不動産につき、共同相続人の１人（Ａ）が持分権を第三者（Ｃ）に譲渡し、その後、遺産分割が行われて、他の相続人（Ｂ）がこれを単独で相続した場合</p>
          <p class="p6 ind3"><img class="embed-img" src="img/min15-01.png" alt="min15-01.png"></p>
          <p class="p6 ind3">遺産分割の遡及効により（909条本文）、 Ｂは相続開始の時にさかのぼってこの相続不動産の所有権を取得することになるが、第三者の権利を害することができない（909条ただし書）｡そこで、ＣはＡの持分についてのＡからの取得をＢに対抗することができることになるが、その際Ｃには登記が必要とするのが通説である。もっとも、ここにいう登記は対抗要件としての登記ではなく、909条ただし書の権利保護資格要件としての登記である。</p>
