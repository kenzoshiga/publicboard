          <p class="p6 ind2"><img class="embed-img" src="img/min15-03.png" alt="min15-03.png"></p>
          <p class="p6 ind2">（理由）</p>
          <p class="h6 ind3 nobold">① 放棄は相続資格の遡及的消滅である（939条）。</p>
          <p class="h6 ind3 nobold">② 放棄によっては個々の財産の帰属は確定しない。</p>
          <p class="h6 ind3 nobold">③ 放棄の有無は家庭裁判所で確認できる（938条）。</p>
