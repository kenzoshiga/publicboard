      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　登記請求権</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 登記請求権の意義</p>
          <p class="p6 ind2">登記申請手続に協力することを請求することができる権利＝実体私法上の権利</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 登記請求権の発生原因</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 実体的権利関係と登記の不一致</p>
          <p class="p6 ind4">物権そのものの効力として発生する（物権的登記請求権）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">② 権利変動と登記の不一致</p>
          <p class="p6 ind4">権利変動を反映するために当然に当事者間に発生する（物権変動的登記請求権）。</p>
          <p class="p6 ind4">買主は、転売後でも売主に対する移転登記請求権を失わない（大判大５.４.１）。すなわち、現在の権利者だけでなく既往の権利変動の当事者にも発生する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">③ 当事者の特約</p>
          <p class="p6 ind4">債権的効果として発生する（債権的登記請求権）。</p>
          <p class="p6 ind4">賃借人は、賃借権を登記すべき特約がある場合に限り、賃借権の登記請求権を有する。債権的登記請求権は、通常の債権として消滅時効にかかる。</p>
        </div>
<div class="kakomon"><a href="pu26min15q.php#q-h07-15" title="H7-15">過去問</a></div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　登記の有効要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 形式的要件－登記が不動産登記法の定めるところに適合すること</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①登記記録に記録すること</p>
          <p class="h6 ind3 nobold">②既に登記のあるとき、これと抵触する登記は許されない。後の登記は却下</p>
          <p class="p6 ind4">される。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 実質的要件－登記が実体関係と一致すること</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 不実登記</p>
          <p class="p6 ind3">実体関係との間に不一致があれば無効である。もっとも、登記の表示によって同一性が認められるときは有効である。</p>
          <p class="p6 ind3">ex. 建物を同一敷地上において移動させた上、増築した場合であっても、登記記録上の建物と同一性を失わない限り、効力を失わない（最判昭31.７.20）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 中間省略登記</p>
          <p class="p6 ind3">中間省略登記とは、ＡＢＣの順に所有権が移転した場合に、中間者Ｂをとばして直接ＡＣ間での移転登記をすることをいう。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　中間省略登記請求の可否</p>
          <p class="p6 ind4">→　全員の合意がある場合に限り可能だとした判例がある（大判大５.９.12）。</p>
          <p class="p6 ind4">ただし、その場合でもＢはＡに対する登記請求権を失わない（最判昭46.11.30・通説）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　既にされた中間省略登記の有効性</p>
          <p class="p6 ind4">中間者Ｂの同意がない場合であっても、Ｂは抹消を求める正当な利益がない限り、抹消請求をすることはできない（最判昭35.４.21）。</p>
          <p class="p6 ind4">（理由）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 少なくとも現在の権利関係を表しており、登記法の精神はその限りでは実現されている。</p>
          <p class="h6 ind3 nobold">② 登録免許税が高いなどの理由で、現実によく行われている中間省略登記につき、その効力を一般的に否定すると混乱を生じる。</p>
          <p class="h6 ind3 nobold">③ だとしても、中間省略登記により害される中間者Ｂの利益は保護すべきである。</p>
          <p class="p6 ind4">　なお、中間省略登記によって利益を害されるのは中間者のみであるから、中間者以外の者による抹消請求は認められない（最判昭44.５.２）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 登記の流用</p>
          <p class="p6 ind3">登記の流用の可否とは、初め有効であった登記が後に実体関係を欠くこととなって無効となり、さらにその後、初めの登記に対応する実体関係を備えるに至った場合、初めの登記を後の実体の関係の登記として用いることができるか、という問題である。</p>
          <p class="p6 ind3">登記の流用について、判例上問題となったケースとしては、①抵当権の登記の流用の場合と、②滅失した建物の登記を滅失後の新築建物に流用した場合とがあるが、②については無効とされる。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　抵当権の登記の流用</p>
          <p class="p6 ind4">これを肯定すると、被担保債権の消滅にかかわらず順位を維持したままで不動産の担保価値を利用できることになるし、また、登記をする労力や費用の節約にもなる。しかし反面、公示制度や取引の安全を害する結果を招かないかが問題となる。</p>
          <p class="p6 ind4">→　大判昭11.１.14・多数説</p>
          <p class="p6 ind4">前の抵当権の登記が現在の権利状態に一致する限り、その効力を認めてさしつかえないが、流用されるまでの間に出現した第三者に対する関係では、流用された登記の効力を主張することができない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　滅失建物の登記の流用</p>
          <p class="p6 ind4">滅失建物の登記を滅失後の新築建物に流用することについては、判例・学説ともこれを否定する。</p>
          <p class="p6 ind4">（具体例）</p>
          <p class="h6 ind3 nobold">① 滅失建物の登記を滅失後の新築建物に流用</p>
          <p class="p6 ind4">　登記は無効である（最判昭40.５.４）。</p>
          <p class="h6 ind3 nobold">② 弁済等により消滅した抵当権の登記を他の債権担保に流用する契約</p>
          <p class="p6 ind4">　そのような契約は無効である（大判昭６.８.７）。</p>
          <p class="h6 ind3 nobold">③ 所有権移転請求権保全の仮登記の流用</p>
          <p class="p6 ind4">　所有権移転請求権保全の仮登記移転付記登記後に目的不動産上に利害関係を取得した第三者は原則として無効の主張ができない（最判昭49.12.24）。</p>
        </div>
        </div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　登記の効力</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 推定力</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 登記の記載事項は一応真実と推定（反証でくつがえされる）。</p>
          <p class="h6 ind3 nobold">② 占有の推定力（188条）との関係</p>
          <p class="p6 ind4">判例は、登記に推定力を認めつつも（最判昭34.１.８）、登記のある不動産についても、一般に188条を適用する（大判大13.９.25）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 形式的確定力</p>
          <p class="p6 ind2">登記が存在するときは、不適法なものであっても抹消しない限り登記できない。</p>
