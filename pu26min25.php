          <p class="p6 ind2">存続期間は、抵当権設定当時の建物の状態等を考慮して決定する必要がある（268条２項）。合意がなければ、借地借家法３条が適用され、「30年」となる。</p>
          <p class="p6 ind2">地代について当事者の協議が整わない時は、裁判所が決定する（388条後段）。</p>
        </div>
