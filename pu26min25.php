        <div class="p-card" id="min25-q32">
          <p class="h6 ind0">4.　一般債権者の差押えによる法定地上権成立</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">土地及びその上にある建物が債務者の所有に属する場合において、その土地又は建物の差押えがあり、その売却により所有者を異にするに至ったときは、その建物について、地上権が設定されたものとみなす（民事執行法81条前段）。この場合の地代は、当事者の請求により、裁判所が定める（民事執行法81条後段）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 仮差押後の競売と法定地上権</p>
          <p class="p6 ind2">地上建物に仮差押えがされ、その後、当該仮差押えが本執行に移行してされた強制競売手続における売却により買受人がその所有権を取得した場合において、土地及び地上建物が当該仮差押えの時点で同一の所有者に属していたときは、その後に土地が第三者に譲渡された結果、当該強制競売手続における差押えの時点では土地及び地上建物が同一の所有者に属していなかったとしても、法定地上権が成立する（最判平28.12.1）。</p>
          <p class="p6 ind2">（理由）</p>
          <p class="p6 ind2">法定地上権の趣旨は、土地と地上建物が同一人所有に属する場合には、土地の使用権を設定することができず、強制競売によって土地と地上建物の所有者が別人となったときに法定地上権を成立させることにより、地上建物の除去という社会経済的損失を防止するものである。</p>
          <p class="p6 ind2">仮差押えの時点で土地と地上建物が同一人所有に属していた場合も、土地の使用権を設定することはできず、その後に土地を第三者に譲渡したとしても地上建物について土地の使用権を設定するとは限らない。この場合に当該仮差押えが本執行に移行してされた強制競売手続により買受人が取得した地上建物につき法定地上権を成立させることは、法定地上権の趣旨に沿うものである。</p>
        </div>
