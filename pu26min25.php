      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">土地と建物が同一の所有者に属する場合に、そのいずれかに抵当権が設定され、その実行により、土地又は建物の所有者が別人となったときは、土地所有者である抵当権設定者は、その際に地上権を設定したものとみなす制度である（388条）。</p>
          <p class="p6 ind1">本条は強行規定であるから、法定地上権の成立を認めないという趣旨の特約は効力を有しない（大判明41.５.11）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　趣旨</p>
          <p class="p6 ind1">民法上、土地と建物は別個の不動産とされるので、両者は独立して抵当権の目的となり得る。そのため、両者の一方に抵当権が設定されて、実行により所有者を異にすることが容易に生じ得る。しかし、現行民法では、自己借地権の設定は認められていない。そこで、抵当権の実行によって土地と建物が別人の所有に属した場合、土地利用権を有さない建物が出現するおそれがある。しかし、このような場合に建物を収去しなければならないとするのは社会経済上不利益であるし、当事者の合理的意思にも反する。そこで、このような不利益を回避するため法定地上権の制度を定めた。</p>
        </div>
