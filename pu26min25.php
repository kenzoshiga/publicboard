        <div class="p-card" id="min25-q37">
          <p class="h6 ind0">6.　一括競売（389条）</p>
          <p class="p6 ind1">更地に抵当権を設定した後に建築された建物のためには法定地上権（388条）は成立しないから、本来ならば、抵当権者は建物の存在を無視して、更地として競売することができる。しかし、建物が現に存在するのに土地だけを競売することは困難であるし、建物収去を強制することは社会経済的に不利益となる。そこで、土地抵当権者に一括競売権を与えた（389条１項本文）。</p>
          <p class="p6 ind1">この一括競売は、抵当権設定後に建物を築造した者が、抵当権設定者であるか否かを問わずに認められる。</p>
          <p class="p6 ind1">ただし、抵当権者が不当に利益を得ることは避ける必要があるので、土地の競売価格だけから優先弁済を受けるとした（389条１項ただし書）。</p>
          <p class="p6 ind1">もっとも、本条は抵当権者に一括競売を義務づけるものではないから、抵当権者は一括競売せずに土地だけを競売することもできる（大判大15.２.５）。</p>
