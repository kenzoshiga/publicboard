          <p class="p6 ind2">法定地上権の及ぶ範囲は、建物の敷地のみならず、建物の利用に必要な土地を含む（大判大９.５.５）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 存続期間・地代</p>
