    <div class="p-card rank4-card">
      <p id="A01-06-04-3" class="h6 ind0">Ⅲ　強迫による意思表示（96条）</p>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-3-01">１　意義</div>
      <p>強迫：他人に害意を告知し、恐怖を感じさせる行為</p>
      <p>強迫による意思表示：相手に畏怖を生じさせ、それによってなされる意思表示強迫による意思表示は、取り消すことができる（96条１項）。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-3-02">２　要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　故意</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 相手方に畏怖を生じさせようとする意思と、②畏怖によって意思表示をさせようとする意思との二重の故意があること</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　強迫行為</p>
          <p class="p6 ind1">相手方に畏怖を生じさせる行為であること</p>
          <p class="p6 ind1">行為の態様には制限がなく、沈黙・不作為も強迫となり得る。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　表意者が強迫行為によって畏怖を生じ、その畏怖によって強迫者が欲した意思表示をすること</p>
          <p class="p6 ind1">強迫の結果、完全に意思の自由を失った者の意思表示は当然に無効となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　強迫が違法性のあるものであること</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-3-03">３　効果</div>
      <p>強迫による意思表示は常に取り消すことができる（96条１項）。</p>
      <p>第三者による強迫であっても取り消すことができる（96条２項の反対解釈）。</p>
      <p>善意無過失の第三者に対しても対抗することができる（96条３項の反対解釈）。</p>
      <p>※　詐欺に比べて、表意者保護の要請が強いために、意思主義が徹底されている。</p>
