    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-2-03">３　効果</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　原則－取消し可</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 通常の詐欺の場合</p>
          <p class="p6 ind2">詐欺による意思表示は取り消すことができる（96条１項）。</p>
          <p class="p6 ind2">表示に対する効果意思そのものは存在しており、その効果意思を形成する過程（動機の段階）に、他人の不当な干渉が加わるという欠陥（瑕疵という）があるにすぎないからである。</p>
          <p class="p6 ind2">取消しによって、行為は初めから無効となる（取消しの遡及効、121条本文）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 第三者による詐欺の場合</p>
          <p class="p6 ind2">第三者による詐欺の場合、相手方が詐欺の事実を知り、又は知ることができた場合のみ取り消すことができる（96条２項）。</p>
        </div>
<div class="kakomon"><a href="pu26min06q.php#q-h18-06-o" title="H18-6-オ他">過去問</a></div>
        </div>
        <div class="p-card" id="min06-q18">
          <p class="h6 ind0">2.　例外－第三者保護</p>
          <p class="p6 ind1">取消しの効果は善意無過失の第三者に対抗することができない（96条３項）。</p>
<div class="kakomon"><a href="pu26min06q.php#q-r05-05-e" title="R5-5-エ他">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(1) 第三者の意義</p>
          <p class="p6 ind2">善意無過失の第三者とは、詐欺の事実を知らないで、詐欺による法律行為に基づいて取得された権利関係について新たな利害関係に入った者で、詐欺の事実を知らないことに過失のない者をいう。</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 第三者はいつまでに利害関係に入ることを要するか。</p>
          <p class="p6 ind3">→　96条３項にいう第三者とは取消し前に利害関係を有するに至った者に限られる（大判昭17.９.30・通説）。</p>
          <p class="p6 ind3">（理由）</p>
          <p class="p6 ind3">同条項は取消しの遡及効を制限して取引の安全を図る趣旨であるから、取消しによって影響を受くべき者に限られると解すべきである。</p>
        </div>
        <div class="p-card" id="min06-q23">
          <p class="h6 ind2 nobold">(b) 取消し後の第三者の保護</p>
          <p class="p6 ind3"><img class="embed-img" src="img/min06-02.png" alt="min06-02.png"></p>
          <p class="p6 ind3">本来、取消しによってＡＢ間の売買は遡及的に無効となり、ＢＣ間の売買は他人物売買であってＣは土地所有権を取得できないとも思える（561条参照）。また、この場合、Ｃは取消し後に利害関係に入った第三者であるから、96条３項で保護することはできない。</p><br>
          <p class="p6 ind3">Ｑ　では、Ｃは一切保護されないのか。</p><br>
          <p class="p6 ind3">Ａ　177条説（大判昭17.９.30・通説）</p>
          <p class="p6 ind3">　ＡとＣは対抗関係に立ち、ＣはＡに先んじて登記を備えれば保護される。</p>
          <p class="p6 ind3">（理由）</p>
          <p class="h6 ind3 nobold">　① 取消しによる遡及的無効という効果は、契約の効果を否定するための擬制にすぎず、現実には取消しまでは有効であり、取消しにより物権が復帰すると考えるべきである（上記の例でいえば、ＢからＡへの復帰的物権変動が生じ、これとＢからＣへの譲渡とが二重譲渡類似の関係に立つといえる）。</p>
          <p class="h6 ind3 nobold">　② いったん取り消した以上、迅速に登記を回復して権利変動を公示すべきであるので、これを怠った者が権利を失うのはやむを得ない。</p>
          <p class="p6 ind4">（批判）</p>
          <p class="h6 ind3 nobold">　① 取消し前の第三者については遡及的無効が生ずることを前提にしながら、取消し後の第三者についてはこれを無視しているが、矛盾である。</p>
          <p class="h6 ind3 nobold">　② 悪意の第三者であっても登記を了しさえすれば保護されることになり不都合である（特に、取消し前の第三者が一切保護されない強迫の場合、取消し前の第三者は善意無過失であっても保護されないのに、取消し後の第三者は悪意であっても保護されることになる）。</p><br>
<div class="kakomon"><a href="pu26min06q.php#q-r05-07-e" title="R5-7-エ他">過去問</a></div>
          <p class="p6 ind4">Ｂ　94条２項類推適用説（有力説）</p>
          <p class="p6 ind4">　取消し一般につき取消し時を基準として、取消し後は94条２項類推適用により第三者を保護すべきであるとする。</p>
          <p class="p6 ind4">（理由）</p>
          <p class="h6 ind3 nobold">　① 取消しの遡及効に適合的である（取消し前と後とで取消しの効果を同一に扱うことができる）。</p>
          <p class="h6 ind3 nobold">　② 第三者の善意・悪意、過失の有無を考慮したきめ細やかな調整が可能である。</p>
          <p class="p6 ind4">（批判）</p>
          <p class="p6 ind4">　被欺罔者には、虚偽の外観を作出した者ほどの帰責性はなく、94条２項類推適用の基礎を欠く。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 第三者の対抗要件の要否</p><br>
          <p class="p6 ind2">Ａ　不要説（最判昭49.９.26）</p>
          <p class="p6 ind2">（理由）</p>
          <p class="h6 ind3 nobold">　① 第三者との関係では詐欺による意思表示も完全に有効と扱われるから、被欺罔者と第三者は前主・後主の関係に立ち、対抗関係に立たない。</p>
          <p class="h6 ind3 nobold">　② 第三者は物権の取得者に限らない。</p><br>
          <p class="p6 ind4">Ｂ　必要説</p>
          <p class="p6 ind4">（理由）</p>
          <p class="h6 ind3 nobold">　① 第三者は、その取得した権利を誰に対しても主張することができるようにしておくべきである（公示制度の重視）。</p>
          <p class="h6 ind3 nobold">　② 第三者への不動産の譲渡時期が被欺罔者による取消しの前後を問わず、同様の扱いをすべきである。</p>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-2-04">４　錯誤と詐欺</div>
      <p>Ｑ　動機の錯誤が95条にいう「錯誤」に当たると解する立場から、また、判例の立場によっても動機が表示されていた場合には、詐欺によって生じた錯誤が「要素の錯誤」に該当するときには詐欺取消しと錯誤取消しの両方の要件をみたすことになる。そのような場合、表意者は詐欺取消しと錯誤取消しとの選択的主張をすることができるのか。</p>
      <p>→　二重効肯定説（通説）</p>
      <p>表意者はいずれかを選択して主張できる。</p>
      <p>（理由）</p>
      <p>詐欺取消しも錯誤取消しも、表意者保護のために法律行為（意思表示）の効力を否定する理論構成の一つにすぎない。</p>
