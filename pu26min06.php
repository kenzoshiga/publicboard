      <p id="A01-06-04-2" class="h6 ind0">Ⅱ　詐欺による意思表示（96条）</p>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-2-01">１　意義</div>
      <p>詐欺：欺くこと（欺罔）によって人を錯誤に陥れること</p>
      <p>詐欺による意思表示：相手方又は第三者に欺かれて、錯誤に陥り、その錯誤によってなされる意思表示</p>
      <p>詐欺による意思表示は、取り消すことができる（96条１項）。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-04-2-02">２　要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　故意</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 相手方を欺いて錯誤に陥れようとする意思と、② 錯誤によって意思表示をさせようとする意思との二重の故意があること</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　欺く行為（欺罔行為）</p>
          <p class="p6 ind1">積極的な作為に限らず、沈黙も詐欺となり得る。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　表意者が欺く行為によって錯誤に陥り、その錯誤によって詐欺者が欲した意思表示をすること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　詐欺が違法性のあるものであること</p>
          <p class="p6 ind1">社会生活上、多少の欺く行為は放任されるべきだから、違法性のあるものでなければ詐欺とはならない。</p>
          <p class="p6 ind1">この点で、沈黙・意見の陳述などは詐欺とならないことが多いが、例えば、不動産の所有者が、抵当権が設定され登記されていることを秘して高価に売却した場合は詐欺となる（大判昭４.３.７）。</p>
        </div>
