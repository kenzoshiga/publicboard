      <p id="A08-04-04-2" class="h6 ind0">Ⅱ　普通養子縁組の効果</p>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-2-01">１　養親子関係の創設</div>
      <p>普通養子縁組の基本的効果は、当事者間に養親子関係を創設することである。</p>
      <p>そこで、養子は縁組の日から嫡出子たる身分を取得し（809条）、これに伴い、養子は養親の血族との間に法定血族関係を生じる（727条）。</p>
      <p>養子縁組は、養親側に養子だけを取り込む制度であるため、縁組より前に養子に生じている身分関係は、養親には関係がない。したがって、養子縁組より前に生まれた養子の直系卑属と養親との間には親族関係は生じない（大判昭7.5.11）。</p>
<div class="kakomon"><a href="pu26min57q.php#q-r05-20-e" title="R5-20-エ">過去問</a></div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-2-02">２　実親との親族関係</div>
      <p>養子は、縁組によって実親との親族関係を失うことがないから、実方の親族との親族関係には何らの影響もない。実親子の間でも相続権を有する。</p>
<div class="kakomon"><a href="pu26min57q.php#q-h24-20-o" title="H24-20-オ">過去問</a></div>
    </div>
    </div>
    <div class="p-card rank4-card">
      <p id="A08-04-04-3" class="h6 ind0">Ⅲ　普通養子縁組の解消</p>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-3-01">１　協議離縁</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　成立</p>
          <p class="p6 ind1">縁組の当事者は、その協議で離縁することができる（811条１項）。この協議離縁は、協議離婚と同様に当事者の合意と届出（812条、739条１項）だけで成立する。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 能力</p>
          <p class="p6 ind2">協議離縁の能力は、意思能力があれば足りるとされるが、15歳以上の者は有するとされ、成年被後見人も意思能力を回復していれば後見人の同意を得ずに離縁することができる（812条、738条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 未成年者養子との離縁</p>
          <p class="p6 ind2">養子が15歳未満のときは養親と離縁後に法定代理人となるべき者（多くの場合実父母）が、代諾者となり、その者との協議によってする（811条２項）。</p>
          <p class="p6 ind2">養親が夫婦である場合に未成年者である養子と離縁するには、夫婦の一方が意思表示をすることができない場合を除き、夫婦が共にしなければならない（811条の２）。未成年者を養子とする場合に子の健全な育成のために夫婦の共同縁組を必要とした趣旨を貫くためである。</p>
          <p class="p6 ind2">養子が15歳未満である場合の離縁において、養子の父母が離婚しているときは、その協議で、その双方又は一方を養子の離縁後にその親権者となるべき者と定めなければならない（改正民法811条３項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 死後離縁</p>
          <p class="p6 ind2">縁組の一方当事者が死亡した場合にも、生存している当事者が離縁をすることはできるが、家庭裁判所の許可が必要である（811条６項）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　無効・取消し</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 協議離縁が無効な場合は、真の離縁の合意がないとき、合意があっても権限のない者の代諾である場合、届出が出されていない場合である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 協議離縁の取消しは、詐欺又は強迫により離縁した場合に限られ、詐欺を発見し、若しくは強迫を免れた後６か月を経過し、又は追認をしたときは、取消請求できない（812条、747条）。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h21-22-i" title="H21-22-イ">過去問</a></div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-3-02">２　裁判離縁</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　離縁原因</p>
          <p class="p6 ind1">縁組の一方当事者は、以下の３つの場合に限り、離縁の訴えを提起できる（814条１項）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①　他の一方から悪意で遺棄されたとき</p>
          <p class="h6 ind3 nobold">②　他の一方の生死が３年以上不明なとき</p>
          <p class="h6 ind3 nobold">③　その他縁組を継続し難い重大な事由があるとき</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(1) 有責当事者からの離縁請求</p>
          <p class="p6 ind2">離縁については、有責当事者からの離縁請求は許されない（最判昭39.８.４）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 請求の棄却</p>
          <p class="p6 ind2">上記①・②にあたる具体的事由があっても、裁判所は一切の事情を考慮して養親子関係の継続を相当と認めるときは、離縁の請求を棄却することができる（814条２項、770条２項）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　手続</p>
          <p class="p6 ind1">離縁の訴えは、人事訴訟法に従って審理され、判決によって離縁の効力が生じる。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-3-03">３　離縁の効果</div>
      <p>離縁の効果は、協議離縁と裁判離縁とで差異はない。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　親族関係の消滅</p>
          <p class="p6 ind1">養親子間の法定嫡出親子関係の消滅とともに、血族関係も消滅する。</p>
          <p class="p6 ind1">※　ただし、養親子関係があることによる婚姻障害は、離縁後もなお残存する（736条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　復氏と復籍</p>
          <p class="p6 ind1">養子は、原則として縁組前の氏に復する（816条１項本文）。ただし、配偶者とともに養子をした養親の一方のみと離縁した場合は、復氏しない（同条項ただし書）。</p>
          <p class="p6 ind1">なお、縁組の日から７年以上経過した後に離縁により復氏した者は、離縁の日から３か月以内に戸籍法の定めるところにより届け出ることによって、離縁の際に称していた氏を称することができる（816条２項）。</p>
        </div>
