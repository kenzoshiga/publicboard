    </div>
    <div class="p-card rank4-card">
      <p id="A08-04-04-4" class="h6 ind0">Ⅳ　特別養子</p>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-4-01">１　意義</div>
      <p>特別養子制度は、孤児や非嫡出子などの要保護児の適正な養護、養子をすべての関係において実子として取り扱いたいという社会的要請などから創設された。特別養子縁組が成立すると、実親族との親族関係は終了する。また、戸籍上、一見しただけでは養子であることが分からないようになっている。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-4-02">２　特別養子の成立</div>
      <p>特別養子縁組は、もっぱら子の福祉の増進を図るものであり、かつ、実親との親族関係を消滅させるものであるから、かなり厳格な要件が要求される。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　家庭裁判所の審判</p>
          <p class="p6 ind1">特別養子は当事者間の合意ではなく、養親となる者の請求により、以下の要件を判断した家庭裁判所の審判によって成立する（817条の２）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 夫婦共同縁組</p>
          <p class="p6 ind2">養親は配偶者のある者で、かつ、原則として夫婦がともに養親とならなければならない（817条の３第１項、２項本文）。ただし、夫婦の一方が他方の嫡出子（普通養子を除く）を養子とする場合（いわゆる、連れ子養子）は単独で養親となることができる（同条２項ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 養親の年齢</p>
          <p class="p6 ind2">養親は25歳以上でなければならない（817条の４本文）。ただし、養親となる夫婦の一方が25歳に達している場合には、他の一方は20歳以上であれば足りる（同条ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 養子の年齢</p>
          <p class="p6 ind2">原則：養子は、家庭裁判所への縁組請求の時に１５歳未満でなければならない（817条の5第1項前段）。</p>
          <p class="p6 ind2">例外：養子となる者が、①１５歳に達する前から養親候補者が引き続き養育を受けており、かつ、②１５歳に達するまでに申立てができなかったことにやむを得ない事由がある場合には、１５歳以上であってもよい（817条の5第2項）。</p>
          <p class="p6 ind2">※　審判確定時における制限</p>
          <p class="p6 ind2">養子は、審判確定時に１８歳未満でなければならない（817条の5第1項後段）。</p>
          <p class="p6 ind2">※　養子候補者の同意</p>
          <p class="p6 ind2">養子候補者が、審判時に１５歳に達している場合には、養子候補者の同意がなければ特別養子縁組は成立しない（817条の5第3項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 父母の同意</p>
          <p class="p6 ind2">実方の父母の同意が必要となる（817条の６本文）。ただし、父母が意思表示できない場合又は父母による虐待、悪意の遺棄その他養子となることができる者の利益を著しく害する事由がある場合には不要である（同条ただし書）。</p>
<div class="ftnote">
ｃｆ．同意の撤回権の制限（家事事件手続法164条の2第5項）
</div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(5) 必要性と試験養育期間</p>
          <p class="p6 ind2">特別養子縁組は、父母による養子となる者の監護が著しく困難又は不適当であることその他特別の事情がある場合において、子の利益のため特に必要があると認められるときにだけ成立する（817条の７）。</p>
          <p class="p6 ind2">また、養親が必要な監護能力・適格性を備えているか、養子との和合可能性があるかを家庭裁判所が判断するために、６か月の試験養育期間が設けられている（817条の８）。</p>
        </div>
