      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　審判による離縁</p>
          <p class="p6 ind1">特別養子の離縁は原則として認められず、①養親による虐待、悪意の遺棄その他養子の利益を著しく害する事由があり、かつ②実父母が相当の監護をすることができる場合で、養子の利益のため特に必要があると認められるときにのみ、家庭裁判所の審判によって認められる（817条の10）。</p>
          <p class="p6 ind1">離縁の請求は、養子、実父母、又は検察官からすることができ、養親からは請求できない（817条の10第１項柱書）。</p>
