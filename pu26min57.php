  <div class="p-card chap-card">
    <h2 id="A08-04">第４章　親子・親権</h2>
    <div class="p-card sec-card">
      <h3 id="A08-04-04">第４節　養子</h3>
    <div class="p-card rank4-card">
      <p id="A08-04-04-1" class="h6 ind0">Ⅰ　普通養子縁組の成立及び無効・取消し</p>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-04-1-01">１　普通養子縁組の成立要件</div>
      <p>普通養子縁組における養親子関係は縁組によって発生する。縁組の成立要件を図示すると以下のようになる。</p>
      <p><img class="embed-img" src="img/min57-01.png" alt="img/min57-01.png"></p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　実質的要件</p>
        <div class="p-card" id="min57-q1">
          <p class="h6 ind1">(1) 縁組意思の合致</p>
          <p class="p6 ind2">縁組をする意思とは、真に親子関係を創設しようとする意思（実質的意思説）であると解されるので、単に縁組届出をするだけという、何らかの方便のための縁組（ex. 労働基準法の適用や学区制を潜脱するため）は、縁組意思を欠き無効である（802条１号）。</p>
          <p class="p6 ind2">縁組意思の合致は、縁組届出時に存在しなければならない（通説）。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h24-20-eh19-22-a" title="H24-20-エ">過去問</a></div>
        <div class="p-card" id="min57-q2">
          <p class="h6 ind1">(2) 養親の年齢</p>
          <p class="p6 ind2">養親となる者は20歳（※）に達していなければならない（792条）。養親の年齢制限はこれだけである。同条違反は取消事由となる（804条）。</p>
          <p class="p6 ind2">※　成年に達する＝養親となれるのではないことに注意。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h24-20-ah01-19-a" title="H24-20-ア">過去問</a></div>
        <div class="p-card" id="min57-q3">
          <p class="h6 ind1">(3) 尊属養子・年長者養子の禁止</p>
          <p class="p6 ind2">養子となる者が養親となる者の尊属又は年長者であってはならない（793条）。同条違反は取消事由となる（805条）。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h24-20-ih01-21-1" title="H24-20-イ">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(4) 後見人が被後見人を養子とする場合</p>
          <p class="p6 ind2">後見人が被後見人を養子とする場合には、家庭裁判所の許可が必要である（794条）。被後見人の財産管理が不正に行われることを防ぐためである。無許可の場合、取消事由となる（806条）。</p>
        </div>
        <div class="p-card" id="min57-q4">
          <p class="h6 ind1">(5) 未成年養子</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 家庭裁判所の許可</p>
          <p class="p6 ind3">未成年者（18歳未満の者。4条）を養子とするには、家庭裁判所の許可が必要である（798条本文）。養子となる未成年者の保護のためである。ただし、自己又は配偶者の直系卑属を養子とする場合は除く（同条ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 代諾縁組</p>
          <p class="p6 ind3">養子となる者が15歳未満であるときは、その法定代理人がこれに代わって、縁組の承諾をすることができる（797条１項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 同意を要する場合</p>
          <p class="p6 ind3">法定代理人が縁組の承諾をするには、養子となる者の父母でその監護をすべき者であるものが他にあるときは、その同意を得なければならない（797条２項前段）。養子となる者の父母の親権が停止されていても同意を要する（797条２項後段）。</p>
          <p class="p6 ind3">適法な代諾を欠く縁組は一種の無権代理で無効であり、養子が15歳に達した後は追認できる（最判昭27.10.３）。</p>
          <p class="p6 ind3">縁組をすることが子の利益のため特に必要であるにもかかわらず、上記の者が縁組の同意をしないときは、家庭裁判所は、養子となる者の法定代理人の請求により、その同意に代わる許可を与えることができる（改正民法797条３項）。この請求を受けた家庭裁判所は、代諾縁組（同条１項）をすることが子の利益のため特に必要であると認めるときに限り、同条３項の規定による審判をすることができる（改正民法797条４項）。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-r04-20-e" title="R4-20-エ">過去問</a></div>
        </div>
        <div class="p-card" id="min57-q8">
          <p class="h6 ind1">(6) 配偶者のある者の縁組</p>
          <p class="p6 ind2">配偶者のある者が養子縁組をすると、他方配偶者にも大きな影響を与える可能性があるので（相続、扶養、現実の生活等）、その利益の保護、意思の尊重のため、以下の要件が定められている。</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 養子が未成年者の場合</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　原則</p>
          <p class="p6 ind4">夫婦共同縁組（795条本文）―　未成年者の適切、円滑な養育のためである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　例外</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">①夫婦の一方が他方の嫡出子を養子とする場合には、その他方の同意で足りる（795条ただし書、796条本文）。</p>
          <p class="p6 ind4">嫡出子を養子とすることは一般にできないことと解されているからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">②配偶者が表意不能（心神喪失、行方不明等）の場合には、同意不要である（795条ただし書、796条ただし書）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 養子が成年者の場合</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　原則</p>
          <p class="p6 ind4">配偶者の同意があれば、夫婦一方の単独縁組が可能である（796条本文）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　例外</p>
          <p class="p6 ind4">配偶者とともに縁組する場合、又は配偶者が表意不能の場合には、同意不要である（796条ただし書）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 要件を欠く縁組の効力</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　共同縁組に反する場合</p>
          <p class="p6 ind4">原則として無効である。ただし、配偶者の利益、子の福祉等の点から795条の趣旨に反しない特段の事情がある場合には、縁組意思のある夫婦の一方と養子との間の縁組は有効である（最判昭48.４.12）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　同意がない場合</p>
          <p class="p6 ind4">当然には無効とならず（802条参照）、取消事由となる（806条の２）。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h20-21-uh13-20-i" title="H20-21-ウ">過去問</a></div>
        </div>
        </div>
        </div>
        <div class="p-card" id="min57-q12">
          <p class="h6 ind0">2.　形式的要件－届出</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 届出</p>
          <p class="p6 ind2">縁組は、婚姻と同様、戸籍法の定めるところにより、これを届け出ることによって成立する（799条、739条）。届出に関する問題はほとんど婚姻届の場合と同じである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 虚偽の出生届による縁組の成否</p>
          <p class="p6 ind2">Q1　自分の子をいったん他人の子として届け出たうえ、その戸籍上の親の代諾によって縁組届出をする場合</p>
          <p class="p6 ind2">→　養子が15歳に達した後に適法な追認をすれば、縁組は初めから有効なものとなる（最判昭27.10.３）。</p><br>
          <p class="p6 ind2">Q2　他人の子をもらって自分の嫡出子として届け出る場合（いわゆる「藁(わら)の上からもらって育てる養子」）</p>
          <p class="p6 ind2">→　嫡出親子関係はもちろん、養親子関係も生じない（最判昭25.12.28）。</p>
          <p class="p6 ind2">ただし、戸籍上の父母とその嫡出子として記載されている者との間で55年間も存続した事実上の実親子関係について、父母の子による親子関係不存在確認請求が権利の濫用に当たり得るとした判例がある（最判平18.７.７）。</p>
        </div>
