      <div class="outer-title" id="A08-04-04-4-03">３　特別養子の効果</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　養方との関係</p>
          <p class="p6 ind1">養親の嫡出子としての身分の取得、養方の血族との親族関係の成立は普通養子と同様である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　実方との断絶</p>
          <p class="p6 ind1">特別養子縁組が成立すると、審判確定の時から、養子と実方の父母及び血族との親族関係は終了する（817条の９）。</p>
          <p class="p6 ind1">これにより、特別養子は民法その他の法令の適用上も実方の親族ではないものとして扱われる（ただし、近親婚の禁止の規定（734条、735条）はなお適用される）。</p>
