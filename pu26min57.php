      <div class="outer-title" id="A08-04-04-1-03">３　縁組の取消し</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　取消しの方法</p>
          <p class="p6 ind1">縁組の取消しは、民法の定める取消事由がある場合で（803条）、かつ、取消権者が家庭裁判所に請求するという方法（すなわち訴訟）によってのみ可能である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　取消事由・取消権者・取消しの制限</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 養親が20歳未満である場合（804条）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―養親又はその法定代理人</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―養親が20歳に達した後６か月を経過し、又は追認をしたときは取消請求できない。</p>
        </div>
        </div>
        <div class="p-card" id="min57-q15">
          <p class="h6 ind1">(2) 尊属又は年長者を養子とした場合（805条）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―各当事者又はその親族</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―なし</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h31-21-a" title="H31-21-ア">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 後見人が被後見人を家庭裁判所の許可を得ないで養子とした場合（806条）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―養子又はその実方の親族</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―管理の計算が終わった後、養子が成年に達し又は行為能力を回復した時から、追認をし、又は６か月が経過したときは取消請求できない。</p>
        </div>
        </div>
        <div class="p-card" id="min57-q16">
          <p class="h6 ind1">(4) 配偶者の同意がない場合（806条の２第１項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―縁組の同意をしていない配偶者</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―当該配偶者が、縁組を知った後６か月を経過し、又は追認したときは取消請求できない。</p>
        </div>
<div class="kakomon"><a href="pu26min57q.php#q-h26-20-o" title="H26-20-オ">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(5) 詐欺・強迫によって同意した場合（806条の２第２項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―詐欺・強迫によって同意した者</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―詐欺を発見し、若しくは強迫を免れた後６か月を経過し、又は追認をしたときは取消請求できない。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(6) 代諾縁組において監護権者の同意がない場合（806条の３第１項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―縁組の同意をしていない者</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―同意をしていない者が追認したとき、又は養子が15歳に達した後６か月を経過し、若しくは追認したときは取消請求できない。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(7) 代諾縁組において、詐欺・強迫によって同意した場合（806条の３第２項）</p>
          <p class="p6 ind2">上記(5)と同じである（806条の２第２項を準用）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(8) 家庭裁判所の許可を得ないで未成年者を養子とした場合（807条）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　取消権者―養子、その実方の親族又は養子に代わって縁組の承諾をした者</p>
          <p class="h6 ind3 nobold">ⅱ　取消しの制限―養子が成年に達した後６か月を経過し、又は追認したときは取消請求できない。</p>
          <p class="p6 ind4">※　婚姻と異なり、検察官は取消権者ではない。縁組の取消事由の多くは公益的理由ではなく、当事者の一方を保護する趣旨のものだからである。</p>
        </div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　取消しの効果</p>
          <p class="p6 ind1">婚姻の取消しと同じく、身分関係については遡及せず、将来に向かってのみ法定親子関係を消滅させる（808条１項前段、748条１項）。</p>
          <p class="p6 ind1">原則として養子が縁組前の氏に復すること（808条２項、816条）等も離婚・離縁の場合と同様である。</p>
          <p class="p6 ind1">また、縁組により生じた親族関係は当然に終了し、未成年者である養子に対する養親の親権は消滅し、実親の親権が復活する。</p>
        </div>
