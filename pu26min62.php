          <p class="p6 ind2">被相続人は、遺言で、相続開始の時から５年を超えない期間を定めて、遺産の分割を禁止することができる（908条１項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 分割禁止の契約（908条２項）</p>
          <p class="p6 ind2">共同相続人間で遺産の全部又は一部について分割をしない旨の契約をすることができる（908条２項）。ただし、禁止期間に関する制限がある。</p>
