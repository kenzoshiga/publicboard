          <p class="p6 ind2">遺産の分割前に遺産に属する財産が処分された場合であっても、処分した相続人を除く共同相続人全員の同意により、当該処分された財産が遺産分割時に遺産として存在するとみなすことができる（906条の２）。</p>
          <p class="p6 ind2">同条の趣旨は、相続開始後に共同相続人の一人が遺産に属する財産を処分した場合に生ずる計算上の不公平を是正することにある。</p>
          <p class="p6 ind2">遺産に属する財産を処分したのが共同相続人以外の第三者であっても、この規定の適用がある。</p>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A09-04-02-0-03">３　遺産分割の方法</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　総説</p>
          <p class="p6 ind1">遺産分割には様々な方法がある。まず、遺言による分割方法の指定があればそれに従って分割しなければならない（指定分割）。次に、遺言による指定がない場合には共同相続人間の協議によって分割することになる（協議分割）。さらに、協議が調わないか、協議することができない場合には、相続人の申立てによって家庭裁判所による分割が行なわれる（審判分割）。もっとも、相続人は、審判分割の請求に先立って、家庭裁判所に調停（調停分割）の申立てをしなければならない（家事事件手続法257条）。</p>
        </div>
        <div class="p-card" id="min62-q19">
          <p class="h6 ind0">2.　指定分割</p>
          <p class="p6 ind1">被相続人が遺言によって分割方法を指定し、又は、相続人以外の第三者に分割方法の指定を委託した場合には、それに従って分割が行われる（908条１項）。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 「相続させる」旨の遺言</p>
          <p class="p6 ind2">判例は、特定の相続人に「相続させる」旨の遺言があった場合には、この遺言は分割方法の指定であると同時に、指示された相続財産は遺産分割手続を待つことなく、直ちに当該相続人に相続によって承継取得されるとする（最判平３.４.19）。</p>
          <p class="p6 ind2">ただし、法定相続分を超える部分については、対抗要件を備えなければ第三者に対抗できない（899条の２）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 指定と異なる分割</p>
          <p class="p6 ind2">分割方法の指定があっても、遺言執行者が存在しない限り、共同相続人全員の合意（協議分割）によって、指定と異なる分割をすることも可能である。</p>
        </div>
<div class="kakomon"><a href="pu26min62q.php#q-h11-22-i" title="H11-22-イ">過去問</a></div>
        </div>
        <div class="p-card" id="min62-q22">
          <p class="h6 ind0">3.　協議分割</p>
          <p class="p6 ind1">共同相続人は、被相続人が遺言で禁じた場合又は分割をしない旨の契約（908条１項、２項）をした場合を除き、いつでも、遺産の全部又は一部について、協議分割をすることができる（907条１項）。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 協議の当事者</p>
          <p class="p6 ind2">分割協議には共同相続人全員の参加が必要であり、一部の相続人を除外してなされた分割協議は無効である。</p>
          <p class="p6 ind2"><img class="embed-img" src="img/min62-02.png" alt="img/min62-02.png"></p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 相続人の中に未成年者がいる場合</p>
          <p class="p6 ind3">相続人の中に未成年者とその親権者とがいる場合には、分割協議は利益相反行為になるから、未成年者のために特別代理人を選任しなければならない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 相続開始後に新たに子と推定された者の価額の支払請求権（778条の４）</p>
          <p class="p6 ind3">相続の開始後、774条の規定により否認権が行使され、772条４項の規定により読み替えられた同条第３項の規定により新たに被相続人がその父と定められた者が相続人として遺産の分割を請求しようとする場合において、他の共同相続人が既にその分割その他の処分をしていたときは、当該相続人の遺産分割の請求は、価額のみによる支払の請求により行うものとする（778条の４）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 協議の無効・取消し</p>
          <p class="p6 ind2">分割協議に参加した相続人が無資格者であった場合、相続人の一部を除いて分割協議がなされた場合には、分割協議は原則として無効となる。また、分割協議の意思表示につき錯誤ないし詐欺・強迫があった場合には、民法総則の規定（95条、96条）に従って、分割協議の取消しが問題となる。</p>
          <p class="p6 ind2">なお、遺産分割協議を債務不履行により解除すること、すなわち541条による解除は、認められない（最判平元.２.９）。遺産分割の安定を図る必要があるからである。もっとも、再分割の前提として、相続人全員によって協議分割を合意解除することは許されている（最判平２.９.27）。</p>
        </div>
<div class="kakomon"><a href="pu26min62q.php#q-h03-19-i" title="H3-19-イ">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　審判分割</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 審判分割の要件</p>
          <p class="p6 ind2">協議が調わないか、協議することができない場合には、相続人の申立てによって家庭裁判所による分割が行われる（907条２項本文）。</p>
          <p class="p6 ind2">ただし、遺産の一部を分割することにより他の共同相続人の利益を害するおそれがある場合には、一部の分割はできない（907条２項ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 取下げの制限</p>
          <p class="p6 ind2">相続開始の時から10年を経過した後の遺産分割の調停・審判の申立ての取下げは、相手方の同意を得なければ、その効力を生じない（改正家事事件手続法273条２項、199条２項）。</p>
          <p class="p6 ind2">相続開始から10年経過後は、原則として具体的相続分による遺産分割が制限されるため（904条の３参照）、10年経過後の取下げによる他の相続人の不利益を回避する趣旨である。</p>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A09-04-02-0-04">４　遺産分割の効力</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　分割の遡及効</p>
          <p class="p6 ind1">遺産分割の効果は、相続開始時にさかのぼって生じ、分割によって取得した個々の権利は、相続開始の時からその相続人に帰属していたことになる（909条本文）。</p>
          <p class="p6 ind1">もっとも、分割の遡及効は、分割前に個々の相続財産の持分を取得した第三者の権利を害することができない（909条ただし書）。これにより保護される第三者は、善意・悪意を問わないが、保護されるためには対抗要件を具備することを要する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　相続人の担保責任</p>
          <p class="p6 ind1">遺産分割によって取得した財産に瑕疵がある場合には、各共同相続人は、その相続分に応じて売主と同じ担保責任を負う（911条）。共同相続人間の公平を図る趣旨である。また、遺産中の債権については、分割時における債務者の資力が共同相続人によって担保される（912条１項）。</p>
          <p class="p6 ind1">もっとも、相続人の担保責任は、被相続人の遺言によって変更することができる（914条）。</p>
