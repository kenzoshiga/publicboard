          <p class="p6 ind2">遺産分割は、遺産に属する物又は権利の種類及び性質、各相続人の年齢、職業、心身の状態及び生活の状況その他一切の事情を考慮して決定する（906条）。指定相続分ないし法定相続分に従わない分割も直ちに無効となるわけではない。</p>
