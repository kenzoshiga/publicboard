      <div class="outer-title" id="A01-07-04-0-04">４　単独行為の無権代理（118条）</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　単独行為</p>
          <p class="p6 ind1">一方的意思表示により法律効果の変動を生じさせる行為</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　効果</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 相手方のない単独行為の無権代理は常に無効</p>
          <p class="p6 ind2">絶対的に無効であり、本人の追認に効力はなく、無権代理人の責任も生じない。</p>
          <p class="p6 ind2">ex. 相続の承認・放棄、所有権の放棄</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 相手方のある単独行為</p>
          <p class="p6 ind2">原則として無効であるが、以下の場合には契約に関する無権代理の規定を準用する。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①　代理権なしに行為することに相手方が同意し、又は代理権を争わなかったとき</p>
          <p class="h6 ind3 nobold">②　無権代理人が同意して相手方の意思表示を受領したとき</p>
          <p class="p6 ind4">ex. 契約の解除、債務の免除</p>
        </div>
        </div>
        </div>
      </div>
    </div>
    <div class="p-card rank4-card">
      <p id="A01-07-04-1" class="h6 ind0">Ⅱ　表見代理</p>
    <div class="blk outer">
      <div class="outer-title" id="A01-07-04-1-01">１　総説</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">無権代理の中にも、無権代理人と本人との間に代理権の存在を推測させるような客観的事情が存在し、相手方がそれを信頼して取引関係に入る場合がある。このような場合に、有権代理と同様の効果を生じさせる制度を表見代理という。すなわち、表見代理が成立する場合、本人は無権代理を理由として自分への効果帰属を拒否することができない。</p>
          <p class="p6 ind1">民法は、表見代理について３種に分けて規定している（109条、110条、112条）。</p>
          <p class="p6 ind1"><img class="embed-img" src="img/min09-01.png" alt="min09-01.png"></p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　趣旨</p>
          <p class="p6 ind1">取引の安全及び代理制度に対する信頼の保護</p>
          <p class="p6 ind1">理論的根拠は、禁反言又は権利外観理論である。すなわち、一方で、外観に対する第三者の信頼を保護し、他方で、本人の帰責事由を要求することにより、真の権利者保護と取引安全の調和を図っている。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　効果</p>
          <p class="p6 ind1">本人の有権代理的責任（本人は代理行為の効果帰属を否定できない）。</p>
          <p class="p6 ind1">ただし、有権代理とまったく同一ではない。相手方は、表見代理を主張せずに無権代理人の責任を追及することもできるし、取消権を行使することもできる（最判昭62.７.７）。もちろん、本人が追認することも可能である。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-07-04-1-02">２　代理権授与の表示による表見代理（109条）</div>
<div class="jobun">
（代理権授与の表示による表見代理等）
第109条　第三者に対して他人に代理権を与えた旨を表示した者は、その代理権の範囲内においてその他人が第三者との間でした行為について、その責任を負う。ただし、第三者が、その他人が代理権を与えられていないことを知り、又は過失によって知らなかったときは、この限りでない。
２　第三者に対して他人に代理権を与えた旨を表示した者は、その代理権の範囲内においてその他人が第三者との間で行為をしたとすれば前項の規定によりその責任を負うべき場合において、その他人が第三者との間でその代理権の範囲外の行為をしたときは、第三者がその行為についてその他人の代理権があると信ずべき正当な理由があるときに限り、その行為についての責任を負う。
</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　趣旨</p>
          <p class="p6 ind1">109条は、本人Ａが第三者Ｃに対して他人Ｂに代理権を与えた旨を「表示」した場合には、その代理権の範囲内においてＢが代理人としてＣと行った行為について責任を負わなければならない旨を規定している。</p>
          <p class="p6 ind1">実際には、代理権を授与していなかったにも関わらず「表示」がなされた場合の責任である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 表見代理人が表示された代理権の範囲内で代理行為をした場合（109条１項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 代理権の授与がないこと</p>
          <p class="h6 ind3 nobold">② 第三者に対して他人に代理権を与えた旨を表示したこと（代理権授与表示（授権表示））</p>
          <p class="h6 ind3 nobold">③ 相手方が、表見代理人に代理権がないことにつき善意無過失であること</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 表見代理人が表示された代理権の範囲外で代理行為をした場合（109条２項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 代理権の授与がないこと</p>
          <p class="h6 ind3 nobold">② 第三者に対して他人に代理権を与えた旨を表示したこと（代理権授与表示（授権表示））</p>
          <p class="h6 ind3 nobold">③ 第三者がその行為についてその他人の代理権があると信ずべき正当な理由があること</p>
        </div>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-07-04-1-03">３　権限外の行為の表見代理（110条）</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　趣旨</p>
          <p class="p6 ind1">何らかの基本代理権（基本権限）を有する者が、その代理権の範囲を越えて代理行為（権限外の行為、越権行為）をする場合である。この場合の本人が責任を負う根拠は、信用できない者を代理人に選んだ本人がリスクを負担せよということである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 基本代理権の存在（「その権限」110条）</p>
          <p class="h6 ind3 nobold">② 基本代理権を逸脱して代理行為をしたこと（「その権限外の行為をした場合」、110条）</p>
          <p class="h6 ind3 nobold">③ 相手方の善意無過失（「代理人の権限があると信ずべき正当な理由」、110条）</p>
        </div>
<div class="kakomon"><a href="pu26min09q.php#q-h12-03-2" title="H12-3-2">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　基本代理権の意義</p>
          <p class="p6 ind1">110条の基本代理権にあたるか問題となるものに以下のものがある。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 公法上の行為</p>
          <p class="p6 ind2">原則：公法上の行為は基本代理権にあたらない</p>
          <p class="p6 ind2">　ex. 印鑑証明書下付申請の代理権（最判昭39.４.２）</p>
          <p class="p6 ind2">例外：当該行為が「特定の私法上の取引行為の一環としてなされるものであるとき」は表見代理の成立を認め得る（最判昭46.６.３）</p>
          <p class="p6 ind2">　ex. 所有権移転登記申請の代理権（最判昭46.６.３）</p>
          <p class="p6 ind2">（理由）</p>
          <p class="h6 ind3 nobold">①表見代理制度は取引の安全を目的としているから、基本代理権は私法上の代理権であることを要する。</p>
          <p class="h6 ind3 nobold">②しかし、当該行為と私法取引との関連性が強く、対外的にみて基本代理権授与とみられるような場合には、代理人の外観を信頼した第三者の保護を図る必要がある。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 法定代理権も基本代理権となるか</p>
          <p class="p6 ind2">Ｑ　法定代理権も基本代理権となり得るか、法定代理の場合、代理権の内容は法定されており本人の関与はないのであるから、表見代理を成立させるべきではないようにも思えるので問題となる。</p>
          <p class="p6 ind2">→　法定代理にも110条は適用される（大連判昭17.５.20）。</p>
        </div>
        <div class="p-card" id="min09-q22">
          <p class="h6 ind1">(3) 夫婦間の日常家事（761条）と110条</p>
<div class="jobun">
（日常の家事に関する債務の連帯責任）
第７６１条　夫婦の一方が日常の家事に関して第三者と法律行為をしたときは、他の一方は、これによって生じた債務について、連帯してその責任を負う。ただし、第三者に対し責任を負わない旨を予告した場合は、この限りでない。
</div>
          <p class="p6 ind2">例えば、妻が、夫から代理権を与えられているわけではないのに、夫の所有する土地を第三者に無断で譲渡したような場合、相手方を110条で保護し得るか。この点については以下のような問題が生じる。</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 761条を根拠に夫婦相互の代理権を認めることができるか。</p>
          <p class="p6 ind3">文言上761条は日常家事に関する夫婦の連帯責任を規定したものであって、代理権を規定したものではないので問題となる。</p>
          <p class="p6 ind3">→　肯定（最判昭44.12.18）</p>
          <p class="p6 ind3">※　ただし、通常の代理とは異なり、①顕名不要であり、②効果が双方に帰属する点に注意。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 110条適用の可否</p>
          <p class="p6 ind3">Ｑ　夫婦間での明示的・黙示的な代理権の授与はなく、日常家事の範囲内の行為とも認められず761条による代理権も認められない場合、761条の夫婦間での代理権を基本代理権として、110条の表見代理が成立する余地はないかが問題となる。</p>
          <p class="p6 ind3">→　110条の趣旨類推適用説（最判昭44.12.18）</p>
          <p class="p6 ind3">第三者が、当該行為を日常家事の範囲内と信ずるにつき正当な理由がある場合には、110条の趣旨を類推適用して、第三者を保護する。</p>
        </div>
<div class="kakomon"><a href="pu26min09q.php#q-h18-04-e" title="H18-4-エ">過去問</a></div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　基本代理権を逸脱して代理行為をしたこと</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 代理人の行為が、基本権限と同質のものであることは要しない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 代理人が本人名義で代理権限を越えて取引を行った場合</p>
          <p class="p6 ind2">Ｑ　直接本人名義を用いても、顕名として認められる（前述）。ではこの場合、110条の適用を認めることができるか。</p>
          <p class="p6 ind2">→　本人自身の行為であると信じたことについて正当な理由がある場合には110条を類推適用できる（最判昭44.12.19）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">5.　「正当な理由」（110条）の意義、判断要因</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">「正当な理由」とは善意・無過失と同義である（信頼の正当性）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 正当理由の判断</p>
          <p class="p6 ind2">単に相手方から見た事情のみならず、本人側の事情も含んで、表見代理の成立を認めるべきか否かの総合的判断を行う（最判昭51.６.25）。従って、具体的事案ごとに個別に考えるべきである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 「第三者」の範囲</p>
          <p class="p6 ind2">Ｑ　「第三者」（110条）には、転得者が含まれるか。</p>
          <p class="p6 ind2">→　否定説（最判昭36.12.12・通説）</p>
          <p class="p6 ind2">第三者は直接の相手方に限る。</p>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-07-04-1-04">４　代理権消滅後の表見代理（112条）</div>
<div class="jobun">
（代理権消滅後の表見代理等）
第112条　他人に代理権を与えた者は、代理権の消滅後にその代理権の範囲内においてその他人が第三者との間でした行為について、代理権の消滅の事実を知らなかった第三者に対してその責任を負う。ただし、第三者が過失によってその事実を知らなかったときは、この限りでない。
２　他人に代理権を与えた者は、代理権の消滅後に、その代理権の範囲内においてその他人が第三者との間で行為をしたとすれば前項の規定によりその責任を負うべき場合において、その他人が第三者との間でその代理権の範囲外の行為をしたときは、第三者がその行為についてその他人の代理権があると信ずべき正当な理由があるときに限り、その行為についての責任を負う。
</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　趣旨</p>
          <p class="p6 ind1">112条は、かつて代理人であった者が、その代理権消滅後も代理人と称して代理行為を行った場合に、本人は表見代理責任を負わなければならないとしたものである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 代理人として行為した者がその代理権の範囲内で代理行為をした場合（112条１項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 代理人として行為した者がかつて代理権を有していたこと</p>
          <p class="h6 ind3 nobold">② 相手方の善意・無過失</p>
          <p class="p6 ind4">　相手方が、「過去に存在した代理権が代理行為の前に消滅したことを知らなかったこと」が必要である。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 代理人として行為した者がその代理権の範囲外で代理行為をした場合（112条２項）</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 代理人として行為した者がかつて代理権を有していたこと</p>
          <p class="h6 ind3 nobold">② 相手方がその行為についてその他人の代理権があると信ずべき正当な理　由があること</p>
        </div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　適用範囲</p>
          <p class="p6 ind1">112条は、法定代理には適用されない。</p>
