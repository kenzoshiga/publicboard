      <div class="blk inner">
        <div class="p-card" id="min09-q1">
          <p class="h6 ind0">1.　無権代理人が本人を相続した場合（無権代理人相続型）</p>
          <p class="p6 ind1">無権代理人が本人を相続した場合の処理については以下の問題点がある。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 本人と無権代理人の地位は融合するか、それとも併存するか。</p><br>
          <p class="p6 ind2">Ａ　地位融合説（総合的立場。最判昭40.６.18〔単独相続の事案〕）</p>
          <p class="p6 ind2">相続によって、本人としての地位と無権代理人としての地位が融合し、追認があったのと同様に無権代理が治癒される。</p>
          <p class="p6 ind2">（批判）</p>
          <p class="p6 ind2">共同相続人がいる場合、他の者の追認拒絶権を無視することになる。</p><br>
          <p class="p6 ind2">Ｂ　地位併存説（分析的立場。最判平５.１.21〔共同相続の事案〕はこれを前提としていると解される）</p>
          <p class="p6 ind2">無権代理行為は、相続によって当然に有効とはならず、無権代理人において本人の地位と無権代理人の地位が併存する。</p>
          <p class="p6 ind2">そして、この場合、相手方は、相続開始前と同様に取消権の行使や無権代理人の責任追及ができる。</p>
          <p class="p6 ind2">（理由）</p>
          <p class="h6 ind3 nobold">① 相続は、被相続人が生存しているのと同じ法律関係を、相続の前後を通じて維持しようとするものである。相続という偶然的事実で、利益や不利益を生じさせるべきではない。</p>
          <p class="h6 ind3 nobold">② 相手方が取消権（115条）や無権代理人の責任追及（117条）を選択することが可能となり、相手方の利益保護の点でも妥当である。</p>
<div class="kakomon"><a href="pu26min09q.php#q-r02-05-a" title="R2-5-ア">過去問</a></div>
        </div>
        <div class="p-card" id="min09-q7">
          <p class="h6 ind1">(2) 地位が併存するとして、無権代理人は本人の地位において追認拒絶できるか。</p>
          <p class="p6 ind2">→　信義則説（最判平５.１.21〔共同相続の事案〕）</p>
          <p class="p6 ind2">無権代理人は、信義則上、追認拒絶できない。</p>
          <p class="p6 ind2">（理由）</p>
          <p class="p6 ind2">追認拒絶は先行する無権代理行為と相容れない矛盾する行為である。</p>
<div class="kakomon"><a href="pu26min09q.php#q-h13-03-e" title="H13-3-エ">過去問</a></div>
        </div>
        <div class="p-card" id="min09-q8">
          <p class="h6 ind1">(3) 共同相続の場合、他の共同相続人が追認拒絶しても追認の効力が生じ無権代理行為が有効となるのか、追認権の帰属態様が問題となる。</p>
          <p class="p6 ind2">→　追認不可分説（最判平５.１.21）</p>
          <p class="p6 ind2">追認するかどうかを選択できる地位は性質上すべての相続人に承継される。従って、共同相続人全員がそろって追認しない限り、無権代理行為は、無権代理人の相続分に相当する部分についても有効にならない。</p>
<div class="kakomon"><a href="pu26min09q.php#q-r02-05-ih20-06-u" title="R2-5-イ">過去問</a></div>
        </div>
        <div class="p-card" id="min09-q11">
          <p class="h6 ind1">(4) 本人が追認拒絶後に死亡し、無権代理人が本人を相続した場合の無権代理行為の効力はどうか。前述と同様に、無権代理人の追認拒絶は許されないとして有効になるのか。</p>
          <p class="p6 ind2">→　有効にはならない（最判平10.７.17）。</p>
          <p class="p6 ind2">（理由）</p>
          <p class="h6 ind3 nobold">① 本人が追認を拒絶すれば無権代理の効力が本人に及ばないことが確定し、追認拒絶の後は本人であっても追認によって無権代理行為を有効にすることができない。追認拒絶の後に無権代理人が本人を相続したとしても、このような追認拒絶の効果に何ら影響はない。</p>
          <p class="h6 ind3 nobold">② この場合、相続した無権代理人が本人の追認拒絶の効果を主張することがそれ自体信義則に反するものであるということはできない。</p>
