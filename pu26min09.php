        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　本人が無権代理人を相続した場合（本人相続型）</p>
          <p class="p6 ind1">本人が無権代理人を相続した場合の処理については以下の問題点がある。</p>
        <div class="p-card" id="min09-q14">
          <p class="h6 ind1">(1) 両者の地位は併存するのか。</p>
          <p class="p6 ind2">無権代理人が本人を相続した場合と同様の議論である。</p>
          <p class="p6 ind2">→　地位併存説（最判昭37.４.20）</p>
          <p class="p6 ind2">両者の地位は併存し、さらにこの場合、本人の地位で追認を拒絶することは何ら信義に反するものではないから、本人は追認拒絶できる。</p>
<div class="kakomon"><a href="pu26min09q.php#q-r02-05-u" title="R2-5-ウ">過去問</a></div>
        </div>
        <div class="p-card" id="min09-q17">
          <p class="h6 ind1">(2) 無権代理人の責任の取扱い</p>
          <p class="p6 ind2">Ｑ　地位併存説を前提とした場合、無権代理人の責任（117条の義務）は本人に承継されるか。</p><br>
          <p class="p6 ind2">Ａ　無権代理人の責任は本人に承継されない。</p>
          <p class="p6 ind2">（理由）</p>
          <p class="p6 ind2">117条の責任を負わせたのでは、本人に追認拒絶権の行使を認めたことが無意味になる。</p>
          <p class="p6 ind2">（批判）</p>
          <p class="p6 ind2">他の共同相続人がいる場合、本人のみが責任を免れることになり不公平である。</p><br>
