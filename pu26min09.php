          <p class="p6 ind1">→　相続人は本人の資格で無権代理行為の追認を拒絶する余地はない（最判昭63.３.１）。</p>
          <p class="p6 ind1">（理由）</p>
          <p class="p6 ind1">無権代理人が本人の地位を相続した場合と同様の法律上の地位ないし効果を生じる。</p>
          <p class="p6 ind1">（批判）</p>
          <p class="p6 ind1">相続の先後関係は偶然に決まるものであり、相続人は何ら非難されるべき行為をしていないのだから、むしろ、本人が無権代理人を相続した場合と同様に扱うべきである。</p>
