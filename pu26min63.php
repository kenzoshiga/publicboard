        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 用法及び善管注意義務</p>
          <p class="p6 ind3">配偶者は、従前の用法に従い、善良な管理者の注意をもって、居住建物の使用及び収益をしなければならない（1032条１項本文）。</p>
          <p class="p6 ind3">ただし、従前居住の用に供していなかった部分について、これを居住の用に供することはできる（1032条１項ただし書）。配偶者の居住権を保護する趣旨である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 譲渡</p>
          <p class="p6 ind3">配偶者居住権を譲渡することはできない（1032条２項）。配偶者居住権は、相続開始後の配偶者の居住建物の確保等を目的とする権利であり、第三者への譲渡は制度趣旨と合わないからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 修繕等</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　配偶者は、居住建物の使用及び収益に必要な修繕をすることができる（1033条１項）。</p>
          <p class="h6 ind3 nobold">ⅱ　居住建物の修繕が必要な場合に、配偶者が相当の期間内に必要な修繕をしないときは、居住建物の所有者は、その修繕をすることができる（1033条２項）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(d) 通知義務</p>
          <p class="p6 ind3">居住建物が修繕を要するとき（配偶者が自ら修繕するときを除く）、又は居住建物について権利を主張する者があるときは、配偶者は、居住建物の所有者に対し、遅滞なくその旨を通知しなければならない（1033条３項本文）。ただし、居住建物の所有者が既にこれを知っているときは、この限りでない（1033条３項ただし書）。</p>
        </div>
