        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 費用の償還に関する期間制限</p>
          <p class="p6 ind2">費用償還請求権は、貸主が返還を受けた時から１年以内に行使しなければならない（1036条、600条１項）。</p>
        </div>
