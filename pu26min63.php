        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 配偶者が相続欠格に該当し又は廃除により相続権を失っていないこと</p>
          <p class="p6 ind2">これらの場合には、居住建物の所有者の権利を制約してまで、配偶者の保護をする必要性が乏しいためである。</p>
          <p class="p6 ind2">なお、配偶者が相続放棄をした場合であっても、配偶者短期居住権は成立する。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　対抗要件</p>
          <p class="p6 ind1">配偶者短期居住権は登記することはできず、対抗要件は不要である。</p>
