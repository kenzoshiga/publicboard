          <p class="p6 ind2">最終順位の相続人が、欠格、廃除、放棄等により相続権を有しなくなった場合も含む。</p>
          <p class="p6 ind2">相続財産全部の包括受遺者が存在する場合は、これに当たらない（最判平９.９.12）。</p>
