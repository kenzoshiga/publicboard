        <div class="p-card">
          <p class="h6 ind1">(4) 適法な承諾を得た転貸等の効果</p>
          <p class="p6 ind2">配偶者が居住建物の所有者の承諾を得て、第三者に居住建物を使用させる場合には、賃借物の適法な転貸に関する613条の規定が適用される（1036条、613条）。</p>
