          <p class="h6 ind0">1.　相続財産法人</p>
          <p class="p6 ind1">相続人不存在の場合、相続財産は法人となる（951条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　相続財産清算人の選任（952条１項）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">相続財産法人となる場合には、家庭裁判所は、利害関係人又は検察官の請求によって、相続財産の清算人を選任しなければならない（952条１項）。</p>
          <p class="p6 ind2">相続財産清算人は、相続財産法人を代表し、及びその後に出現するかもしれない相続人や包括受遺者の法定代理人である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 相続財産清算人の権限及び義務</p>
          <p class="p6 ind2">相続財産清算人の権限や義務については、27条から29条までの不在者財産管理人の規定が準用される（953条）。</p>
          <p class="p6 ind2">また、財産状況報告義務を負う（954条）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　公告手続</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 相続財産清算人選任・相続人捜索の公告（952条２項）</p>
          <p class="p6 ind2">相続財産の清算人を選任した家庭裁判所は、遅滞なく、その旨及び相続人があるならば一定の期間内（６か月以上）にその権利を主張すべき旨を公告しなければならない（952条２項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 請求申出の公告（957条１項）</p>
          <p class="p6 ind2">（1）の公告後、相続財産の清算人は、全ての相続債権者及び受遺者に対し、２か月以上の期間を定めて、その期間内にその請求の申出をすべき旨を公告しなければならない（957条１項前段）。請求申出の公告は、相続財産や相続債権者の調査期間を設けるため、（1）の公告後に実施するものとされた。</p>
          <p class="p6 ind2">請求申出の公告は、（1）の規定により定めた相続人が権利を主張すべき期間内に満了しなければならない（957条１項後段）。</p>
          <p class="p6 ind2">なお、請求申出の公告の開始時期については規定がないため、清算人が事案に応じて行うこととなる。</p>
          <p class="p6 ind2"><img class="embed-img" src="img/min63-02.png" alt="img/min63-02.png"></p>
          <p class="p6 ind2">清算人は、請求申出の公告期間満了前には、相続債権者及び受遺者に対して弁済を拒むことができる（957条２項、928条）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　失権</p>
          <p class="p6 ind1">952条２項の期間内に相続人としての権利を主張する者がないときは、相続人並びに相続財産の清算人に知れなかった相続債権者及び受遺者は、その権利を行使することができない（958条）。</p>
          <p class="p6 ind1">相続債権者及び受遺者が失権すると、相続財産は特別縁故者への財産分与（958条の２）の対象となる。</p>
