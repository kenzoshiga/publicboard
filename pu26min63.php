          <p class="h6 ind0">1.　取得方法</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 遺産分割による取得（1028条１項１号）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 遺贈による取得（1028条１項２号）</p>
          <p class="p6 ind2">配偶者居住権の遺贈があった場合には、持戻し免除の意思表示の規定（903条４項）が適用される（1028条３項）。したがって、903条４項の要件に該当する配偶者居住権は、特別受益として取り扱うことを要しない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 家庭裁判所の審判による取得（1029条）</p>
          <p class="p6 ind2">遺産の分割の請求を受けた家庭裁判所は、次に掲げる場合に限り、配偶者が配偶者居住権を取得する旨を定めることができる（1029条各号）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　共同相続人間に配偶者が配偶者居住権を取得することについて合意が成立しているとき</p>
          <p class="h6 ind3 nobold">ⅱ　配偶者が家庭裁判所に対して配偶者居住権の取得を希望する旨を申し出た場合において、居住建物の所有者の受ける不利益の程度を考慮してもなお配偶者の生活を維持するために特に必要があると認めるとき（ⅰの場合を除く。）</p>
