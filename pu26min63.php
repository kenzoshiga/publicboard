        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　存続期間</p>
          <p class="p6 ind1">原則として、配偶者の終身の間存続する。遺産分割協議・遺言・家庭裁判所の遺産分割の審判で別段の定めをしたときは、その定めによる（1030条）。</p>
