      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　成立要件</p>
          <p class="p6 ind1">配偶者短期居住権が成立するには、以下の要件を満たす必要がある（1037条１項）。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 被相続人の配偶者が被相続人の財産に属した建物に相続開始時に無償で居住していたこと</p>
          <p class="p6 ind2">「配偶者」とは、法律上の婚姻関係があった者である。</p>
          <p class="p6 ind2">「無償で」使用していたことが必要である。有償で使用していた場合には配偶者と被相続人との間に契約関係があったと考えられ、契約関係が継続するため配偶者短期居住権は要しない。</p>
        </div>
        <div class="p-card">
