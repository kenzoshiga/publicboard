        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">相続人がいるか不明の場合、一方ではこれを探さなければならないが、他方その出現までの間、相続財産を管理し、相続債権者に弁済するなどの処理をしなければならない。</p>
          <p class="p6 ind2">そこで、相続財産自体を法人化し、家庭裁判所が選任した相続財産清算人が、相続財産を管理し、清算手続を進めるための規定を置いている。</p>
