        <div class="p-card">
          <p class="h6 ind1">(1) 被相続人の配偶者が被相続人の財産に属した建物に相続開始時に居住していたこと</p>
          <p class="p6 ind2">「配偶者」とは、法律上の婚姻関係があった者である。配偶者居住権は、法律上婚姻していた配偶者の一身専属権である。</p>
          <p class="p6 ind2">「被相続人の財産に属した建物」とは、被相続人所有だった建物のことをいう。したがって、賃借建物に住んでいた場合には、配偶者居住権は成立しない。</p>
        </div>
        <div class="p-card">
