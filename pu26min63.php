          <p class="p6 ind1">配偶者居住権とは、被相続人の配偶者が被相続人の財産に属した建物（居住建物）に居住していた場合に居住建物の全部を無償で使用収益できる権利である（1028条１項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　趣旨</p>
          <p class="p6 ind1">生存配偶者の生活の安定、自宅以外の財産の取り分の確保である。</p>
        </div>
      </div>
    </div>
