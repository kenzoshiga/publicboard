          <p class="p6 ind4">ただし、権原によって附属させた場合には例外が認められる（242条ただし書）。</p>
          <p class="p6 ind4">権原：地上権・賃借権・永小作権のように当該不動産を利用し、物を附属させることができる法律上の地位</p>
          <p class="p6 ind4">権原による所有権の留保が認められるのは弱い付合の場合だけである。</p>
