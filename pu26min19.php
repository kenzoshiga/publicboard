        <div class="p-card" id="min19-q24">
          <p class="h6 ind2 nobold">(a) 不動産の付合（242条）</p>
          <p class="p6 ind3">付合とは、動産が不動産に付着して不動産そのものとみられるに至ることをいう。</p>
        <div class="p-card">
