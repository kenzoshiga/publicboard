      <div class="outer-title" id="A01-02-02-1-07">７　制限行為能力者の相手方の保護</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　総説</p>
          <p class="p6 ind1">前記のような制限行為能力者の行為がいつまでも取り消すことができるものであるとすると、法律関係は依然として確定せず、相手方の地位は不安定となり、取引の安全が害されるおそれが生ずる。</p>
          <p class="p6 ind1">　このような観点から、民法は、制限行為能力者と取引をした相手方を保護する制度として、① 法律関係を速やかに確定させるための催告権（20条）、② 制限行為能力者が詐術を用いた場合の取消権の剥奪（21条）、③ 取消権の行使期間の制限（126条）、④ 法定追認制度（125条）を用意している。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　相手方の催告権（①）</p>
          <p class="p6 ind1">催告とは、ある人に対してある行為を要求・催促することをいう。20条は不確定な状態にある制限行為能力者の相手方を保護するため、相手方に催告権を与えた。</p>
        <div class="p-card" id="min02-q36">
          <p class="h6 ind1">(1) 催告の相手方</p>
          <p class="p6 ind2">受領能力（相手方の意思表示の内容を理解する能力）を有する者でなければならない。</p>
          <p class="p6 ind2"><img class="embed-img" src="img/min02-03.png" alt="min02-03.png"></p>
<div class="kakomon"><a href="pu26min02q.php#q-h29-04-i" title="H29-4-イ他">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 催告の期間</p>
          <p class="p6 ind2">1か月以上の期間を定めて催告しなければならない。催告期間が1か月未満の場合や期間を定めなかった場合、催告は無効である。</p>
        </div>
        <div class="p-card" id="min02-q38">
          <p class="h6 ind1">(3) 催告に対する確答がない場合</p>
          <p class="p6 ind2">催告を受けた者が単独で追認できる場合には追認したものとみなされ、単独で追認できない場合には取り消したものとみなされる。</p>
          <p class="p6 ind2"><img class="embed-img" src="img/min02-04.png" alt="min02-04.png"></p>
