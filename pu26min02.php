      <div class="outer-title" id="A01-02-02-1-06">６　法定後見制度③－補助</div>
      <div class="blk inner">
        <div class="p-card" id="min02-q34">
          <p class="h6 ind0">1.　被補助人の行為能力</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 同意権又は代理権付与の審判</p>
          <p class="p6 ind2">補助開始の審判により被補助人の行為能力自体が制限を受けるわけではない。補助開始の審判のみでは、補助人は同意権・代理権のいずれも与えられない。</p>
          <p class="p6 ind2">そこで、同時に同意権付与の審判（17条１項）又は代理権付与の審判（876条の9第１項）をしなければならない（15条３項）。そして、特定の法律行為について同意権又は代理権を与えるのが適当であると認められる場合に、家庭裁判所は補助人に同意権・代理権の双方又は一方を付与する旨の審判をすることができる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 被補助人の行為能力の制限</p>
          <p class="p6 ind2">特定の法律行為に同意権が与えられた場合は、同意を要する特定の法律行為に関しては、被補助人の行為能力は制限される。</p>
          <p class="p6 ind2">従って、補助人の同意を要する行為であるにもかかわらず、補助人の同意又は同意に代わる許可を得ないでした行為は、取り消すことができる（17条４項）。</p>
          <p class="p6 ind2">これに対し、特定の法律行為に代理権のみが与えられた場合には、被補助人の行為能力は制限されない。</p>
          <p class="p6 ind2">従って、被補助人は補助人に代理権の与えられた行為を含めすべての行為を有効にすることができる。</p>
        </div>
<div class="kakomon"><a href="pu26min02q.php#q-r06-20-i" title="R6-20-イ他">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　補助人の権限及び義務</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 同意権（17条）</p>
          <p class="p6 ind2">家庭裁判所の審判により与えられる。対象は13条１項に定める行為の一部に限られる（17条１項ただし書）。補助制度は、保佐制度を利用するほどに判断能力が不十分ではない者が利用することを予定した制度であり、単独ですることができる行為の範囲を被保佐人よりも広くする趣旨である。</p>
          <p class="p6 ind2">なお、この場合も、日用品の購入その他日常生活に関する行為は、同意を要する行為とはならない。</p>
          <p class="p6 ind2">被補助人の利益を害するおそれがないにもかかわらず補助人が同意しない場合、家庭裁判所は代諾許可を与えることができる（17条３項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 代理権（876条の９）</p>
          <p class="p6 ind2">家庭裁判所は、補助の審判開始の請求権者（15条１項本文）又は補助人若しくは補助監督人の請求によって、｢特定の法律行為｣について補助人に代理権を付与する旨の審判をすることができる（876条の９第１項）。</p>
          <p class="p6 ind2">補助人が代理権をもつことは、被補助人にとって重大な利害があるから、本人以外の者の請求によって代理権を付与する旨の審判をするには、本人の同意がなければならない（876条の９第２項、876条の４第２項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 取消権（120条1項）</p>
          <p class="p6 ind2">同意権が付与されている場合に取消権も付与される。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 追認権（122条）</p>
          <p class="p6 ind2">同意権が付与されている場合に追認権も付与される。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(5) 身上配慮義務（876条の10第１項、876条の５第１項）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(6) 被補助人の居住用不動産の処分（876条の10第１項、859条の３）</p>
          <p class="p6 ind2">補助人に代理権が付与された場合であっても、被補助人の居住用不動産を処分するについては家庭裁判所の許可を得なければならない。</p>
