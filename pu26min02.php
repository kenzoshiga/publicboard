        <div class="p-card" id="min02-q41">
          <p class="h6 ind0">3.　取消権の剥奪（②）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">制限行為能力者が「詐術」を用いて取引の相手方に自分が行為能力者である旨を信じさせたような場合には、もはやその制限行為能力者はその行為を取り消すことができない（21条）。</p>
          <p class="p6 ind2">制限行為能力者が相手方を欺いて行為能力を有すると誤信させた場合には、もはやこれを保護する必要がないため制裁として取消権を喪失させ、他方、行為能力者と信じた相手方の取引安全を図ったものである。</p>
        </div>
<div class="kakomon"><a href="pu26min02q.php#q-h29-04-uh09-01-4" title="H29-4-ウ他">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(2) 要件</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 制限行為能力者が行為能力者であることを信じさせるため</p>
          <p class="p6 ind4">制限行為能力者でないとする詐術のみならず、法定代理人や保佐人の同意を得たとする詐術も本条の「詐術」に該当する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">② 詐術を用いたこと</p>
          <p class="p6 ind4">「詐術」とは、積極的に欺いた場合のみならず、普通に人を欺くに足りる言動によって相手方の誤信を誘発し、又は誤信を強めたと認められるときも含むとされる。</p>
          <p class="p6 ind4">　制限行為能力者であることを黙秘している場合、それだけでは「詐術」にはあたらないが、それが制限行為能力者の他の言動と相まって、相手方を誤信させ、又は誤信を強めたと認められるときは「詐術」に当たるとされる（最判昭44.２.13）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">③ 相手方が、行為能力者であることを信じたこと</p>
          <p class="p6 ind4">「信じた」とは、一般に、善意かつ無過失を意味すると解されている。</p>
