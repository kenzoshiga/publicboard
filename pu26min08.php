        <div class="p-card" id="min08-q25">
          <p class="h6 ind2 nobold">(d) 表見代理が成立する場合にも無権代理人の責任を追及できるか</p><br>
          <p class="p6 ind3">Ａ　否定説</p>
          <p class="p6 ind3">　表見代理が成立する場合には、117条の責任追及は認められない。</p><br>
          <p class="p6 ind3">（理由）</p>
          <p class="h6 ind3 nobold">① 表見代理が成立する場合には、相手方は予定していた効果をおさめることができるから、取引の安全のための117条の責任を生じさせる必要はない。</p>
          <p class="h6 ind3 nobold">② 有権代理の場合でさえ本人が責任を負うだけなのに、表見代理の場合には本人、無権代理人のいずれにも責任を追及できるのは不当である。</p><br>
          <p class="p6 ind4">（批判）</p>
          <p class="h6 ind3 nobold">① 相手方が予定の効果をおさめることができるのは、まさに表見代理が認められた場合であって、その主張がなされていない場合にまでこれを理由とすることはできない。だとすれば、無権代理であるはずの表見代理を117条の適用からはずす根拠はなく、むしろ、113条、115条の適用が是認されていることと不均衡が生じる。</p>
          <p class="h6 ind3 nobold">② 表見代理の立証は一般に困難であり、無権代理人への責任追及も本人のすばやい追認で切り落とされる危険にさらされていることを考慮すれば、本人、表見代理人のいずれに対しても責任追及ができるとしても、相手方の保護は過大にならない。</p><br>
          <p class="p6 ind4">Ｂ　肯定説（最判昭62.７.７）</p>
          <p class="p6 ind4">　表見代理が成立し得る場合にも、表見代理の成立と無権代理人への責任追及の主張が選択的に認められる。</p><br>
          <p class="p6 ind4">（理由）</p>
          <p class="h6 ind3 nobold">① 表見代理と無権代理人の責任とは別個独立の制度であり、無権代理人の責任は表見代理が成立しない場合における補充的な責任ではない。</p>
          <p class="h6 ind3 nobold">② 無権代理人が表見代理の成立を理由にその責任を免れるとするのは妥当でない。</p>
