        <div class="p-card">
          <p class="h6 ind1">(2) 追認拒絶（113条）</p>
          <p class="p6 ind2">追認拒絶とは、この追認を拒絶することである。</p>
          <p class="p6 ind2">代理行為の効果は本人に帰属しないことに確定する。</p>
