          <p class="h6 ind3 nobold">　② 代理権の存在を証明できなかったこと（117条１項）</p>
          <p class="h6 ind3 nobold">　③ 本人の追認がないこと（117条１項）</p>
          <p class="h6 ind3 nobold">　④ 相手方が取消権（115条）を行使していないこと</p>
          <p class="h6 ind3 nobold">　⑤ 相手方が無権代理であることにつき、善意であること（117条２項１号）</p>
