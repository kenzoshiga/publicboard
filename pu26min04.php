      <p>そこで、意思表示の相手方がその意思表示を受けたときに意思能力を有しなかったとき又は未成年者若しくは成年被後見人であったときは、その意思表示をもってその相手方に対抗することができない（98条の２本文）。対抗の問題であるから、受領者側から到達を主張することは妨げられない。</p>
