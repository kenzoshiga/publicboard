    <div class="p-card sec-card">
      <h3 id="A01-04-01">第１節　物－総説</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-01-0-01">１　物とは何か</div>
      <p>民法上の「物」とは有体物をいう（85条）。</p>
      <p>有体物とは、空間の一部を占める外界の物質すべてをいう（固体・液体・気体）。</p>
      <p>もっとも、以下に示すように、有体物がすべて民法にいう「物」というわけではない。権利の客体としてふさわしいことが必要である。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-01-0-02">２　要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind3 nobold">① 有体性</p>
          <p class="h6 ind3 nobold">② 支配可能性</p>
          <p class="p6 ind4">　大気、海面、月、太陽などは除かれる。</p>
          <p class="h6 ind3 nobold">③ 非人格性</p>
        </div>
      </div>
    </div>
    </div>
    <div class="p-card sec-card">
      <h3 id="A01-04-02">第２節　不動産と動産</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-02-0-01">１　不動産</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　定義</p>
          <p class="p6 ind1">：土地及びその定着物（86条１項）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　種類</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 土地</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 土地の定着物</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 定着物</p>
          <p class="p6 ind3">：土地に固定されており、取引通念上継続的に固定されて使用されるもの</p>
          <p class="p6 ind3">ex. 樹木や石垣、機械もそれが土地又は建物に据え付けられたときには定着物となる。</p>
          <p class="p6 ind3">定着物は原則として土地の一部をなし、土地所有権に含まれるが、以下の２つの例外がある。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 建物</p>
          <p class="p6 ind3">建物は、常に独立の不動産とされている（370条参照）。</p>
          <p class="p6 ind3">建築中の物が、いつ独立の不動産としての建物となるかにつき、判例（大判昭10.10.１）は、木材を組み立てて屋根をふいただけでは建物といえないが、独立に風雨をしのげる程度、すなわち、屋根瓦がふかれ周壁として荒壁がぬられた程度に達すれば、床や天井が未完成でも「建物」といえるとする。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 立木</p>
          <p class="p6 ind3">立木法による登記をした立木は、土地とは独立の不動産とされ、また、明認方法をすることにより独立の物として扱われる。</p>
        </div>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-02-0-02">２　動産</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　定義</p>
          <p class="p6 ind1">動産：不動産以外の物（86条２項）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　金銭</p>
          <p class="p6 ind1">金銭も形式的には動産にあたるが、支払手段としての価値表象物であるから、動産というよりも価値そのものであり、動産に適用される多くの規定（178条、192条等）が適用されない（192条につき、最判昭39.１.24）。</p>
        </div>
      </div>
    </div>
