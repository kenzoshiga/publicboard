          <p class="h6 ind0">1.　原則－到達主義（97条１項）</p>
          <p class="p6 ind1">意思表示は、その通知が相手方に到達した時からその効力を生ずる（97条1項）。「到達」したといえるためには、意思表示が相手方の勢力圏内に入れば足り、相手方が了知することは必要ではない（最判昭36.４.20）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　例外－発信主義</p>
          <p class="p6 ind1">制限行為能力者に対する相手方の催告への確答（20条）</p>
        </div>
<div class="kakomon"><a href="pu26min04q.php#q-r07-05-a" title="R7-5-ア他">過去問</a></div>
      </div>
