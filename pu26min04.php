    <div class="blk outer">
      <div class="outer-title" id="A01-06-01-2-02">２　発信後の表意者の死亡・能力喪失</div>
      <p>表意者が発信した後に死亡したり、意思能力を喪失したり、制限行為能力者となっても、意思表示はそのまま効力がある（97条３項）。</p>
      <p>発信後の死亡・能力喪失は意思表示とは無関係な事実だからである。</p>
      <p>また、相手方が正当な理由なく意思表示の通知が到達することを妨げたときは、その通知は、通常到達すべきであった時に到達したものとみなされる（97条２項）。</p>
