    <div class="p-card sec-card">
      <h3 id="A01-04-03">第３節　主物と従物</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-03-0-01">１　定義</div>
      <p>従物：独立の物でありながら、客観的・経済的には他の物に従属して、その効用を助ける物（87条１項）</p>
      <p>主物：従物を付属させる対象となる物</p>
      <p>ex. 刀に対して鞘は従物。レストランの建物を主物とすると、営業用のテーブル・イスは従物。</p>
      <p>従物は、主物の処分に従う（87条２項）。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-03-0-02">２　趣旨</div>
      <p>物が独立性を有している場合、その処分も独立になされるのが原則であるが、２個の物の間に客観的・経済的な主従結合関係があるとき、これを法律的運命においても同一に取扱い、その結合を破壊しないように要請される場合が多い。かかる要請に応じて設けられたのが、87条２項である。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-03-0-03">３　従物の要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　継続的に主物の効用を助けること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　主物に付属すると認められる程度の場所的関係にあること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　主物と同一の所有者に属すること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　独立の物であること</p>
          <p class="p6 ind1">一方が他方の構成部分となり、又は、合して単一の物として認められるだけになるときは、従物について別異の物としての地位を認めることはできない。</p>
          <p class="p6 ind1"><img class="embed-img" src="img/min04-01.png" alt="min04-01.png"></p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-03-0-04">４　効果</div>
      <p>従物は主物の処分に従う（87条２項）。ただし、本条は任意規定である。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-04-03-0-05">５　従たる権利</div>
      <p>従たる権利についても87条２項を類推する。</p>
      <p>ex. 借地上の建物にとって、借地権は従たる権利である（最判昭40.５.４）。</p>
    </div>
