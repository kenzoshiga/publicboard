    <h2 id="A01-05">第５章　法律行為</h2>
    <div class="p-card sec-card">
      <h3 id="A01-05-01">第１節　法律行為の意義</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-01-0-01">１　定義</div>
      <p>：意思表示を要素とする私法上の法律要件</p>
      <p>意思表示とは、当事者が法律効果を欲し、かつ、そのことを発表する行為をいう。</p>
      <p>法律要件とは、権利・義務を発生させる一定の社会関係をいう。</p>
      <p>私権（権利・義務）は一定の事実を原因として変動（発生、変更、消滅）する。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-01-0-02">２　法律事実</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">：法律要件を構成する個々の要素</p>
          <p class="p6 ind1">この法律事実の中で、最も重要なものが意思表示である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　法律事実の種類</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 容態（人の精神作用に基づく事実）</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 外部的容態（行為）：意思表示、準法律行為など</p>
          <p class="h6 ind2 nobold">(b) 内部的容態：「善意」（32条１項、94条２項など）、「制限する旨の意思表示」（474条４項）</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 事件（人の精神作用に基づかないもの）</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 事件：人の生死、時の経過など、人の精神作用と関係のないもの</p>
          <p class="h6 ind2 nobold">(b) 事実行為：物を破壊し、果実を分離するというような、人の行為でなくとも同一の効果を生ずる行為</p>
          <p class="p6 ind3"><img class="embed-img" src="img/min04-02.png" alt="min04-02.png"></p>
        </div>
        </div>
        </div>
      </div>
    </div>
