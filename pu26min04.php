    <div class="p-card sec-card">
      <h3 id="A01-06-01">第１節　総説</h3>
    <div class="p-card rank4-card">
      <p id="A01-06-01-1" class="h6 ind0">Ⅰ　意思表示</p>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-01-1-01">１　意思表示の意義・成立過程</div>
      <p>意思表示：当事者が法律効果を欲し、かつ、そのことを表示する行為</p>
      <p>意思表示を細かく分析すると、動機・（内心的）効果意思・表示意思・表示行為に分けられる。</p>
      <p>例えば、ある土地（甲地とする）を買う場合、以下の過程を辿る。</p>
      <p></p>
      <p>　　「甲土地上に家を建てたい」・・・・・・・・・・・・・・動機</p>
      <p>　　「甲土地を買おう」・・・・・・・・・・・・・・・・・・・・・効果意思</p>
      <p>　　「それを手紙で伝えよう」・・・・・・・・・・・・・・・・・表示意思</p>
      <p>　　「甲土地を売ってください」という手紙を出す・・・表示行為</p>
      <p>これらの過程に問題があるのが「意思の欠缺」や「瑕疵ある意思表示」の話である（後述）。</p>
      <p></p>
    </div>
    </div>
    <div class="p-card rank4-card">
      <p id="A01-06-01-2" class="h6 ind0">Ⅱ　意思表示の到達と受領</p>
    <div class="blk outer">
      <div class="outer-title" id="A01-06-01-2-01">１　意思表示の効力発生時期</div>
      <div class="blk inner">
