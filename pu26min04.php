          <p class="p6 ind1">すなわち、意思表示を要素としない点で法律行為と異なる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　種類</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 狭義の準法律行為</p>
          <p class="p6 ind2">一定の意識内容を表現するもの</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 意思の通知</p>
          <p class="p6 ind3">　意思の発表であり、法はこれに対して一定の効果を付与するが、意思が法律効果の発生を内容としない。</p>
          <p class="p6 ind3">ex. 催告（20条等）、受領の拒絶（493条等）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 観念の通知</p>
          <p class="p6 ind3">一定の事実の通知であって、意思の発表という要素を含まないもの</p>
          <p class="p6 ind3">ex. 代理権授与の表示（109条１項）</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 非表現行為</p>
          <p class="p6 ind2">ex. 先占（239条１項）、遺失物拾得（240条）、事務管理（697条）</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　効果</p>
          <p class="p6 ind1">　狭義の準法律行為に対しては、意思表示・法律行為に関する規定が類推適用され得る。</p>
          <p class="p6 ind1">　なぜなら、狭義の準法律行為は、当事者の意思や感情の表現に対して法が一定の効果を付与するものであり、特定の意識内容の表現であって、また、当事者は通常法定の効果を予期してこれを行うものであるので、この点で法律行為・意思表示に類似しているからである。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-01-0-04">４　法律行為の種類・態様</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意思表示の態様による分類</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 契約</p>
          <p class="p6 ind2">：複数当事者の関与によって彼らが相互に拘束されるという効果を発生させる法律行為</p>
          <p class="p6 ind2">ex. 売買（555条）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 単独行為</p>
          <p class="p6 ind2">：一方の者の１個の意思表示だけで成立することのできる法律行為</p>
          <p class="p6 ind2">ex. 取消し（123条）、解除（540条）、遺言（960条以下）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 合同行為</p>
          <p class="p6 ind2">：相対立しない複数当事者の内容と方向を同じくする複数の意思表示が合致することによって成立する法律行為</p>
          <p class="p6 ind2">ex. 社団設立行為</p>
          <p class="p6 ind2">※　「法律行為」として説明されている部分のほとんどは「契約」に置き換えて理解してさしつかえない。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　有償行為・無償行為</p>
          <p class="p6 ind1">有償行為：財産の出捐を目的とする行為のうち、対価のあるもの</p>
          <p class="p6 ind1">ex. 売買（555条）、交換（586条）、賃貸借（601条）、請負（632条）</p>
          <p class="p6 ind1">無償行為：財産の出捐を目的とする行為であるが、対価のないもの</p>
          <p class="p6 ind1">ex. 贈与（549条）、使用貸借（593条）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　要式行為・不要式行為</p>
          <p class="p6 ind1">意思表示が一定の形式を伴うか否かで区別される</p>
          <p class="p6 ind1">要式行為　：その法律行為を組成する要素たる意思表示が、一定の方式、例えば書面の作成を要するもの</p>
          <p class="p6 ind1">不要式行為：要式行為以外のもの</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　生前行為・死後行為</p>
          <p class="p6 ind1">死後行為（死因行為）：行為者の死亡によって効果の発生するもの</p>
          <p class="p6 ind1">生前行為：死後行為以外のもの</p>
        </div>
      </div>
    </div>
    </div>
