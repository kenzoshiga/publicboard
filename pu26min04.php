          <p class="h6 ind0">3.　善意の第三者保護規定（93条２項）</p>
          <p class="p6 ind1">93条１項ただし書の無効は善意の第三者に対抗できるか。</p><br>
          <p class="p6 ind1">Ｑ　甲が、贈与の意思もないのに壺を乙に贈与する旨の意思表示をしたところ、乙がこれを自分の物として丙に売り渡した。甲の心裡留保について、乙が悪意、丙が善意であった場合、甲は、丙に贈与の無効を主張できるか。</p><br>
          <p class="p6 ind1">　→　無効は「善意の第三者」に対抗することができない（93条２項）。</p>
          <p class="p6 ind1">　「対抗することができない」とは、善意の第三者からは、無効を主張することも有効を主張することも許されるが、善意の第三者に対して無効を主張することは、当事者のみならず他の第三者も許されないということである。本条項は、第三者保護のための規定であるからである。</p>
