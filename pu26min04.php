      <div class="outer-title" id="A01-06-01-2-03">３　公示による意思表示（98条1項）</div>
      <p>相手方を知ることができず、又は所在不明の場合には、公示による意思表示が認められる（98条１項）。</p>
      <p>公示による意思表示は、最後に官報に掲載した日又はその掲載に代わる掲示を始めた日から2週間を経過した時に、相手方に到達したものとみなす（98条３項）。</p>
      <p>ただし、表意者が相手方を知らないこと又はその所在を知らないことにつき過失があったときは、到達の効力を生じない（98条３項ただし書）。</p>
