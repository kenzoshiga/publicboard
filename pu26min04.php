          <p class="p6 ind1">その表示を受けた者が、表意者の内心的意思について悪意又は有過失の場合（真実の内心的意思を知っていたか、又は、一般人の注意をもってすればその内心的意思を知ることができた場合）には、無効となる（93条１項ただし書）。</p>
