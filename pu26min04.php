          <p class="h6 ind3 nobold">② 公信の原則の有無（192条）</p>
          <p class="h6 ind3 nobold">③ その上に成立する権利の差異（369条等）</p>
          <p class="h6 ind3 nobold">④ 経済的価値の差異からくる取扱いの差異（13条１項３号、864条）</p>
          <p class="h6 ind3 nobold">⑤ 無主物の取扱い（239条）</p>
          <p class="h6 ind3 nobold">⑥ 相隣関係（209条以下）の有無</p>
        </div>
        </div>
      </div>
