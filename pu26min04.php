      <h3 id="A01-05-02">第２節　法律行為の解釈</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-02-0-01">１　意義</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind3 nobold">① 法律行為の意味を明らかにすること（狭義の法律行為の解釈）</p>
          <p class="h6 ind3 nobold">② 法律行為の内容の補充ないし修正をすること（補充的・修正的解釈）</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-02-0-02">２　解釈の基準</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　狭義の法律行為の解釈</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">① 当事者の付与した共通の意味（主観的意味）を確定する。</p>
          <p class="h6 ind3 nobold">② 共通の主観的意味を確定し得ない場合、表示の客観的意味を確定する。</p>
          <p class="p6 ind4">　当事者の用いた表示手段（言動・動作など）が、当該事情の下において、慣習や条理に従って判断した場合、相手方又は一般社会によってどのように理解されるかを標準とする。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　補充的・修正的解釈</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 慣習</p>
          <p class="p6 ind2">任意規定と異なった慣習がある場合において、法律行為の当事者が、その慣習による意思を有するものと認められるときは、法律行為はその慣習に従って解釈される（92条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 任意規定</p>
          <p class="p6 ind2">：公の秩序に関しない規定</p>
          <p class="p6 ind2">当事者が任意法規と異なった意思を表示した場合（91条）でない限り、法律行為の解釈の標準となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 条理（信義則）</p>
          <p class="p6 ind2">条理は制定法、判例法、慣習法などの礎えとなっているものであるから、法律行為の補充的、修正的解釈の標準となる。</p>
          <div class="sp"></div>
        </div>
        </div>
      </div>
    </div>
