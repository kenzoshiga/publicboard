      <h3 id="A01-05-03">第３節　法律行為の要件</h3>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-03-0-01">１　法律行為の成立要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　原則</p>
          <p class="p6 ind1">法律行為は、当事者が法律行為を組成する意思表示をなすことによって成立する。</p>
          <p class="p6 ind1">契約の場合、両当事者の意思表示（申込みと承諾）が機能的に合致して初めて成立する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要式行為・要物契約</p>
          <p class="p6 ind1">要式行為（手形行為など）や要物契約（質権設定契約など）では、さらに、方式や目的物の交付を必要とする。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-03-0-02">２　有効要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　内容の確定性</p>
          <p class="p6 ind1">当事者の目的、慣習、任意規定、条理によって補充してもなお不確定な法律行為に対しては、法は助力を拒まざるを得ない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　内容の実現可能性</p>
          <p class="p6 ind1">内容の実現不可能な法律行為は、法的効果を帰属させても無意味であるから、無効とされる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　内容の適法性－強行法規違反</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 適法性と強行法規</p>
          <p class="p6 ind2">法律行為が有効に成立するためにはその内容が法律の精神に適ったものでなければならない。従って、法律行為が強行法規に反する場合は無効となる（91条の反対解釈）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 取締法規等との関係</p>
          <p class="p6 ind2">取締法規とは経済政策や行政目的から、一定の行為を禁止又は制限する規定である。一方、強行法規は「公の秩序」維持を目的とするものであるから、取締法規とは目的の次元・観点を基本的に異にしている。もっとも、近時は取締規定に違反する契約自体を総合的に判断して契約が無効になるか否かを判断しようとする立場が有力である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 脱法行為</p>
          <p class="p6 ind2">脱法行為とは強行法規が禁止する法律効果をその法規には抵触しない他の法律行為によって実現することである。実質的には強行法規違反であり、無効となる。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　契約内容の社会的妥当性・公序良俗違反（90条）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 公序良俗の意義</p>
          <p class="p6 ind2">：国家社会の一般的利益秩序（公の秩序）と社会一般の道徳的観念（善良の風俗）を併せたもの</p>
          <p class="p6 ind2">公の秩序又は善良の風俗に反する法律行為は、無効とする（90条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 公序良俗違反の態様</p>
          <p class="p6 ind2">人倫に反するもの、正義の観念に反するもの、暴利行為、基本的人権を侵害する行為、射倖的行為等</p>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-03-0-03">３　効果帰属要件</div>
      <p>例えば、他人の行った法律行為の効果が本人に帰属するためには、行為者が本人に対する関係で代理権を有することが必要である（後述、「代理」参照）。また、自己又は他人の物に対してなされた処分行為の効果がその物ないし他人に及ぶためには、処分者がその物について処分権を有することが必要である。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A01-05-03-0-04">４　効力発生要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　法定条件</p>
          <p class="p6 ind1">：有効に成立した法律行為がその本来の効果を生ずるために、法が特に一定の要件を要求する場合</p>
          <p class="p6 ind1">ex. 遺言者より後まで受遺者が生きていること（994条１項）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　条件・期限</p>
          <p class="p6 ind1">：当事者の意思によって法律行為の効力の発生・消滅ないしその時期を左右するもの</p>
        </div>
      </div>
    </div>
    </div>
