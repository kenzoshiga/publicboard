      <p>留置権者が目的物の占有を喪失すれば、留置権は消滅する（302条本文）。ただし、留置権者が目的物を賃貸し、又は質権の目的とした場合には、留置権者は、いまだ代理人を通じて占有しているので、留置権を失わない（302条ただし書）。</p>
