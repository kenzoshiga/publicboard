    <div class="blk outer">
      <div class="outer-title" id="A03-04-01-0-03">３　同時履行の抗弁権との差異</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　共通点</p>
          <p class="p6 ind1">留置権、同時履行の抗弁権（533条）は、ともに公平の原則から認められている。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　相違点</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 留置権は物権であるからいったん成立したら、目的物が譲渡されてもその譲受人に対しても主張できる。これに対し、同時履行の抗弁権は主張できなくなる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 留置権は他人の物を留置することができるにとどまるが、同時履行の抗弁権では、給付の内容いかんを問わず履行を拒絶できる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 留置権は、債務者が代担保を提供することによって消滅させられるが（301条）、代担保で同時履行の抗弁権を消滅させることはできないと解されている。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 同時履行の抗弁権で履行を拒絶する債務は、相手方の不履行度合に応じて割合的であり得るが、留置権には不可分性がある（296条）。</p>
