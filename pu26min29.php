      <h3 id="A03-04-02">第２節　成立要件</h3>
    <div class="blk outer">
      <div class="outer-title">本文</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind3 nobold">①　他人の物を占有していること（295条１項本文）</p>
          <p class="h6 ind3 nobold">②　その物に関して生じた債権であること（目的物と債権との牽連関係、295条１項本文）</p>
          <p class="h6 ind3 nobold">③　債権が弁済期にあること（295条１項ただし書）</p>
          <p class="h6 ind3 nobold">④　占有が「不法行為」によって始まったものではないこと（295条２項）</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-04-02-0-01">１　「他人」の物（要件①）</div>
      <p>「他人」には債務者以外の第三者も含む（通説）。</p>
<div class="kakomon"><a href="pu26min29q.php#q-h14-10-a" title="H14-10-ア">過去問</a></div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-04-02-0-02">２　目的物と債権との牽連関係（要件②）</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　牽連関係</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①債権が物自体から生じた場合</p>
          <p class="h6 ind3 nobold">②債権が物の返還義務と同一の法律関係又は事実関係から生じた場合</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　留置権成立の範囲</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 売買代金債権と建物</p>
          <p class="p6 ind2">建物を売却後、売主が引き続き建物を占有していたが、買主が代金を支払う前に第三者に当該建物を売却し、売主が第三者から建物の明渡請求を受けたときは、買主に対する売買代金債権を被担保債権とする留置権が成立しているため、売主は建物を留置することができる（最判昭47.11.16）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 建物買取請求権と敷地</p>
          <p class="p6 ind2">建物買取請求権を有する者がある場合、留置権が成立し、建物のみならずその敷地をも留置することができる（大判昭14.８.24・通説）。</p>
          <p class="p6 ind2">理由①：建物買取請求権を有する者は、その支払を受けるまで建物を留置できるのであるから、この建物引渡拒絶の効力として、敷地の引渡しの拒絶をも認められるべきである。</p>
          <p class="p6 ind2">理由②：もし敷地について留置権の成立を否定すると、留置権者は建物を土地から分離した上で留置せざるを得ず、留置の効力を著しく弱めるのみならず、社会通念にも反する結果となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 費用償還請求権と家屋</p>
          <p class="p6 ind2">家屋につき必要費・有益費を費やした者がある場合、費用償還請求権を被担保債権とする留置権が成立し、その償還を請求（608条）する際に、家屋自体を留置することができる（大判昭14.４.28・通説）。費用部分と他の部分とは明確に区別することが難しく、独立性が全くないか、少なくとも独立性が乏しいと考えられるためである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 造作買取請求権と家屋</p>
          <p class="p6 ind2">造作買取請求権（借地借家法33条）を有する者は、造作のみならず、家屋をも留置することができるか。</p>
          <p class="p6 ind2">→　留置することができない（最判昭29.１.14）。</p>
          <p class="p6 ind2">理由①：建物と造作とは別個の存在であり、かつ造作代金債務と建物引渡債務には対価関係がない。</p>
          <p class="p6 ind2">理由②：従物に関する債権で主物たる建物全体の明渡しを拒絶することができるのは公平に反する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(5) 敷金返還請求権と家屋</p>
          <p class="p6 ind2">→　留置権は成立しない（最判昭49.９.２）。</p>
          <p class="p6 ind2">敷金には、賃借人の与えた損害を担保するという保証金の意味もあるので、敷金返還請求権は建物の明渡し後に発生することから、建物明渡義務は先履行である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(6) 二重譲渡と目的物の留置</p>
          <p class="p6 ind2">不動産を二重に譲渡し、第１譲受人が当該不動産の引渡しを受けた後、第２譲受人が登記を具備して第１譲受人に対してその引渡しを請求した場合、第１譲受人は譲渡人に対する損害賠償請求権を保全するために留置権を主張することはできるか。</p>
          <p class="p6 ind2">→　留置権は成立しない（最判昭43.11.21・通説）。</p>
          <p class="p6 ind2">かかる損害賠償請求権は、引渡請求権者である第三者とは別人に対するものであり、債務成立当初から、被担保債権の債務者と留置権による引渡拒絶の相手方が別人であるときにまでその成立範囲を拡大することは、第三者の負担により債権者を保護するものであり、留置権制度の本来の趣旨を逸脱する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(7) 他人物売買と目的物の留置</p>
          <p class="p6 ind2">他人の物の売買による買主が、その物の真の所有者から返還請求を受けた場合、買主は留置権を行使できない（最判昭51.6.17）。買主の売主に対する損害賠償請求権と、所有者の買主に対する引き渡し請求権とは牽連関係に立たないからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(8) 土地賃貸借と新所有者</p>
          <p class="p6 ind2">対抗力を有しない賃借権の目的不動産が譲渡され、譲受人が明渡しを請求してきたとき、賃借人は前所有者に対して取得する損害賠償請求権に基づいて留置権を主張できるか。</p>
          <p class="p6 ind2">→　留置権は成立しない（大判大11.８.21）。</p>
          <p class="p6 ind2">賃借物を使用・収益する債権は「物に関して生じた債権」ではないため、このような物自体を目的とする債権は、留置権の成立要件とはならない。</p>
        </div>
