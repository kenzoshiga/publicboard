          <p class="p6 ind1">※　必要費の償還請求権を被担保債権として建物を留置中、留置物についてさらに必要費を支出した場合は、既に生じている費用償還請求権と共に、上記建物について留置権を行使することができる（最判昭33.１.17）。</p>
