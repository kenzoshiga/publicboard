      <h3 id="A03-04-01">第１節　意義</h3>
    <div class="blk outer">
      <div class="outer-title" id="A03-04-01-0-01">１　意義</div>
      <p>：他人の物の占有者が、その物に関して生じた債権を有するときに、その債権の弁済を受けるまで、その物を留置できる権利（295条）</p>
      <p>他人の物の占有者が、その物に関して生じた債権を有するときには、その債権の弁済を受けるまで、その物の返還を拒絶することができるものとすることが公平の原則に適うと考えたのである。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-04-01-0-02">２　法的性質</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　担保物権性（通有性）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 付従性・随伴性・不可分性あり。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 物上代位性はない。</p>
          <p class="p6 ind2">留置権は目的物を留置することがその主たる内容であって、目的物の交換価値を把握するものではないからである。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　物権性</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 対抗力</p>
          <p class="p6 ind2">債権の弁済を受けるまでは、何人に対してもその物を留置することができる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 追及力</p>
          <p class="p6 ind2">追及力がない。占有を失えば、留置権は消滅する（302条本文）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 不動産上の留置権</p>
          <p class="p6 ind2">不動産の上にも成立する。対抗要件は不要である。</p>
        </div>
