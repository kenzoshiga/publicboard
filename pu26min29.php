          <p class="p6 ind1">Ｑ　賃借人が、賃貸借契約終了後に必要費を支出した場合等、当初占有権原があったが、後に権原がなくなった場合（権原喪失型）が問題となる。</p><br>
          <p class="p6 ind1">→　295条２項類推適用（最判昭51.６.17・通説）</p>
          <p class="p6 ind1">無権原占有につき広く295条２項の類推適用を認め、悪意ないし善意有過失占有者には留置権を否定する。</p>
