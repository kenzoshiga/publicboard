        <div class="p-card">
          <p class="h6 ind0">3.　遺留分の算定</p>
<div class="jobun">
（遺留分を算定するための財産の価額）
第1043条　遺留分を算定するための財産の価額は、被相続人が相続開始の時において有した財産の価額にその贈与した財産の価額を加えた額から債務の全額を控除した額とする。
２　条件付きの権利又は存続期間の不確定な権利は、家庭裁判所が選任した鑑定人の評価に従って、その価格を定める。
第1044条　贈与は、相続開始前の１年間にしたものに限り、前条の規定によりその価額を算入する。当事者双方が遺留分権利者に損害を加えることを知って贈与をしたときは、１年前の日より前にしたものについても、同様とする。
２ 第904条の規定は、前項に規定する贈与の価額について準用する。
３ 相続人に対する贈与についての第１項の規定の適用については、同項中「１年」とあるのは「10年」と、「価額」とあるのは「価額（婚姻若しくは養子縁組のため又は生計の資本として受けた贈与の価額に限る。）」とする。
