    <h2 id="A09-08">第８章　遺留分</h2>
    <div class="blk outer">
      <div class="outer-title" id="A09-08-00-0-01">１　意義</div>
      <p>遺留分とは、一定の相続人が、相続について法律上取得することを保障されている相続財産の一定の割合であって、被相続人の生前処分又は死因処分によっても奪われることのないものをいう（1042条～1049条）。</p>
      <p>その趣旨は、被相続人による財産処分の自由・取引安全と、相続人の生活の安定・財産の公平な分配という相対立する要求の妥協・調整を図るところにある。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A09-08-00-0-02">２　遺留分の放棄</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 遺留分の放棄は、原則として遺留分権利者の自由である。遺留分は相続人保護のために認められたものだからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 例外的に、相続開始前の遺留分の放棄は、家庭裁判所の許可を要する（1049条１項）。被相続人等が相続人に放棄を強要する危険があるからである。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　効果</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 遺留分をもたない相続人になるにすぎず、相続人たる地位まで失うわけではない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 一部の相続人が遺留分を放棄しても、他の相続人の遺留分には影響を及ぼさず（1049条２項）、被相続人が放棄された部分だけ余計に自由処分をすることができるにとどまる。</p>
        </div>
<div class="kakomon"><a href="pu26min65q.php#q-h28-23-eh10-20-ah06-19-o" title="H28-23-エ">過去問</a></div>
        </div>
      </div>
    </div>
