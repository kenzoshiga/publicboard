第1012条　遺言執行者は、遺言の内容を実現するため、相続財産の管理その他遺言の執行に必要な一切の行為をする権利義務を有する。
２　遺言執行者がある場合には、遺贈の履行は、遺言執行者のみが行うことができる。
３　第644条から第647条まで及び第650条の規定は、遺言執行者について準用する。
（特定財産に関する遺言の執行）
第1014条　前三条の規定は、遺言が相続財産のうち特定の財産に関する場合には、その財産についてのみ適用する。
２　遺産の分割の方法の指定として遺産に属する特定の財産を共同相続人の一人又は数人に承継させる旨の遺言（以下「特定財産承継遺言」という。）があったときは、遺言執行者は、当該共同相続人が第899条の２第１項に規定する対抗要件を備えるために必要な行為をすることができる。
３　前項の財産が預貯金債権である場合には、遺言執行者は、同項に規定する行為のほか、その預金又は貯金の払戻しの請求及びその預金又は貯金に係る契約の解約の申入れをすることができる。ただし、解約の申入れについては、その預貯金債権の全部が特定財産承継遺言の目的である場合に限る。
４　前二項の規定にかかわらず、被相続人が遺言で別段の意思を表示したときは、その意思に従う。
