      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　遺留分権利者</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 兄弟姉妹を除く法定相続人である（1042条）。相続欠格、廃除、相続放棄によって相続権を失った者は、遺留分も有しない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 胎児も生きて産まれることを条件に遺留分を有する（886条）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　遺留分の率</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 相続財産に対する遺留分の率</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 直系尊属のみが相続人の場合は３分の１である（1042条１項１号）。</p>
          <p class="h6 ind2 nobold">(b) その他の場合は２分の１である（同条同項２号）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 遺留分権利者各人の遺留分の率</p>
          <p class="p6 ind2">「相続財産に対する遺留分の率×各遺留分権利者の法定相続分の率」で算出する（1042条２項、900条、901条）。</p>
        </div>
