      <p>多くの場合、遺言の効力が生じてもその具体的実現のためには特別な手続を必要とする。そして遺言の執行は相続人の利益に反することも少なくないため、この手続をすべて相続人に委ねると公平な執行が確保できないおそれがある。そこで、民法は遺言の執行手続について詳細な規定を設けた。</p>
