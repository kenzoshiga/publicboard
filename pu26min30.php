  <h2>第３編　担保物権</h2>
  <div class="p-card chap-card">
    <h2 id="A03-05">第５章　先取特権</h2>
    <div class="p-card sec-card">
      <h3 id="A03-05-01">第１節　意義</h3>
    <div class="blk outer">
      <div class="outer-title" id="A03-05-01-0-01">１　意義</div>
      <p>：法律の定める一定の債権を有する者が、債務者の財産について、他の債権者に先立って自己の債権の弁済を受けることができる法定担保物権</p>
      <p>先取特権は、①当事者の意思の推測、②公平の見地、③社会政策的配慮などから、法が債権者平等の原則の例外として、特殊の債権を特に保護しようとするものである。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-05-01-0-02">２　成立</div>
      <p>法定担保物権であり、当事者の契約で発生させることはできない。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-05-01-0-03">３　性質（通有性）</div>
      <p>付従性・随伴性・不可分性（305条、296条）・物上代位性（304条。ただし、一般の先取特権を除く）を有する。</p>
    </div>
