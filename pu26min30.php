      <div class="outer-title">本文</div>
      <p>先取特権には、一般の先取特権（306条以下）、動産の先取特権（311条以下）、不動産の先取特権（325条以下）がある。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-05-02-0-01">１　一般の先取特権</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">：債務者の一般財産の上に成立する先取特権</p>
          <p class="p6 ind1">一般の先取特権は、債務者の総財産を目的とし、しかも不動産について登記がなくても一般債権者に対抗できるので（336条）、その効力はきわめて強く、一般債権者を害するおそれが大きい。このため、一般の先取特権によって担保される債権を、その性質が特に保護されるべきもので、債権額の比較的小さいものに限定した。</p>
          <p class="p6 ind1">一般先取特権は、まず不動産以外の財産から弁済を受け、不足があれば不動産から弁済を受ける（335条1項）。</p>
          <p class="p6 ind1">2.　種類（306条）</p>
          <p class="p6 ind1">共益の費用（１号）、雇用関係（２号）、子の監護の費用（３号）、葬式の費用（４号）、日用品供給費（５号）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 共益費用（307条）</p>
          <p class="p6 ind2">各債権者の共同の利益のためにされた債務者の財産の保存、清算又は配当に関する費用</p>
          <p class="p6 ind2">ｅｘ．債務者の財産を競売した場合の競売手続の費用</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 雇用関係（308条）</p>
          <p class="p6 ind2">給料その他債務者と使用人との間の雇用関係に基づいて生じた債権</p>
          <p class="p6 ind2">雇用主に比べて弱い立場である労働者を保護する趣旨である。</p>
          <p class="p6 ind2">通常の給料だけでなく、パートや日雇い、退職金や賞与でも成立する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 子の監護の費用（308条の２）</p>
          <p class="p6 ind2">子の監護の費用の先取特権は、次の①から④の義務に係る確定期限の定めのある定期金債権の各期における定期金のうち子の監護に要する費用として相当な額（子の監護に要する標準的な費用その他の事情を勘案して当該定期金により扶養を受けるべき子の数に応じて法務省令で定めるところにより算定した額）について存在する。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①752条の規定による夫婦間の協力及び扶助の義務</p>
          <p class="h6 ind3 nobold">②760条の規定による婚姻から生ずる費用の分担の義務</p>
          <p class="h6 ind3 nobold">③766条及び766条の3（これらの規定を749条、771条及び788条において準用する場合を含む。）の規定による子の監護に関する義務</p>
          <p class="h6 ind3 nobold">④877条から第880条までの規定による扶養の義務</p>
          <p class="p6 ind4">　養育費債権は重要な権利であるため、子の監護の費用に一般先取特権が成立することとして、債権者である子の父又は母は、債務名義がなくても、民事執行（担保権実行等）の申立てをすることができ、かつ、他の一般債権者に優先して弁済を受けられるようにしたものである。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 葬式の費用（309条）</p>
          <p class="p6 ind2">葬儀屋の債権が被担保債権となる。</p>
          <p class="p6 ind2">ｅｘ．債務者のためになされた葬式の費用</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(5) 日用品供給費（310条）</p>
          <p class="p6 ind2">日用品の供給の先取特権は、債務者又はその扶養すべき同居の親族及びその家事使用人の生活に必要な最後の６か月間の飲食料品、燃料及び電気の供給について存在する（310条）。</p>
          <p class="p6 ind2">ｅｘ．電気代（電力会社が債権者となる）</p>
          <p class="p6 ind2">債務者とその家族の生活の維持のための規定であるため、債務者は自然人に限られ、法人は含まない（最判昭46.10.21）。</p>
