      <p>先取特権は、目的物の占有を要しない非占有担保物権である点で抵当権と類似しているから、先取特権の効力については抵当権の規定（目的物の範囲・370条、被担保債権の範囲・375条、代価弁済・378条、抵当権消滅請求・379条等）が準用される（341条）。</p>
