  <h2>第８編　親　族</h2>
  <div class="p-card chap-card">
    <h2 id="A08-04">第４章　親子・親権</h2>
    <div class="p-card sec-card">
      <h3 id="A08-04-02">第２節　実子</h3>
    <div class="p-card rank4-card">
      <p id="A08-04-02-1" class="h6 ind0">Ⅱ　嫡出でない子（非嫡出子）</p>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-02-1-01">１　意義</div>
      <p>生まれた子の父母に婚姻関係がない場合、その子のことを嫡出でない子（非嫡出子）という。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-02-1-02">２　認知</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">非嫡出子であっても実の親子である以上、両親とその子との間に生物学的親子関係は生じている。しかし、非嫡出子について、民法は、法律的親子関係を生じさせるためには実の親子であること（血縁の存在）に加え、親の意思を必要とした。これが認知である。認知には、父が認知届を出すことによって行う任意認知と、任意認知のない場合に子から父に対して行う強制認知（認知の訴え）とがある。</p>
          <p class="p6 ind1">非嫡出子は、認知によらないで父との間の親子関係の存在確認の訴えを提起することはできない。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 母の認知</p>
          <p class="p6 ind2">民法は、父だけでなく母も認知することができると規定している（779条）。しかし、非嫡出子とその母との親子関係は、分娩の事実によって明らかであるから認知は不要である（判例・通説）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 認知における意思主義と事実主義</p>
          <p class="p6 ind2">認知についての考え方としては、父の家庭の平和等から父の意思を尊重すべきとする立場（意思主義）と、子の利益等から事実としての親子関係（血縁）を重視すべきとする立場（事実主義、血縁主義）とがある。民法は２つの認知の方法を認めており、両者の調和を図ろうとするものといえる。</p>
        </div>
<div class="kakomon"><a href="pu26min56q.php#q-h25-21-a" title="H25-21-ア">過去問</a></div>
        </div>
        <div class="p-card" id="min56-q4">
          <p class="h6 ind0">2.　任意認知</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 要件</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 主体</p>
          <p class="p6 ind3">父〔又は母〕（779条）</p>
          <p class="p6 ind3">未成年者又は成年被後見人であっても、法定代理人の同意は必要ない（780条）。また、代理によることもできない。認知は一種の身分行為だからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 承諾を必要とする場合</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　成年の子（18歳以上の子。4条）を認知する場合には、その子の承諾が必要である（782条）。</p>
          <p class="p6 ind4">子に対する扶養義務を果していないにもかかわらず、子が一人前になったら認知して扶養を期待しようなどとする利己的な認知を防ぐためである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　胎児を認知する場合には、母の承諾が必要である（783条１項）。</p>
          <p class="p6 ind4">母の名誉・利益を守るためである。</p>
          <p class="p6 ind4">認知した胎児が出生した場合において、772条の規定によりその子の父が定められるときは、胎児に対する認知は効力を生じない（783条２項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅲ　死亡した子の認知は、死亡した子に直系卑属があるときに限りすることができ、その直系卑属が成年者の場合にはその者の承諾が必要である（783条３項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅳ　方式</p>
          <p class="p6 ind4">認知は要式行為であり、戸籍法の定めるところにより届け出ることによってする（781条１項）。この認知の届出は創設的届出である。遺言ですることもできる（781条２項）。</p>
        </div>
<div class="kakomon"><a href="pu26min56q.php#q-r04-20-i" title="R4-20-イ">過去問</a></div>
        </div>
        </div>
        <div class="p-card" id="min56-q12">
          <p class="h6 ind1">(2) 認知の取消し</p>
          <p class="p6 ind2">Ｑ　785条は、認知の取消しを禁止しているが、認知が詐欺・強迫によってなされた場合にも、認知者はこれを理由に取り消すことができないのか。</p><br>
          <p class="p6 ind2">Ａ　肯定説</p>
          <p class="p6 ind2">（理由）</p>
          <p class="h6 ind3 nobold">①意思主義の重視</p>
          <p class="h6 ind3 nobold">②785条の「取消し」は「撤回」の意味である。</p><br>
          <p class="p6 ind4">Ｂ　否定説</p>
          <p class="p6 ind4">（理由）</p>
          <p class="p6 ind4">事実主義からは、真実の親子関係がある以上、たとえ認知がなされるにつき詐欺・強迫があっても、認知の効力には影響を生じない。</p>
        </div>
<div class="kakomon"><a href="pu26min56q.php#q-h06-22-i" title="H6-22-イ">過去問</a></div>
        <div class="p-card" id="min56-q13">
          <p class="h6 ind1">(3) 認知の無効</p>
          <p class="p6 ind2">認知も意思表示であるから、認知意思を欠く場合、父の意思によらず第三者が勝手に認知届を出した場合等は無効である（最判昭52.２.14）。</p>
          <p class="p6 ind2">また、事実に反する認知も無効である。786条はその場合に認知の無効が主張できることを定めている。</p>
        </div>
<div class="kakomon"><a href="pu26min56q.php#q-h16-24-u" title="H16-24-ウ">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(4) 認知無効の訴え（786条）</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 原則の提訴期間</p>
          <p class="p6 ind3">次に掲げる者は、それぞれ当該各号に定める時（胎児の認知（783条１項）がされた場合は、子の出生の時）から７年以内に限り、認知について反対の事実があることを理由として、認知の無効の訴えを提起することができる（786条１項本文）。</p>
          <p class="p6 ind3"><img class="embed-img" src="img/min56-01.png" alt="img/min56-01.png"></p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 子の提訴期間が延長される場合（786条２項）</p>
          <p class="p6 ind3">子は、その子を認知した者と認知後に継続して同居した期間（当該期間が２以上あるときは、そのうち最も長い期間）が３年を下回るときは、前記(a)表ⅰの規定にかかわらず、21歳に達するまでの間、認知の無効の訴えを提起することができる（786条２項本文）。ただし、子による認知の無効の主張が認知をした者による養育の状況に照らして認知をした者の利益を著しく害するときは、この限りでない（786条２項ただし書）。</p>
          <p class="p6 ind3">この規定は、子の法定代理人が第１項の認知の無効の訴えを提起する場合には、適用しない（786条３項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 監護費用の償還の制限（786条４項）</p>
          <p class="p6 ind3">認知無効の訴え（786条１項、２項）により認知が無効とされた場合であっても、子は、認知をした者が支出した子の監護に要した費用を償還する義務を負わない（786条４項）。</p>
        </div>
        </div>
        </div>
        <div class="p-card" id="min56-q14">
          <p class="h6 ind0">3.　強制認知〔認知の訴え〕（787条）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 当事者</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 原告：子、その直系卑属又はこれらの者の法定代理人（787条本文）</p>
          <p class="p6 ind3">胎児及びその母は提訴できない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 被告：父、父の死亡後は検察官（人事訴訟法44条１項）</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 出訴期間</p>
          <p class="p6 ind2">父の死後３年以内（父の生存中はいつでもよい）に出訴しなければならない（787条ただし書）。</p>
        </div>
<div class="kakomon"><a href="pu26min56q.php#q-h11-18-o" title="H11-18-オ">過去問</a></div>
        </div>
        <div class="p-card" id="min56-q16">
          <p class="h6 ind0">4.　認知の効果</p>
          <p class="p6 ind1">認知があると親子関係が生じ、親子関係に認められる全部の効果（親権、扶養、相続等）が発生する。その効果は出生時にさかのぼって発生するが、第三者の権利を害することはできない（784条）。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 親権</p>
          <p class="p6 ind2">非嫡出子については母が親権を行うが、父が認知した後に、父母の協議で父母の双方又は父を親権者と定めることができる（改正民法819条４項）。</p>
          <p class="p6 ind2">父母の協議が調わないとき又は協議をすることができないときは、家庭裁判所は、父又は母の請求によって、協議に代わる審判をすることができる（改正民法819条５項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 扶養</p>
          <p class="p6 ind2">父は認知によって子の出生時より扶養義務を負っていたことになる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 相続</p>
          <p class="p6 ind2">相続が問題となるのは、死後認知の場合であり、それについては特則がある（910条。後述）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 氏と戸籍</p>
          <p class="p6 ind2">氏と戸籍については、認知によって直接の影響はない。父の認知後も子は依然母の氏を称し、母の戸籍に属する。もっとも、子は家庭裁判所の許可を得て、戸籍法の定めるところにより届け出ることにより、父の氏を称することができる（791条１項）。</p>
        </div>
