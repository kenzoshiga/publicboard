    <div class="blk outer">
      <div class="outer-title" id="A08-04-02-1-03">３　準正</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">準正とは、非嫡出子が父母の婚姻を契機に嫡出子となる制度をいう。</p>
          <p class="p6 ind1">このうち、認知が先行し、婚姻によって準正が生じる場合を婚姻準正（789条１項）といい、婚姻が先行し、認知によって準正が生じる場合を認知準正（同条２項）という。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　準正の効果</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 婚姻準正の効果は「婚姻の時から」生じる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 認知準正の効果は「認知の時から」生じると規定されている（789条２項）。</p>
          <p class="p6 ind2">しかし、通説は、婚姻準正との均衡から、この場合も「婚姻の時から」生じると解している。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 準正の効果を反対の意思表示により妨げることはできない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 準正が生じた後に婚姻が解消されても準正の効果に影響はない。また、一旦婚姻が成立し、婚姻解消後に認知がされた場合にも準正が生じる。</p>
        </div>
