      <h3 id="A08-04-03">第３節　親子の氏</h3>
    <div class="blk outer">
      <div class="outer-title">本文</div>
      <p>親子の氏は、原則として同一となるよう規定されている（親子同氏の原則）。しかし、事情によっては、親子で異なる氏となる場合も生じるため、子の氏の変更が認められている。以下、子の氏とその変更について説明する。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-03-0-01">１　原則</div>
      <div class="blk inner">
        <div class="p-card" id="min56-q26">
          <p class="h6 ind0">1.　嫡出子</p>
          <p class="p6 ind1">嫡出子は父母の氏を称する（790条１項本文）。ただし、子の出生前に父母が離婚したときは、離婚の際の父母の氏を称する（同条項ただし書）。</p>
          <p class="p6 ind1">なお、養子の場合も親子同氏の原則が適用され、養子は養親の氏を称する（810条本文）。ただし、婚姻によって氏を改めた者が、婚姻の際に定めた氏を称すべき間は、その限りでない（同条ただし書）。</p>
        </div>
<div class="kakomon"><a href="pu26min56q.php#q-h29-20-eh26-20-uh13-18-u" title="H29-20-エ">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind0">2.　非嫡出子</p>
          <p class="p6 ind1">非嫡出子は母の氏を称する（790条２項）。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-03-0-02">２　子の氏の変更</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　家庭裁判所の許可を得てする氏の変更</p>
          <p class="p6 ind1">子が父又は母と氏を異にする場合には、子は家庭裁判所の許可を得て、戸籍法の定めるところにより届け出ることによって、その父又は母の氏を称することができる（791条１項）。</p>
          <p class="p6 ind1">ex 1.父母が離婚し、母が復氏したときに、その嫡出子が母の氏に変更する場合</p>
          <p class="p6 ind1">ex 2.非嫡出子が、認知した父の氏に変更する場合</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　家庭裁判所の許可を要しない氏の変更</p>
          <p class="p6 ind1">父又は母が氏を改めたことにより子が父母と氏を異にする場合には、子は、父母の婚姻中に限り、家庭裁判所の許可を得ないで、戸籍法の定めるところにより届け出ることによって、その父母の氏を称することができる（791条２項）。</p>
          <p class="p6 ind1">ex. 現行法の下では、たとえ準正が生じても、当然には子の氏は変更されない。そこで、母の氏を称する子は、父母の婚姻によって母が氏を改めた場合には、届出のみで父母の氏を称することができる。</p>
