          <p class="p6 ind4">以上２つの結果から、この要件は推定されることになり、即時取得を否認する者に立証責任が負わされる。</p>
          <p class="p6 ind4">法人における善意・無過失は、その法人の代表者について決するが、代理人が取引行為をしたときは、その代理人について決すべきである（最判昭47.11.21）。</p>
