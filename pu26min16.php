          <p class="h6 ind2 nobold">(a) 占有改定（183条）と即時取得</p>
          <p class="p6 ind3">　占有改定による即時取得はできない（最判昭35.2.11）</p><br>
          <p class="p6 ind3">（理由）</p>
          <p class="h6 ind3 nobold">①一般の外観上従来の占有事実の状態に何らの変更の生じない占有改定により即時取得が生じるとすれば、他人の利害関係、ことに従前占有を他人に委ねた権利者の利害を全然顧慮しないことになる。</p>
          <p class="h6 ind3 nobold">②対抗要件としての「引渡し」（178条）と権利取得の要件としての「占有」（192条）とは分けて考えるべきである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 指図による占有移転（184条）と即時取得</p>
          <p class="p6 ind3">指図による占有移転によって即時取得は成立するだろうか。指図による占有移転も、外観上物の占有状態に変動を生じさせない点では、占有改定と同じであり問題となる。</p>
          <p class="p6 ind3">→　肯定説（最判昭57.９.７）</p>
          <p class="p6 ind3">（理由）</p>
          <p class="h6 ind3 nobold">①所持こそ動いていないが、真正権利者の信頼は形の上でも完全に裏切られている。</p>
          <p class="h6 ind3 nobold">②譲渡人が現に所持していない点と、第三者たる所持人に対する命令を必要とする点（184条）で、即時取得行為の存否を比較的に外部から認識することができる。</p>
