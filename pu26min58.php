    <div class="blk outer">
      <div class="outer-title" id="A08-04-05-0-03">３　親権の内容</div>
      <div class="blk inner">
        <div class="p-card" id="min58-q11">
          <p class="h6 ind0">1.　身上監護権</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 監護教育</p>
          <p class="p6 ind2">親権を行う者は、子の利益のために子の監護及び教育をする権利を有し、義務を負う（820条）。</p>
          <p class="p6 ind2">親権者は、他人に優先して子の監護教育についての方針を決定することができる。</p>
          <p class="p6 ind2">820条は、親権者の監護教育に関する権利義務をうたった包括的規定と解されている。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 子の人格の尊重等（821条）</p>
          <p class="p6 ind2">親権を行う者は、前条（820条）の規定による監護及び教育をするに当たっては、子の人格を尊重するとともに、その年齢及び発達の程度に配慮しなければならず、かつ、体罰その他の子の心身の健全な発達に有害な影響を及ぼす言動をしてはならない（821条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 居所指定権</p>
          <p class="p6 ind2">子は親権を行う者が指定した場所にその居所を定めなければならない（822条）。</p>
          <p class="p6 ind2">居所指定は、子に意思能力がある場合にだけ意味がある。</p>
        </div>
<div class="kakomon"><a href="pu26min58q.php#q-pu58-1" title="q-pu58-1">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　財産管理権</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 法定代理権</p>
          <p class="p6 ind2">親権者は、子の財産を管理し、その財産に関する法律行為について子を代表する（824条本文）。</p>
          <p class="p6 ind2">「管理」には、財産の保存、利用、改良のほか処分をも含む。</p>
          <p class="p6 ind2">ここにいう「代表」とは、代理と同義であると解されている。同条は親権者に法定代理権を認めるものである。</p>
          <p class="p6 ind2">父母の共同親権の場合には、共同代理となる。</p>
          <p class="p6 ind2">親権者自身の利益のために代理行為が行われた場合は、代理権限の濫用の問題となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 管理の対象となる財産</p>
          <p class="p6 ind2">子のすべての財産が管理の対象となるのが原則である。</p>
          <p class="p6 ind2">例外として、親権者が処分を許した財産（５条３項）、営業の許可を受けた子が管理する営業財産（６条１項）には親権者の管理権は及ばない。</p>
        </div>
        <div class="p-card" id="min58-q12">
          <p class="h6 ind1">(3) 注意義務</p>
          <p class="p6 ind2">親権者は財産管理権を行使するにあたり、自己のためにすると同一の注意義務を負う（827条）。</p>
          <p class="p6 ind2">※未成年後見人には善管注意義務が要求される（869条、644条）。</p>
        </div>
<div class="kakomon"><a href="pu26min58q.php#q-h28-21-ah19-21-oh06-21-i" title="H28-21-ア">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(4) 職業許可権</p>
          <p class="p6 ind2">子は、親権者の許可を得なければ、職業を営むことができない（823条１項）。</p>
          <p class="p6 ind2">未成年者が未だその営業に堪えることができない事由があるときは、親権者は、職業の許可を取り消し、又は制限することができる（823条２項）。</p>
