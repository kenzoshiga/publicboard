<section class="sec-title" id="A08">
  <h2>第８編　親　族</h2>
  <div class="p-card chap-card">
    <h2 id="A08-04">第４章　親子・親権</h2>
    <div class="p-card sec-card">
      <h3 id="A08-04-05">第５節　親権</h3>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-05-0-01">１　意義</div>
      <p>親権とは、未成年の子に対する父母の権利義務の総称をいう。</p>
      <p>民法は、親が未成年の子に対し「監護及び教育をする権利を有し、義務を負う」（820条）とし、親権の行使は権利であるとともに義務でもあることを明言している。</p>
      <p>なお、父母は親権の有無にかかわらず子の養育にかかわり責任を果たさなければならないため、令和6年民法改正では、親の責務を次のように明確化した。</p>
      <p>父母は、子の心身の健全な発達を図るため、その子の人格を尊重するとともに、その子の年齢及び発達の程度に配慮してその子を養育しなければならず、かつ、その子が自己と同程度の生活を維持することができるよう扶養しなければならない（改正民法817条の12第１項）。</p>
      <p>父母は、婚姻関係の有無にかかわらず、子に関する権利の行使又は義務の履行に関し、その子の利益のため、互いに人格を尊重し協力しなければならない（改正民法817条の12第２項）。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A08-04-05-0-02">２　親権の当事者</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　親権に服する子</p>
          <p class="p6 ind1">成年に達しない子である（818条１項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　親権者</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 親権を有する者</p>
          <p class="p6 ind2">親権は、成年に達しない子について、その子の利益のために行使しなければならない（改正民法818条1項）。誰が親権者になるかについては、父母の法的地位（婚姻中か離婚後か未婚か）や、子の法的地位（実子か養子か、嫡出子か非嫡出子か）によって、決まる。</p>
          <p class="p6 ind2">未成年者が養子になると、次の者が親権者となって親権を行使する（改正民法818条３項）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①養親（当該子を養子とする縁組が２以上あるときは、直近の縁組により養</p>
          <p class="p6 ind4">親となった者に限る。）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">②子の父母であって、①に掲げる養親の配偶者であるもの</p>
        </div>
        </div>
        <div class="p-card" id="min58-q1">
          <p class="h6 ind1">(2) 親権を行う能力</p>
          <p class="p6 ind2">父又は母自身も未成年者であるときは親権を行うことができず、その父又は母の親権者（又は未成年後見人）が、代わって親権を行う（833条、867条１項）。</p>
          <p class="p6 ind2">親権を有する父又は母が成年被後見人であるときは親権を行うことができない（通説）。被保佐人も親権を行うことができない（大判明44.11.27）。</p>
        </div>
<div class="kakomon"><a href="pu26min58q.php#q-h26-21-a" title="H26-21-ア">過去問</a></div>
        <div class="p-card" id="min58-q2">
          <p class="h6 ind1">(3) 親権の行使</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 父母の婚姻中－原則</p>
          <p class="p6 ind3">親権は、父母の婚姻中は、父母が共同して行う（親権共同行使の原則、818条２項、824条の２第１項本文）。父母は、実父母、養父母、父母の一方が実親で一方が養親である場合を含む。</p>
          <p class="p6 ind3">婚姻中の父母の一方が単独で子の財産に関してした行為は無効である（最判昭28.11.26）。</p>
          <p class="p6 ind3">「共同して行う」とは、親権の内容を父母が共同して決定することを意味する。父のみの名義でなされた不動産の売買でも、母の同意があれば有効である（最判昭32.７.５）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 例外</p>
          <p class="p6 ind3">次の①から③のときの親権行使は、その一方が行う（改正民法824条の２第１項ただし書）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①その一方のみが親権者であるとき</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">②他の一方が親権を行うことができないとき</p>
          <p class="p6 ind4">法律上親権を行うことができない場合（ex. 後見開始、親権喪失宣告）のほか、事実上これができない場合（ex. 行方不明、長期の旅行、受刑）も含む。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">③子の利益のため急迫の事情があるとき</p>
          <p class="p6 ind4">父母は、その双方が親権者であるときであっても、親権共同行使の原則にかかわらず、監護及び教育に関する日常の行為（※）に係る親権の行使を単独ですることができる（改正民法824条の２第２項）。</p>
          <p class="p6 ind4">※上記の規定は、父母の婚姻中にも、離婚後にも適用される。</p>
<div class="ftnote">
(※)子の食事や服装、風邪など生活で生ずる医療行為の決定など生活で生じる身上監護に関する行為で子に対して重大な影響を与えないものを指す。
</div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 単独親権となる例及びその変更等</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　父母の一方が死亡し、又は失踪宣告を受け、その他親権を失ったときは、他方が単独で親権者となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　父母が協議離婚するときは、協議で父母の双方又は一方を親権者と定め（改正民法819条１項）、裁判離婚する場合は、裁判所が父母の双方又は一方を親権者と定める（同条２項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅲ　子の出生前に父母が離婚した場合には、親権は、母が行う（改正民法819条３項本文）。ただし、子の出生後に、父母の協議で、父母の双方又は父を親権者と定めることができる（同項ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅳ　非嫡出子については母が親権を行うが、父が認知した後に、父母の協議で父母の双方又は父を親権者と定めることができる（改正民法819条４項）。</p>
          <p class="p6 ind4">なお、ⅲ及びⅳの協議が不調又は不能の場合には、家庭裁判所は、父又は母の請求によって、協議に代わる審判をすることができる（同条５項）。</p>
        </div>
        </div>
        </div>
        <div class="p-card" id="min58-q8">
          <p class="h6 ind1">(4) 家庭裁判所による親権者の変更</p>
          <p class="p6 ind2">子の利益のため必要があると認めるときは、家庭裁判所は、子又はその親族の請求によって、親権者を変更することができる（改正民法819条６項）。</p>
          <p class="p6 ind2">民法819条８項では、家庭裁判所が上記の親権者の変更を判断する際の基準について規定している。</p><br>
          <p class="p6 ind2">＜判例＞　最決平26.４.14</p>
          <p class="p6 ind2">「子が実親の一方及び養親の共同親権に服する場合、民法819条6項の規定に基づき、子の親権者を他の一方の実親に変更することはできない（最決平26.4.14）。」</p>
          <p class="p6 ind2">（理由）</p>
          <p class="p6 ind2">・民法819条６項は、子が実親の一方及び養親の共同親権に服する場合に子の親権者を他の一方の実親に変更することは予定していない</p>
          <p class="p6 ind2">・親権者による親権の行使が不適切で子の保護の観点から何らかの措置をとる必要があるときは、親権喪失の審判等（834条、834条の２参照）を通じて子の保護を図ることも可能である</p>
        </div>
<div class="kakomon"><a href="pu26min58q.php#q-r02-20-eh26-20-ih12-22-a" title="R2-20-エ">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(5) 裁判所が親権者を定める際の考慮要素</p>
          <p class="p6 ind2">裁判所は、819条２項・同条５項・同条６項（前記(3)(ｃ)ⅱ・ⅲ・ⅳ・(4)）の裁判において、父母の双方を親権者と定めるかその一方を親権者と定めるかを判断するに当たっては、子の利益のため、父母と子との関係、父と母との関係その他一切の事情を考慮しなければならない。この場合において、次のⅰ・ⅱのいずれかに該当するときその他の父母の双方を親権者と定めることにより子の利益を害すると認められるときは、父母の一方を親権者と定めなければならない（改正民法819条７項）。</p>
<div class="ftnote">
虐待・ＤＶ等のおそれがある場合や父母の双方を親権者とすることが子の利益を害する場合には必ず単独親権としなければならない旨を規定している。
</div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　父又は母が子の心身に害悪を及ぼすおそれがあると認められるとき</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　父母の一方が他の一方から身体に対する暴力その他の心身に有害な影響を及ぼす言動を受けるおそれの有無、協議が調わない理由その他の事情を考慮して、父母が共同して親権を行うことが困難であると認められるとき</p>
          <p class="p6 ind4">※上記の規定は、養子が15歳未満である場合の離縁において、養子の実父母が離婚している場合にする離縁後に親権者となるべき者の協議（811条３項）が不調又は不能であるときの家庭裁判所による協議に代わる審判で準用される（改正民法811条４項）。</p>
        </div>
<div class="kakomon"><a href="pu26min58q.php#q-r02-20-ah26-21-uh12-22-i" title="R2-20-ア">過去問</a></div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(6) 特定の事項に係る親権行使者の指定</p>
          <p class="p6 ind2">特定の事項に係る親権の行使（前記(3)(b)の規定により父母の一方が単独で行うことができるものを除く。）について、父母間に協議が調わない場合であって、子の利益のため必要があると認めるときは、家庭裁判所は、父又は母の請求により、当該事項に係る親権の行使を父母の一方が単独ですることができる旨を定めることができる（改正民法824条の２第３項）。</p>
<div class="ftnote">
父母の意見の対立により親権行使がされないことが子の不利益となり得るため規定している。
養子縁組の代諾は、この「特定の事項」の対象となる。
</div>
        </div>
        </div>
