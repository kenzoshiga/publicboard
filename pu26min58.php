      <div class="outer-title" id="A08-04-05-0-04">４　利益相反行為についての親権の制限</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">親権を行う父又は母とその子との利益が相反する行為（利益相反行為）については、親権者は、その子のために特別代理人の選任を家庭裁判所に請求しなければならない（826条１項）。そして、この特別代理人に代理又は同意させなければならない。</p>
          <p class="p6 ind1">数人の子に対して親権を行う場合において、子の１人と他の子との利益が相反する行為についても同様である（同条２項）。</p>
          <p class="p6 ind1">この規定は、後見人にも準用されている（860条）。</p>
          <p class="p6 ind1">※　共同親権者の１人とだけ利益が相反する場合には、他方の単独代理を認めるのではなく、特別代理人と利益相反関係のない親権者との共同代理とすべきである（最判昭35.２.25）。</p>
        </div>
        <div class="p-card" id="min58-q13">
          <p class="h6 ind0">2.　利益相反</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 判断基準</p>
          <p class="p6 ind2">利益相反行為に当たるかどうかは、親権者の動機や意図ではなく、もっぱらその行為の外形で決すべきであるとされている（最判昭42.４.18）。取引の安全を図るためである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 具体例</p>
          <p class="p6 ind2">判例に現れた具体例には、以下のようなものがある。</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 財産の譲渡</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　親権者が子に財産を無償で贈与することは利益相反行為にならないが（大判大９.１.21）、それが債務を伴うものであるときは利益相反行為となる（大判昭12.５.18）。</p>
          <p class="h6 ind3 nobold">ⅱ　子の財産を親権者に譲渡する行為は、それが有償であっても、利益相反行為となる（大判昭６.３.９）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 担保権の設定等</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　親権者の債務について、子を連帯債務者（最判昭45.11.24）又は保証人（大判昭11.８.７）にしたり、子の財産に抵当権を設定すること（最判昭37.10.２）は、利益相反行為に当たる。</p>
          <p class="h6 ind3 nobold">ⅱ　他人の債務について、親権者と子がともに連帯保証人になるとともに、子との共有不動産の全部につき物上保証をすることは、利益相反行為に当たる（最判昭43.10.８）。</p>
          <p class="h6 ind3 nobold">ⅲ　親権者である母が、第三者（子の親権者ではない夫）の担保のため、子所有の不動産に抵当権を設定する行為は、利益相反行為にならない（最判昭35.７.15）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 遺産分割の協議</p>
          <p class="p6 ind3">利益相反行為に当たる（最判昭48.４.24）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(d) 相続の放棄</p>
          <p class="p6 ind3">親権者が未成年の子に先立ち、又は同時に相続を放棄する場合には、親権者が未成年の子を代理してする相続の放棄は、利益相反行為に当たらない（最判昭53.２.24）。</p>
        </div>
