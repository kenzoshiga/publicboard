      <div class="outer-title" id="A06-02-04-0-07">７　目的物の滅失・損傷に関する危険の移転</div>
<div class="jobun">
（目的物の滅失等についての危険の移転）
第567条　売主が買主に目的物（売買の目的として特定したものに限る。以下この条において同じ。）を引き渡した場合において、その引渡しがあった時以後にその目的物が当事者双方の責めに帰することができない事由によって滅失し、又は損傷したときは、買主は、その滅失又は損傷を理由として、履行の追完の請求、代金の減額の請求、損害賠償の請求及び契約の解除をすることができない。この場合において、買主は、代金の支払を拒むことができない。
２　売主が契約の内容に適合する目的物をもって、その引渡しの債務の履行を提供したにもかかわらず、買主がその履行を受けることを拒み、又は受けることができない場合において、その履行の提供があった時以後に当事者双方の責めに帰することができない事由によってその目的物が滅失し、又は損傷したときも、前項と同様とする。
</div>
      <p>危険移転時を「引渡し時」とする特約が締結されている実情をもとに、567条１項は、引渡しを基準として目的物の滅失・損傷の危険を買主に移転させることを明文化したものである。</p>
      <p>567条２項は、受領遅滞の場合の危険の移転を明文化した。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　引渡し後の滅失・損傷</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 特定物の売買の場合及び種類物売買で目的物の特定がされている場合</p>
          <p class="p6 ind2">目的物の滅失・損傷に関する危険は、目的物の引渡しによって、売主から買主に移転する（危険移転時＝引渡し時）。</p>
          <p class="p6 ind2">その結果、その引渡しがあった時以後にその目的物が当事者双方の責めに帰することができない事由によって滅失し、又は損傷したときは、買主は、その滅失又は損傷を理由として、履行の追完の請求、代金の減額の請求、損害賠償の請求及び契約の解除をすることができない。この場合において、買主は、代金の支払を拒むことができない（567条１項）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　他の契約不適合事由に基づく売主の責任</p>
          <p class="p6 ind4">滅失又は損傷以外に契約不適合が生じていた場合に、契約不適合に基づく売主に対する請求をすることは可能である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　引渡し後の滅失・損傷が売主の責めに帰すべき事由による場合</p>
          <p class="p6 ind4">引渡し後の滅失・損傷が売主の責めに帰すべき事由による場合は、買主は、目的物の滅失・損傷を理由として、上記の権利を行使できる。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 種類物売買で目的物の特定がされていない場合</p>
          <p class="p6 ind2">売主が契約の内容に適合しない目的物を選定して引き渡しても「特定」の効果が生じない。</p>
          <p class="p6 ind2">この場合は、567条の適用外であり（同条１項括弧書参照）、買主は、引渡し時における契約不適合を理由とする権利主張をすることができる。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　受領遅滞による危険の移転</p>
          <p class="p6 ind1">売主が契約の内容に適合する目的物をもって、その引渡しの債務の履行を提供したにもかかわらず、買主がその履行を受けることを拒み、又は受けることができない場合において、その履行の提供があった時以後に当事者双方の責めに帰することができない事由によってその目的物が滅失し、又は損傷したときも、「１　引渡し後の滅失・損傷」と同様とする（567条２項）。</p>
