<section class="sec-title" id="A06">
  <h2>第６編　契約各論</h2>
  <div class="p-card chap-card">
    <h2 id="A06-02">第２章　売買</h2>
    <div class="p-card sec-card">
      <h3 id="A06-02-04">第４節　売主の契約不適合責任</h3>
    <div class="blk outer">
      <div class="outer-title" id="A06-02-04-0-01">１　契約不適合責任の類型・意義</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　総説</p>
          <p class="p6 ind1">契約不適合責任とは、契約の目的物が本来の契約内容と異なっていた場合に、買主はどのような請求ができ、売主はどのような義務を負うか？という問題である。民法は、売主には契約の内容に適合した権利の移転・目的物の引渡しをなすべき義務があることを前提に、その義務の不履行に対する買主の救済手段に関する統一的な規定を定めている。</p>
          <p class="p6 ind1">目的物の契約不適合（562条）のほか、権利に関する契約不適合（565条）についても規定がある。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　契約不適合の類型</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 562条で定める類型</p>
          <p class="p6 ind2">562条では、目的物に関する契約不適合の類型を定めている。</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 種類に関する契約不適合</p>
          <p class="h6 ind2 nobold">(b) 品質に関する契約不適合</p>
          <p class="h6 ind2 nobold">(c) 数量に関する契約不適合</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 権利に関する契約不適合（565条）</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　契約不適合の意義</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 契約内容の確定</p>
          <p class="p6 ind2">売買契約の当事者が当該売買契約において目的物の種類・品質・数量に対してどのような意味を与えたのかを契約の解釈を通じて探究し、それによって導かれた契約の内容に即してみたときに「あるべき」種類・品質・数量が欠如している場合が契約不適合となる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 対象となる目的物</p>
          <p class="p6 ind2">売買の目的物に限定は付されていない。特定物か不特定物か、代替物か不代替物かを問わない。</p>
          <p class="p6 ind2">ただし、引き渡されたことが必要であり、履行遅滞や履行不能は対象外である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 種類・品質に関する契約不適合</p>
          <p class="p6 ind2">物質面での欠点のみならず、いわゆる環境瑕疵（日照・景観阻害など）や心理的瑕疵も含まれる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(4) 数量に関する契約不適合</p>
          <p class="p6 ind2">売買の目的物に数量不足があったすべての場合に数量に関する契約不適合があったとされるわけではない。</p>
          <p class="p6 ind2">売買契約の当事者が当該契約のもとで「数量」に特別の意味を与え、それを基礎として売買がされたという場合にはじめて、数量に関する契約不適合があったと評価されるというべきである。</p>
        </div>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A06-02-04-0-02">２　権利に関する契約不適合</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　権利に関する契約不適合</p>
<div class="jobun">
（移転した権利が契約の内容に適合しない場合における売主の担保責任）
第565条　前３条の規定は、売主が買主に移転した権利が契約の内容に適合しないものである場合（権利の一部が他人に属する場合においてその権利の一部を移転しないときを含む。）について準用する。
</div>
        <div class="p-card">
          <p class="h6 ind1">(1) 総説</p>
          <p class="p6 ind2">売主には売買契約の内容に適合した権利を移転すべき義務があることを前提に、民法は、移転した権利に関して契約不適合があった場合についても目的物に関する契約不適合と同様に買主の各種請求を可能とする規定を置いている（565条による562条、563条及び564条の準用）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 意義</p>
          <p class="p6 ind2">移転した権利の契約不適合とは、以下のような場合を指す。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　売買の目的である不動産に契約の内容に適合しない他人の地上権や抵当権の負担があったとき</p>
          <p class="h6 ind3 nobold">ⅱ　一部が他人に属していて売主がそれを移転しなかったとき（565条かっこ書参照）</p>
          <p class="h6 ind3 nobold">ⅲ　売買契約の内容が地役権付きの土地所有権である場合にその地役権がなかったとき</p>
          <p class="p6 ind4">　権利に関する契約不適合が契約締結時に既に存在していたか、契約締結後に生じたかは問わない。</p>
        </div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　全部他人物売買の場合</p>
          <p class="p6 ind1">目的たる財産権の「全部」が他人に属し、売主が買主に権利の全部を移転できない場合、売主は権利移転義務（561条）の不履行という債務不履行状態にある。よって、この場合には契約不適合責任の問題とはならず、債務不履行の一般規定(415条以下、541条、542条)により処理される。権利の一部が他人に属していた場合（565条かっこ書）と区別しなければならない。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-h29-16-uh23-17-i" title="H29-16-ウ">過去問</a></div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A06-02-04-0-03">３　責任の内容（買主の救済手段）</div>
<div class="jobun">
（買主の追完請求権）
第562条　引き渡された目的物が種類、品質又は数量に関して契約の内容に適合しないものであるときは、買主は、売主に対し、目的物の修補、代替物の引渡し又は不足分の引渡しによる履行の追完を請求することができる。ただし、売主は、買主に不相当な負担を課するものでないときは、買主が請求した方法と異なる方法による履行の追完をすることができる。
２　前項の不適合が買主の責めに帰すべき事由によるものであるときは、買主は、同項の規定による履行の追完の請求をすることができない。
（買主の代金減額請求権）
第563条　前条第１項本文に規定する場合において、買主が相当の期間を定めて履行の追完の催告をし、その期間内に履行の追完がないときは、買主は、その不適合の程度に応じて代金の減額を請求することができる。
２　前項の規定にかかわらず、次に掲げる場合には、買主は、同項の催告をすることなく、直ちに代金の減額を請求することができる。
　一　履行の追完が不能であるとき。
　二　売主が履行の追完を拒絶する意思を明確に表示したとき。
　三　契約の性質又は当事者の意思表示により、特定の日時又は一定の期間内に履行をしなければ契約をした目的を達することができない場合において、売主が履行の追完をしないでその時期を経過したとき。
　四　前３号に掲げる場合のほか、買主が前項の催告をしても履行の追完を受ける見込みがないことが明らかであるとき。
３　第１項の不適合が買主の責めに帰すべき事由によるものであるときは、買主は、前２項の規定による代金の減額の請求をすることができない。
（買主の損害賠償請求及び解除権の行使）
第564条　前２条の規定は、第415条の規定による損害賠償の請求並びに第541条及び第542条の規定による解除権の行使を妨げない。
</div>
      <p>目的物に関する契約不適合又は権利に関する契約不適合がある場合、買主に以下の1.から4.の請求が認められる。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　追完請求（562条１項）</p>
          <p class="p6 ind1">562条１項により、買主の追完請求権が明文化されている。これは、担保責任が契約責任であるとする考え方を前提とするものである。これにより、特定物か不特定物かを問わず、履行の追完請求権が認められる。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 追完方法</p>
          <p class="p6 ind2">買主は、売主に対して、①目的物の修補、②代替物の引渡し、③不足分の引渡しによる履行の追完を請求でき（562条１項本文）、追完方法の選択権は、買主に与えられている。ただし、売主は、「買主に不相当な負担を課するものでないとき」には、それと異なる方法により履行の追完をすることができる（同条項ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 追完請求が認められない場合</p>
          <p class="p6 ind2">履行の追完が不能の場合（412条の２第１項）又は契約内容の不適合が買主の責めに帰すべき事由による場合（562条２項）は、買主の追完請求は認められない。</p>
        </div>
        </div>
        <div class="p-card" id="min43-q4">
          <p class="h6 ind0">2.　代金減額請求（563条）</p>
          <p class="p6 ind1">563条により、買主の代金減額請求権が明文化されている。代金減額請求権によって対価的均衡を維持する必要性は、数量不足の場合などに限らず、より一般的に認められるものであることから、代金減額請求権は契約不適合の場合一般における救済手段として認められる。</p>
          <p class="p6 ind1">損害賠償請求権とは別個の規定であり、損害賠償請求が認められない場合（415条１項ただし書等）でもなし得る。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 減額請求の要件</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 催告による減額請求</p>
          <p class="p6 ind3">562条１項の追完請求権が認められる場合に、買主が相当の期間を定めて催告をし、その期間内に履行の追完がないときは、買主に代金減額請求権が認められる（563条１項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 無催告減額請求</p>
          <p class="p6 ind3">以下のいずれかに該当する場合には、催告なしに代金減額請求が認められる（563条２項）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　履行の追完の不能</p>
          <p class="h6 ind3 nobold">ⅱ　売主による履行の追完を拒絶する意思の明確な表示</p>
          <p class="h6 ind3 nobold">ⅲ　定期行為における時期の経過</p>
          <p class="h6 ind3 nobold">ⅳ　催告しても履行の追完を受ける見込みがないことが明らかであるとき</p>
        </div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 減額請求が認められない場合</p>
          <p class="p6 ind2">目的物の契約不適合が「買主の責めに帰すべき事由」によるものである場合は、買主は売主に対して代金減額請求をすることができない（563条３項）。</p>
          <p class="p6 ind2">また、代金減額的な趣旨の損害賠償請求と両立はしないため、代金減額的な趣旨の損害賠償請求と代金減額請求の両方をすることは認められない。しかし、信頼利益的な売買コストに関する損害賠償請求と代金減額請求との両立は認められ得る。代金減額請求は、売買契約の一部解除と同じ性質を持つため、解除をしたうえで代金減額請求権を行使することも認められない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 数量が超過する場合</p>
          <p class="p6 ind2">契約不適合の効果としての代金減額請求権を定めた563条を類推適用して代金の増額を求めることはできないと考えられる（最判平13.11.27参照）。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-r03-18-i" title="R3-18-イ">過去問</a></div>
        </div>
        <div class="p-card" id="min43-q6">
          <p class="h6 ind0">3.　損害賠償請求（564条、415条）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 売主の帰責事由の要否</p>
          <p class="p6 ind2">売主に対し債務不履行を理由とする損害賠償請求をすることができるが、売主の帰責性が必要である（415条１項、564条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 損害賠償の範囲</p>
          <p class="p6 ind2">債務不履行を理由とする損害賠償であり、その内容は、履行利益、すなわち、契約に適合した履行がされたならば買主が受けたであろう利益の賠償となる。その範囲は、416条のもとで決せられる。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-h20-20-a" title="H20-20-ア">過去問</a></div>
        </div>
        <div class="p-card" id="min43-q7">
          <p class="h6 ind0">4.　契約の解除（564条、541条、542条）</p>
          <p class="p6 ind1">買主は、541条以下の要件を満たしたときに、売主に対して、債務不履行を理由として売買契約を解除することができる（564条）。契約目的の達成が不能であるかを問わず、解除の一般規定（541条以下）に従った取扱いがなされる。したがって、解除に売主の帰責事由は不要である。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-h13-16-i" title="H13-16-イ">過去問</a></div>
        <div class="p-card" id="min43-q8">
          <p class="h6 ind0">5.　売主の帰責事由の要否</p>
          <p class="p6 ind1">契約不適合が「売主の責めに帰すべき事由」によるものであることは要件ではない。もっとも、損害賠償請求には、売主の帰責事由が必要である。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-r03-18-e" title="R3-18-エ">過去問</a></div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A06-02-04-0-04">４　行使期間</div>
<div class="jobun">
（目的物の種類又は品質に関する担保責任の期間の制限）
第566条　売主が種類又は品質に関して契約の内容に適合しない目的物を買主に引き渡した場合において、買主がその不適合を知った時から１年以内にその旨を売主に通知しないときは、買主は、その不適合を理由として、履行の追完の請求、代金の減額の請求、損害賠償の請求及び契約の解除をすることができない。ただし、売主が引渡しの時にその不適合を知り、又は重大な過失によって知らなかったときは、この限りでない。
</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　目的物に関する契約不適合</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 売主が種類又は品質に関して不適合である目的物を引き渡した場合</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 原則（買主の通知義務と通知を怠ったことによる失権）</p>
          <p class="p6 ind3">買主が契約不適合を知った時から１年以内に契約不適合の通知をすれば、買主の追完請求権等は保全され、以後契約不適合を知った時から５年の主観的消滅時効にかかるまで、権利行使をなし得ることとなる（566条本文）。</p>
          <p class="p6 ind3">逆に言えば、当該通知を怠った場合には、契約不適合による各種請求権は失権するということである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 例外（買主が失権しない場合）</p>
          <p class="p6 ind3">引渡しの際に、目的物の種類又は品質について不適合があることについて売主が悪意又は重過失であるときは、買主が１年以内に通知をしなかった場合であっても権利行使をすることができる（566条ただし書）。通知義務及び通知を怠った場合の失権がないことを規定している。</p>
          <p class="p6 ind3">この規定の趣旨は、売主が、その不適合について悪意又は重過失の場合にまで、引渡しによる履行が終了したとの売主の期待を保護する必要がない点にある。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 数量に関する契約不適合の場合</p>
          <p class="p6 ind2">数量に関する契約不適合には、566条の適用はない。</p>
          <p class="p6 ind2">数量に関する契約不適合の場合は、売主が履行を終了したという期待を抱くことは想定し難く、売主を保護する必要がないと考えられたためである。したがって、この場合には、消滅時効の一般原則に従った扱いのみが妥当する。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　権利に関する契約不適合</p>
          <p class="p6 ind1">権利に関する契約不適合については、566条の適用はない。</p>
          <p class="p6 ind1">権利移転義務の不履行に関しては短期間でその不履行の判断が困難になるとは考え難く、消滅時効の一般原則と異なる短期の期間制限を必要とする趣旨が妥当しないためである。</p>
          <p class="p6 ind1">その結果、専ら債権の消滅時効に関する一般準則(166条１項)によって処理されるため、契約不適合を知った時から５年、不適合な給付がされた時から10年となる。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-r03-18-o" title="R3-18-オ">過去問</a></div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A06-02-04-0-05">５　担保責任に関する特約</div>
<div class="jobun">
（担保責任を負わない旨の特約）
第572条　売主は、第562条第１項本文又は第565条に規定する場合における担保の責任を負わない旨の特約をしたときであっても、知りながら告げなかった事実及び自ら第三者のために設定し又は第三者に譲り渡した権利については、その責任を免れることができない。
</div>
      <p>担保責任に関する規定は強行規定ではないから、当事者の特約によって法定の責任を排除・減軽・加重することができる。しかし、売主が責任を負担しない旨の特約は信義に反する場合があるので、知りながら告げなかった事実及び自ら第三者のために設定し又は第三者に譲り渡した権利については、責任を免れることはできない（572条）。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A06-02-04-0-06">６　その他の担保責任</div>
      <div class="blk inner">
        <div class="p-card" id="min43-q11">
          <p class="h6 ind0">1.　競売における担保責任（568条）</p>
<div class="jobun">
（競売における担保責任等）
第568条　民事執行法その他の法律の規定に基づく競売（以下この条において単に「競売」という。）における買受人は、第541条及び第542条の規定並びに第563条（第565条において準用する場合を含む。）の規定により、債務者に対し、契約の解除をし、又は代金の減額を請求することができる。
２　前項の場合において、債務者が無資力であるときは、買受人は、代金の配当を受けた債権者に対し、その代金の全部又は一部の返還を請求することができる。
３　前２項の場合において、債務者が物若しくは権利の不存在を知りながら申し出なかったとき、又は債権者がこれを知りながら競売を請求したときは、買受人は、これらの者に対し、損害賠償の請求をすることができる。
４　前３項の規定は、競売の目的物の種類又は品質に関する不適合については、適用しない。
</div>
        <div class="p-card">
          <p class="h6 ind1">(1) 競売の目的物に関する制限</p>
          <p class="p6 ind2">競売によって買い受けた物に種類又は品質に関する不適合があった場合は、買受人は、債務者に対し、不適合を理由とする契約の解除や代金減額請求をすることができない（568条４項）。</p>
          <p class="p6 ind2">他方、競売によって買い受けた物に、物の不存在もしくは数量に関する不適合または権利の不存在若しくは権利に関する不適合があった場合は、買受人は、債務者に対し、債務不履行を理由として契約を解除し、または代金減額請求をすることができる（568条１項）。</p>
          <p class="p6 ind2">たとえば、競売の内容が借地権付きの建物である場合にその借地権がなかったとすると、565条の権利の不適合として、568条１項から３項が適用される。</p>
        </div>
<div class="kakomon"><a href="pu26min43q.php#q-h13-16-e" title="H13-16-エ">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind1">(2) 趣旨</p>
          <p class="p6 ind2">種類・品質の不適合を特別に扱い、担保責任が否定される理由は、競売においては、①債務者（所有者）の自由意思によってなされるものではないこと、②債権者は目的物の品質について知る機会が少なく帰責性が乏しいこと、③競売手続においては目的物にある程度の損傷等があることを織り込んで買受けが行われているのが実情であり、買受人は自己の危険で買い取るべきであること、④競売の結果の安定性を図る必要があること、が挙げられている。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　契約不適合の抵当権等がある場合の買主による費用の償還請求（570条）</p>
<div class="jobun">
（抵当権等がある場合の買主による費用の償還請求）
第570条　買い受けた不動産について契約の内容に適合しない先取特権、質権又は抵当権が存していた場合において、買主が費用を支出してその不動産の所有権を保存したときは、買主は、売主に対し、その費用の償還を請求することができる。
</div>
        <div class="p-card">
          <p class="h6 ind1">(1) 意義</p>
          <p class="p6 ind2">売買の目的である不動産に契約の内容に適合しない先取特権、質権又は抵当権が存していた場合において、買主が費用を支出して抵当権等を消滅させた場合に、買主から売主へその費用の償還を認めたものである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 要件</p>
          <p class="p6 ind2">以下の2つの要件を満たす必要がある。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　売買の目的である不動産に契約内容に適合しない先取特権、質権又は抵当権が設定されていること</p>
          <p class="p6 ind4">契約内容に適合しないことが要件であるから、たとえば抵当権が設定されていることを前提として廉価で売買された場合には適用されない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　買主が費用を支出してその不動産の所有権を保存したこと</p>
          <p class="p6 ind4">たとえば、買主が抵当権の被担保債権を第三者弁済や抵当権消滅請求をするなどして当該抵当権を消滅させた場合である。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 効果</p>
          <p class="p6 ind2">買主は売主に対して支出した費用の償還を請求することができる。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　債権の売主の、債務者の資力についての担保責任（569条）</p>
<div class="jobun">
（債権の売主の担保責任）
第569条　債権の売主が債務者の資力を担保したときは、契約の時における資力を担保したものと推定する。
２　弁済期に至らない債権の売主が債務者の将来の資力を担保したときは、弁済期における資力を担保したものと推定する。
</div>
          <p class="p6 ind1">債権の売主も、売買の目的である債権の契約不適合について担保責任を負う。しかし、債務者の資力は債権そのものの内容ではないため、売主は特約をした場合にのみ責任を負う。法は、売主が時期を定めずに債務者の資力を担保したときは、契約時における資力を担保したものと推定し（569条１項）、弁済期に至らない債権について債務者の将来の資力を担保したときは、弁済期における債務者の資力を担保したものと推定する（569条２項）。</p>
        </div>
