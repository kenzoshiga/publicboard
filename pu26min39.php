      <h3 id="A04-05-04">第４節　更改</h3>
    <div class="blk outer">
<div class="jobun">
（更改）
第513条　当事者が従前の債務に代えて、新たな債務であって次に掲げるものを発生させる契約をしたときは、従前の債務は、更改によって消滅する。
一　従前の給付の内容について重要な変更をするもの
二　従前の債務者が第三者と交替するもの
三　従前の債権者が第三者と交替するもの
（債務者の交替による更改）
第514条　債務者の交替による更改は、債権者と更改後に債務者となる者との契約によってすることができる。この場合において、更改は、債権者が更改前の債務者に対してその契約をした旨を通知した時に、その効力を生ずる。
２　債務者の交替による更改後の債務者は、更改前の債務者に対して求償権を取得しない。
（債権者の交替による更改）
第515条　債権者の交替による更改は、更改前の債権者、更改後に債権者となる者及び債務者の契約によってすることができる。
２　債権者の交替による更改は、確定日付のある証書によってしなければ、第三者に対抗することができない。
</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">：債務の同一性が失われる等、債務の要素を変更することで、新債務を成立させるとともに旧債務を消滅させる契約</p>
          <p class="p6 ind1">本来の給付とは別の対価を給付する点で代物弁済と類似するが、本来の給付と異なる給付が現実になされることなく、単に給付すべき新たな債務が成立するにすぎない点で代物弁済とは異なる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 消滅すべき債務が存在すること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 更改の意思（従前の債務に代えて新たな債務を発生させる意思）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 債務の要素に変更があること</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 従前の給付の内容について重要な変更をするもの</p>
          <p class="h6 ind2 nobold">(b) 債務者又は債権者の交替がされるもの</p>
        </div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　契約当事者</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 債権者の交替による更改</p>
          <p class="p6 ind2">新旧債権者と債務者（515条１項）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 債務者の交替による更改</p>
          <p class="p6 ind2">債権者と新債務者のみでも可能である。旧債務者の意思に反するときであってもできる（514条１項前段）。ただし、効力は債権者が旧債務者に通知した時に生ずる（514条１項後段）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　効果</p>
<div class="jobun">
（更改後の債務への担保の移転）
第518条　債権者（債権者の交替による更改にあっては、更改前の債権者）は、更改前の債務の目的の限度において、その債務の担保として設定された質権又は抵当権を更改後の債務に移すことができる。ただし、第三者がこれを設定した場合には、その承諾を得なければならない。
２　前項の質権又は抵当権の移転は、あらかじめ又は同時に更改の相手方（債権者の交替による更改にあっては、債務者）に対してする意思表示によってしなければならない。
</div>
          <p class="p6 ind1">旧債務が消滅し、これと同一性のない新債務が成立する。</p>
          <p class="p6 ind1">担保権・保証債務等も消滅するが、債権者の単独の意思表示により新債務に質権・抵当権を移すことができる。ただし、第三者が設定したものであるときは、その承諾が必要である（518条１項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">5.　更改契約の解除</p>
          <p class="p6 ind1">Ｑ　新債務の不履行を理由とする更改契約の解除は可能か。</p>
          <p class="p6 ind1">→　肯定説（大判昭３.３.10）</p>
          <p class="p6 ind1">更改も１つの契約であるから、契約解除の一般原則により更改契約を解除できる。</p>
          <p class="p6 ind1">新旧両債務が当事者間にのみ存した場合には旧債務が復活するが（大判昭３.３.10）、旧債務が新債務の当事者以外との間に存した場合は、旧債務は復活しない（大判大５.５.８）。</p>
        </div>
      </div>
    </div>
    </div>
    <div class="p-card sec-card">
      <h3 id="A04-05-05">第５節　免除</h3>
    <div class="blk outer">

      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">免除とは、債権者が一方的な意思表示によって無償で債務を消滅させることをいう（519条）。</p>
          <p class="p6 ind1">免除は単独行為であって、債権の放棄に他ならない。</p>
          <p class="p6 ind1">※　免除は単独行為であるが、債務者に不利益にならない限り、これに条件を付しても構わない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　要件</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 免除者に処分権限があること</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 債務者に対する意思表示によってなされること</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　効果</p>
          <p class="p6 ind1">債務が消滅する。</p>
          <p class="p6 ind1">一部免除も、その限りで債務が消滅する。</p>
          <p class="p6 ind1">もっとも、第三者の権利を害することはできないので、債権が第三者の権利の目的になっている場合は、免除の効果はその第三者に及ばない。</p>
        </div>
      </div>
    </div>
    </div>
