  <h2>第４編　債権総論</h2>
  <div class="p-card chap-card">
    <h2 id="A04-05">第５章　債権の消滅</h2>
    <div class="p-card sec-card">
      <h3 id="A04-05-01">第１節　弁済</h3>
    <div class="p-card rank4-card">
      <p id="A04-05-01-1" class="h6 ind0">Ⅳ　弁済の提供</p>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-01-1-01">１　意義</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">：債務者側で必要な準備行為をして、債権者の協力を求める行為</p>
          <p class="p6 ind1">なお、債務の履行について債権者の協力を必要としない場合には、弁済の提供の問題を生じないことに注意が必要である（例えば不作為債務）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　趣旨</p>
          <p class="p6 ind1">ほとんどの債務では、債権者の協力がなければ履行を完了することはできない。このような場合に債権者が必要な協力をしないために債務者が履行を完了することができず債務不履行責任を負うというのは不当である。そこで、債務者の側で必要な行為をした以上は、不履行により生ずる責任を免れるとしたのである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　要件</p>
          <p class="p6 ind1">弁済の提供と認められるためには、債務の本旨に従った現実の提供、又は口頭の提供が必要である（493条）。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-01-1-02">２　弁済の提供の程度（現実の提供と口頭の提供）</div>
      <p>弁済の提供は、いかなる程度になされなければならないのだろうか。</p>
      <p>民法は、提供の程度について、現実の提供と口頭の提供とに分けて規定を設けている。弁済の提供は、原則として、現実の提供をすべきである。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　現実の提供（493条本文）</p>
          <p class="p6 ind1">：債権者が協力（受領）すれば弁済が完了する程度の準備をすること</p>
          <p class="p6 ind1">具体的にいかなるものが現実の提供となるかの判断には、信義則が大きな役割を果たす。</p>
          <p class="p6 ind1">いかなる場合に現実の提供があったといえるかは、債務ごとに考える。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 金銭債務</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 債務全額の提供が必要である。ただし、僅少な不足であれば信義則上提供があったものとされる場合がある（最判昭35.12.15）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 金銭を持参して債権者の住所に行って、支払をなすべき旨を述べれば、金銭を債権者の面前に提示する必要はない（最判昭23.12.14）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 債務者が約束の日時に金銭を持参して債権者の住所へ行ったが、債権者が外出していて会えなかったときも現実の提供となる（大判大10.３.23）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 金銭債務以外</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 不特定物の一定量を交付すべき債務では、全部の提供が必要である。ただし、僅少の不足であれば、信義則上、現実の提供があったものとされることがある。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 不動産の売主は、占有の移転を提供しなくても移転登記手続を可能にすれば現実の提供といえる（大判大７.８.14）。</p>
        </div>
<div class="kakomon"><a href="pu26min39q.php#q-h02-03-a" title="H2-3-ア">過去問</a></div>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　口頭の提供（493条ただし書）</p>
          <p class="p6 ind1">：債務者が弁済の準備をしたことを通知して、債権者に受領を催告すること</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 要件</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 口頭の提供で足りるのは、以下のいずれかにあたる場合である。</p>
          <p class="h6 ind3 nobold">①債権者があらかじめ受領を拒絶した場合</p>
          <p class="h6 ind3 nobold">②債権者の行為を必要とする場合</p>
          <p class="h6 ind3 nobold">※①の場合に口頭の提供で足りるとされるのは、債権者が拒絶しているのに債務者に履行せよというのは公平に反するからである。これに対し、②の場合は、債権者の協力がなければ、弁済が不可能だからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 受領の拒絶は明示の場合に限らず、黙示でもよい。賃貸人が増額賃料でなければ受領しないという場合（大判昭10.５.16）、理由なく受領期日の延期や契約の解除を要求し、自己の反対給付の履行を拒む場合（大判昭18.６.21）等である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 債権者の受領拒絶は、正当でないことが必要である。従って、現実の提供が不適法なため受領を拒まれたときは、口頭の提供をしても無効である（大判大10.６.30）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 「弁済の準備」</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 債務者は、弁済の準備をして、通知しなければならない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 弁済の準備の程度</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　債権者があらかじめ受領を拒んだとき</p>
          <p class="p6 ind4">債権者が翻意して受領しようとすれば債務者が給付を完了できる程度である。</p>
          <p class="p6 ind4">銀行から資金借用の予約をして、いつでも弁済できる程度に資金調達の道を立てておけばよい（大判大７.12.４）。</p>
