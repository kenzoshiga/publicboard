      <h3 id="A04-05-02">第２節　代物弁済</h3>
    <div class="blk outer">
<div class="jobun">
（代物弁済）
第482条　弁済をすることができる者（以下「弁済者」という。）が、債権者との間で、債務者の負担した給付に代えて他の給付をすることにより債務を消滅させる旨の契約をした場合において、その弁済者が当該他の給付をしたときは、その給付は、弁済と同一の効力を有する。
</div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-02-0-01">１　意義</div>
      <p>代物弁済とは、例えば、金銭支払を目的とする債務がある場合に、その金銭の支払に代えて他の金銭以外の物を給付することによりその債務を消滅させる旨の債権者と弁済者との契約をいう（482条）。弁済者は債務者に限られない。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-02-0-02">２　要件</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind3 nobold">①債務が存在すること、②本来の給付に代わる給付をすることについての合意があれば成立する（諾成契約）。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-02-0-03">３　効果</div>
      <p>代物弁済によって合意された給付行為があった場合、その給付は弁済と同一の効力を有する（482条）。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind3 nobold">①　代物の価格に関係なく債務は全部消滅する。</p>
          <p class="h6 ind3 nobold">②　本来の債権に伴う抵当権・質権・保証等の担保も付従性により消滅する。</p>
          <p class="h6 ind3 nobold">③　給付された物や権利に瑕疵があっても、債権消滅の効果が生じ、債権者は本来の給付や瑕疵のない物の給付を請求することはできないと解されていた。しかし、他の有償契約と同様、物の不適合を理由とする責任の問題と考えると、債権者は追完請求、損害賠償請求、契約の解除ができることになる（559条、562条１項本文）。</p>
        </div>
      </div>
    </div>
