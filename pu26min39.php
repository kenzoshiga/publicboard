      <h3 id="A04-05-06">第６節　混同</h3>
    <div class="blk outer">

      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　意義</p>
          <p class="p6 ind1">混同とは、同一人に債権と債務が帰属することをいう（520条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　効果</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 債権は消滅する（520条本文）。</p>
          <p class="p6 ind2">（理由）</p>
          <p class="p6 ind2">自己が自己に対して債権を有し、自己への履行を請求することは無意味である。物権の混同（179条）と同趣旨である。</p>
          <p class="p6 ind2">ex．土地の賃借人が土地の所有権を取得することにより賃貸人の地位を承継した場合、土地の賃借権は消滅する（大判昭５.６.12）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) ただし、債権が第三者の権利の目的であるときは消滅しない（520条ただし書）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 債権が第三者の権利の目的となっていない場合でも、債権を存続させることに意味がある場合には、混同の効果は否定される。</p>
          <p class="p6 ind2">＜判例＞　最判昭40.12.21</p>
          <p class="p6 ind2">「不動産の賃借人が賃貸人から当該不動産を譲り受けてその旨の所有権移転登記をしないうちに、第三者が右不動産を二重に譲り受けてその旨の所有権移転登記を経由したため、前の譲受人たる賃借人において右不動産の取得を後の譲受人たる第三者に対抗できなくなつたような場合には、一たん混同によつて消滅した右賃借権は、右第三者に対する関係では、同人の所有権取得によつて、消滅しなかつたものとなる」。</p>
