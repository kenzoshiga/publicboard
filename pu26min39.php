      <h3 id="A04-05-03">第３節　相殺</h3>
    <div class="p-card rank4-card">
      <p id="A04-05-03-1" class="h6 ind0">Ⅰ　意義</p>
    <div class="blk outer">
<div class="jobun">
（相殺の要件等）
第505条　二人が互いに同種の目的を有する債務を負担する場合において、双方の債務が弁済期にあるときは、各債務者は、その対当額について相殺によってその債務を免れることができる。ただし、債務の性質がこれを許さないときは、この限りでない。
２　前項の規定にかかわらず、当事者が相殺を禁止し、又は制限する旨の意思表示をした場合には、その意思表示は、第三者がこれを知り、又は重大な過失によって知らなかったときに限り、その第三者に対抗することができる。
</div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-03-1-01">１　意義</div>
      <p>相殺とは、例えば、Ａに対して債務を負うＢもまた、Ａに対して同種の債権（反対債権）を有する場合において、Ａ又はＢが相殺の意思表示をすることによって、その対当額について債務を免れることができるとするものである（505条１項本文）。</p>
      <p>相殺する側の債権を自働債権といい、相殺される側の債権を受働債権という。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-03-1-02">２　趣旨</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　簡便な決済</p>
          <p class="p6 ind1">弁済の手数を省略して、簡便に決済することができる。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　当事者間の公平</p>
          <p class="p6 ind1">一方当事者が無資力である場合にも、相手方は債権の回収ができるので、公平の要請にかなう。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　担保的機能</p>
          <p class="p6 ind1">相殺が機能的には担保としての役割を果たす。</p>
        </div>
      </div>
    </div>
    </div>
    <div class="p-card rank4-card">
      <p id="A04-05-03-2" class="h6 ind0">Ⅱ　要件</p>
    <div class="blk outer">

      <p>相殺適状にあり、かつ、相殺禁止に当たらないことが必要である。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A04-05-03-2-01">１　相殺適状（505条）</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind3 nobold">①２人互いに債務を負担すること（債権の対立）、②両債権が同種の目的を有すること、③両債権が弁済期にあること、④両債権が性質上相殺を許さないものでないこと</p>
          <p class="p6 ind4">上記①～④の要件を充たすとき、これを相殺適状にあるという。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">1.　債権の対立（要件①）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 自働債権</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 原則</p>
          <p class="p6 ind3">自働債権は、原則として、相殺者自身が被相殺者に対して有する債権であることを要する。</p>
          <p class="p6 ind3"><img class="embed-img" src="img/min39-05.png" alt="min39-05.png"></p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 例外</p>
          <p class="p6 ind3">第三者に対する債権で相殺できる場合</p>
          <p class="p6 ind3">ex．債権譲渡（469条）の場合</p>
          <p class="p6 ind3"><img class="embed-img" src="img/min39-06.png" alt="min39-06.png"></p>
          <p class="p6 ind3">Ｑ　相手方の第三者に対する債権を受働債権として、自己の相手方に対する債権で相殺できるか</p>
          <p class="p6 ind3">→　否定説（大判昭８.12.５〔抵当不動産の第三取得者が抵当権者に対して有している債権による相殺につき〕）</p>
          <p class="p6 ind3">（理由）</p>
          <p class="p6 ind3">相殺の担保的機能からすれば、債務者が債権者に対して有する債権の担保を第三者によって消滅させられてしまうことは妥当でない。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 例外</p>
          <p class="p6 ind2">時効消滅した債権であっても、その消滅以前に相殺適状にあればこれを自働債権として相殺できる（508条）。</p>
          <p class="p6 ind2">508条は自働債権が時効消滅した場合でも、その消滅以前に相殺適状にあったときには例外的に相殺を認めたものである。時効消滅前に両債権が相殺適状にあるときは、当事者は相殺により自動的に清算されたものと期待をもっているのが普通であり、本条は相殺に対するこのような当事者の信頼を保護したものである。</p>
          <p class="p6 ind2">＜判例＞　最判昭36.４.14</p>
          <p class="p6 ind2">消滅時効の完成した債権を譲り受け、これを自働債権としてする相殺は許されない。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　両債権が同種の目的を有すること（要件②）</p>
          <p class="p6 ind1">通常は金銭債権であるが、種類債権でも相殺は可能である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　債権が弁済期にあること（要件③）</p>
          <p class="p6 ind1">自働債権については、相手方の期限の利益を一方的に奪うことはできないこ　とから、常に弁済期にあることが必要である。ただし、担保の滅失・損傷等による期限の利益の喪失（137条）や、期限の利益喪失約款によって、相手方が期限の利益を失う場合がある。</p>
          <p class="p6 ind1">自働債権について期限の定めがないときは、いつでも履行を請求できるから、直ちに相殺できる（大判昭８.９.８）。</p>
          <p class="p6 ind1">※　受働債権については、相殺者は原則として期限の利益を放棄できるので、必ずしも弁済期にある必要はない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　両債権が性質上相殺を許さないものでないこと（要件④）</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 現実に履行をしなければ意味のないもの　ex．なす債務、不作為債務</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 抗弁権の付着した債権</p>
          <p class="p6 ind2">自働債権に抗弁権（同時履行の抗弁権、催告・検索の抗弁権）が付着している場合は、相手方の抗弁権を奪うことになるため相殺できない。</p>
        </div>
