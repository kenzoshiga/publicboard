<div class="jobun">
（債権の譲渡における相殺権）
第469条　債務者は、対抗要件具備時より前に取得した譲渡人に対する債権による相殺をもって譲受人に対抗することができる。
２　債務者が対抗要件具備時より後に取得した譲渡人に対する債権であっても、その債権が次に掲げるものであるときは、前項と同様とする。ただし、債務者が対抗要件具備時より後に他人の債権を取得したときは、この限りでない。
一　対抗要件具備時より前の原因に基づいて生じた債権
二　前号に掲げるもののほか、譲受人の取得した債権の発生原因である
契約に基づいて生じた債権
