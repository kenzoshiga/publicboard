        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　債権者の行為を要するとき</p>
          <p class="p6 ind4">債権者の行為があれば、直ちに弁済を完了できる程度である（大判大10.11.８）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 弁済の準備ができない経済状態にあるため提供ができない場合には、債権者の弁済を受領しない意思が明確でも、債務不履行責任を免れない（最判昭44.５.１）。</p>
        </div>
        </div>
