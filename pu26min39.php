（弁済の提供の効果）
第492条　債務者は、弁済の提供の時から、債務を履行しないことによって生ずべき責任を免れる。
</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　債務者は債務不履行を理由とする損害賠償責任を負わず（492条）、遅延利息・違約金を支払う必要もない。</p>
          <p class="p6 ind1">また、担保権の実行を免れる。ただし、弁済の提供により債務が消滅するわけではないから、担保物の返還は請求できない（大判昭６.５.22）。</p>
        </div>
        <div class="p-card">
