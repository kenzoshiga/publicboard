      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　当事者の意思による相殺禁止（505条２項）</p>
          <p class="p6 ind1">当事者は相殺を禁止することができる。ただし、善意・無重過失の第三者には相殺禁止を対抗できない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　不法行為債権を受働債権とする相殺の禁止（509条）</p>
<div class="jobun">
（不法行為等により生じた債権を受働債権とする相殺の禁止）
第509条　次に掲げる債務の債務者は、相殺をもって債権者に対抗することができない。ただし、その債権者がその債務に係る債権を他人から譲り受けたときは、この限りでない。
一　悪意による不法行為に基づく損害賠償の債務
二　人の生命又は身体の侵害による損害賠償の債務（前号に掲げるものを除く。）
</div>
          <p class="p6 ind1">不法行為の被害者に対して、現実の弁済により損害のてん補を受けさせるとともに、不法行為の誘発を防止するために、悪意の不法行為による損害賠償請求権を受働債権とする相殺を禁止したものである。</p>
          <p class="p6 ind1">また、人の生命・身体の侵害による損害賠償請求権については、不法行為に基づくものに限らず、債務不履行に基づくものであっても、これを受働債権とする相殺を禁止した。被害者に現実に弁済を受けさせてその保護を図る必要性は、不法行為に基づく損害賠償請求権と同様に認められるからである。</p>
          <p class="p6 ind1">なお、その債務に係る債権が他人に譲渡された場合は、被害者保護を図る必要がなくなることから、相殺は禁止されない（509条柱書ただし書）。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 許される事例</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 被害者側からの相殺、すなわち、不法行為に基づく損害賠償債権を自働債権とし、不法行為による損害賠償債権以外の債権を受働債権とする相殺は許される（最判昭42.11.30）。この場合、不法行為を誘発するということはないからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 相殺契約によって不法行為による損害賠償請求権を受働債権とする相殺をすることは許される（大判大元.12.16）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 許されない事例</p>
          <p class="p6 ind2">自働債権と受働債権が共に悪意の不法行為によって生じたものであるときには、相殺は許されない。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　差押禁止債権（ex．扶養料・恩給）を受働債権とする相殺の禁止（510条）</p>
          <p class="p6 ind1">差押禁止債権の債務者は相殺をもって債権者に対抗することができない。</p>
          <p class="p6 ind1">差押禁止の趣旨は、債権者に現実に弁済を受けさせるように保障することだからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　差押えを受けた債権を受働債権とする相殺の禁止（511条）</p>
<div class="jobun">
（差押えを受けた債権を受働債権とする相殺の禁止）
第511条　差押えを受けた債権の第三債務者は、差押え後に取得した債権による相殺をもって差押債権者に対抗することはできないが、差押え前に取得した債権による相殺をもって対抗することができる。
２　前項の規定にかかわらず、差押え後に取得した債権が差押え前の原因に基づいて生じたものであるときは、その第三債務者は、その債権による相殺をもって差押債権者に対抗することができる。ただし、第三債務者が差押え後に他人の債権を取得したときは、この限りでない。
</div>
          <p class="p6 ind1">511条２項本文は、相殺の期待を保護するために、差押え後に取得した債権による相殺に関して、取得した債権が差押え前の原因に基づいて生じた債権であるときは、その債権を自働債権とする相殺をもって差押債権者に対抗することができると規定する。破産法の規律との整合を図る趣旨である。同条項ただし書は、他人の債権を、差押え後に取得した者については、相殺の期待を保護する必要がないことから、相殺を認めないものとしている。</p>
