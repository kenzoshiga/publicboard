      <div class="outer-title" id="A04-05-03-3-02">２　相殺の効果</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　債権の消滅</p>
          <p class="p6 ind1">相殺によって両債権は対当額で消滅する（505条１項本文）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　遡及効</p>
          <p class="p6 ind1">相殺は、相殺適状の時にさかのぼって効力を生ずる（506条２項）。</p>
          <p class="p6 ind1">なぜなら、両債権が相殺適状にあるときは、当事者はあたかも両債権が決済されたかのように扱うのが通常であるから、遡及効を認めることが取引の実際や公平に適するからである。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　相殺の充当</p>
<div class="jobun">
（相殺の充当）
第512条　債権者が債務者に対して有する一個又は数個の債権と、債権者が債務者に対して負担する一個又は数個の債務について、債権者が相殺の意思表示をした場合において、当事者が別段の合意をしなかったときは、債権者の有する債権とその負担する債務は、相殺に適するようになった時期の順序に従って、その対当額について相殺によって消滅する。
２　前項の場合において、相殺をする債権者の有する債権がその負担する債務の全部を消滅させるのに足りないときであって、当事者が別段の合意をしなかったときは、次に掲げるところによる。
一　債権者が数個の債務を負担するとき（次号に規定する場合を除く。）は、第488条第４項第２号から第４号までの規定を準用する。
二　債権者が負担する一個又は数個の債務について元本のほか利息及び費用　を支払うべきときは、第489条の規定を準用する。この場合において、同条第２項中「前条」とあるのは、「前条第４項第２号から第４号まで」と読み替えるものとする。
３　第１項の場合において、相殺をする債権者の負担する債務がその有する債権の全部を消滅させるのに足りないときは、前項の規定を準用する。
第512条の２　債権者が債務者に対して有する債権に、一個の債権の弁済として数個の給付をすべきものがある場合における相殺については、前条の規定を準用する。債権者が債務者に対して負担する債務に、一個の債務の弁済として数個の給付をすべきものがある場合における相殺についても、同様とする。
</div>
          <p class="p6 ind1">債権者が債務者に対して有する一個又は数個の債権と、債権者が債務者に対して負担する一個又は数個の債務について、以下の順序で消滅する。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 相殺の充当の順序に関する合意があるとき</p>
          <p class="p6 ind2">相殺の充当の順序に関する合意をしたときは、それによって消滅する。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 相殺の充当の順序に関する合意がないとき</p>
          <p class="p6 ind2">上記（1）の合意をしなかったときは、債権者の有する債権とその負担する債務は、相殺適状となった時期の順序に従って相殺によって消滅する（512条１項）。</p>
          <p class="p6 ind2">相殺適状となった時期が同じである元本債権相互間と利息・費用債権の充当については、弁済の充当の規定が指定充当の規定を除いて準用される（512条２項）。</p>
        </div>
