          <p class="p6 ind2">各不動産につきそれぞれ負担すべき極度額を定めて、根抵当権者の被担保債権を累積的に負担させる方法である。配当については、根抵当権者は各不動産の代価につき、各極度額に至るまで優先権を行使することができる。また、数個の根抵当権はそれぞれ独立の根抵当権であるから、後順位抵当権者の代位は生じない。</p>
          <p class="p6 ind2">（純粋）共同根抵当の要件を満たさない場合は、この累積根抵当として扱われる。</p>
