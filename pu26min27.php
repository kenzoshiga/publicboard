          <p class="h6 ind0">3.　極度額の変更</p>
          <p class="p6 ind1">極度額の変更（増額・減額）は設定当事者の合意と利害関係人の承諾によってなされる（398条の５）。そして、利害関係人の承諾が得られれば、元本確定の前後を問わず変更することができる。</p>
          <p class="p6 ind1">ここでいう利害関係人とは、極度額の増額・減額によって不利益を受ける者をいう。</p>
        </div>
<div class="kakomon"><a href="pu26min27q.php#q-r02-14-ih27-14-eh16-15-uh02-13-2" title="R2-14-イ">過去問</a></div>
        <div class="p-card" id="min27-q23">
