          <p class="h6 ind0">3.　共同抵当権の放棄</p>
          <p class="p6 ind1">共同抵当の目的である甲・乙不動産が債務者所有の場合であって、甲不動産に後順位抵当権者がいるときに、共同抵当権者が乙不動産について設定された抵当権を放棄した場合、392条・504条類推により、放棄がなければ後順位抵当権者が代位することができた額について、後順位者に対して優先弁済権を主張できなくなる（大判昭11.７.14）。</p>
          <p class="p6 ind1">この場合、先順位抵当権者が甲不動産を競売して後順位抵当権者が受けるべき優先額についてまで配当を受けたときは、これを不当利得として、後順位抵当権者に返還しなければならない（最判平４.11.６）。</p>
