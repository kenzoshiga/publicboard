    <div class="p-card sec-card">
      <h3 id="A03-02-06">第６節　根抵当権</h3>
    <div class="p-card rank4-card">
      <p id="A03-02-06-1" class="h6 ind0">Ⅰ　総説</p>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-06-1-01">１　意義</div>
      <p>：一定の範囲に属する不特定の債権を極度額の限度において担保するために設定される抵当権（398条の２第１項）</p>
      <p>継続的に取引している当事者間では、債権・債務が発生・消滅を繰り返し、その額も一定ではない。ところが、通常の抵当権には消滅における付従性が存在するため、このような継続的取引で利用しようとすると発生・消滅に応じていちいち設定・抹消を繰り返さなければならず、煩雑で不便である。そこで、継続的取引関係から生じる債務を一括して担保するために、消滅における付従性を制限された抵当権として生み出されたのが根抵当権である。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-06-1-02">２　法的性質</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　付従性</p>
          <p class="p6 ind1">根抵当権は将来において発生・消滅を繰り返す不特定債権を担保するものであるため、当事者間に発生する個々の具体的な債権と根抵当権とは直接的な関連性を持たないから、付従性は否定される。従って、根抵当権は元本が確定するまでは、当事者間に発生する個々の具体的な債権に発生・消滅という変化が生じ、たとえ債権が皆無となっても消滅しない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　随伴性</p>
          <p class="p6 ind1">元本確定前においては、当事者間に発生する個々の具体的債権が譲渡されても、根抵当権は移転しない（398条の７）。すなわち、随伴性は否定される。</p>
        </div>
<div class="kakomon"><a href="pu26min27q.php#q-h26-14-eh17-16-uh05-15-ih01-12-2" title="H26-14-エ">過去問</a></div>
        <div class="p-card">
          <p class="h6 ind0">3.　独立性</p>
          <p class="p6 ind1">被担保債権から切断された価値支配権としての性質を独立性という。根抵当権は元本確定前においては、付従性が否定されており、一応独立性を有しているといえる。しかし、根抵当権も特定の融資関係ないし債権関係から発生する債権を担保するという性質を有しており、これらの影響が完全に遮断されているわけではない。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-06-1-03">３　設定</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　根抵当権設定の当事者</p>
          <p class="p6 ind1">根抵当権を取得する者と設定者間の根抵当権設定契約によって設定される。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　設定契約の内容</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 必要的事項</p>
          <p class="h6 ind3 nobold">①被担保債権の範囲、②債務者、③極度額（398条の２）</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 任意的事項</p>
          <p class="p6 ind2">元本確定期日（398条の６第１項）</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　被担保債権の範囲</p>
          <p class="p6 ind1">現行法では、債権者と債務者との間に生ずるすべての債権を担保する包括根抵当は認められておらず、被担保債権は「一定の範囲に属する不特定の債権」（398条の２第１項）でなければならない。</p>
          <p class="p6 ind1">なお、設定時に既に生じている特定債権を不特定債権と併せて被担保債権とすることはできるが、特定債権のみを被担保債権として根抵当権を設定することはできない。</p>
        </div>
<div class="kakomon"><a href="pu26min27q.php#q-h17-16-i" title="H17-16-イ">過去問</a></div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-06-1-04">４　効力</div>
      <p>被担保債権の範囲に関して、抵当権について被担保債権の範囲についての規定である375条の適用はない。従って、元本のほか、根抵当権が実行され抵当目的物の売却代金が配当される間に発生したものであれば、利息・遅延損害金についても極度額に達するまですべて担保される。</p>
