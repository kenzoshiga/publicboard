  <h2>第３編　担保物権</h2>
  <div class="p-card chap-card">
    <h2 id="A03-02">第２章　抵当権</h2>
    <div class="p-card sec-card">
      <h3 id="A03-02-05">第５節　共同抵当（392条）</h3>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-05-0-01">１　意義</div>
      <p>共同抵当：債権者が同一の債権の担保として、数個の不動産上に抵当権を有するもの</p>
      <p>その機能は、①数個の不動産を集めて、担保価値を増大させることと、②抵当不動産のうちの一つに滅失･損傷や、価格の下落があっても危険を分散することができること、にある。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-05-0-02">２　設定</div>
      <p>同一の債権を担保するために、数個の不動産の上に抵当権を設定することにより成立する。</p>
      <p>不動産以外の地上権や永小作権に対しても設定できる。それぞれの設定行為が同時でなくてもよい。</p>
      <p>抵当権のみではなく共同抵当権が設定されていることは、登記により公示される。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-05-0-03">３　効力</div>
      <div class="blk inner">
        <div class="p-card" id="min27-q1">
          <p class="h6 ind0">1.　同時配当</p>
          <p class="p6 ind1">同時配当：共同抵当の目的物が同一の手続により競売され、その代価が同時に配当される場合</p>
          <p class="p6 ind1">同時配当の場合、抵当権者の債権額を、各不動産の価格に応じて割付け（按分）し、その割合に従って各不動産から優先弁済を受けることができる（割付主義）。各後順位抵当権者は、それぞれ、残余につき優先弁済を受ける。本条の割付主義は、後順位抵当権者がいない場合にも適用される（大判昭10.４.23）。</p>
        </div>
<div class="kakomon"><a href="pu26min27q.php#q-h07-12-uh13-13-i" title="H7-12-ウ">過去問</a></div>
        <div class="p-card" id="min27-q2">
          <p class="h6 ind0">2.　異時配当</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 総説（負担按分の原則）</p>
          <p class="p6 ind2">異時配当：共同抵当においてある一部不動産の代価のみから配当を受けた場合</p>
          <p class="p6 ind2">この場合には、同時配当と異なり競売された不動産の後順位抵当権者が配当を受けられなくなる可能性がある。そこで、同時配当がなされればその不動産が負担すべきであった金額を限度として、後順位抵当権者は共同抵当権者に代位できるものとした。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 392条の適用</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 債務者所有型及び同一物上保証人所有型</p>
          <p class="p6 ind3">異時配当の場合には、抵当権者はその代価につき債権の全部の弁済を受けられるが（392条２項前段）、競売された不動産についての後順位抵当権者は、同時配当がなされたならば配当を受けたであろう額の限度で、競売されなかった方の不動産に代位することができる（392条２項後段、最判平4.11.6）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 債務者・物上保証人所有型</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　債務者所有不動産から先に競売された場合</p>
          <p class="p6 ind4">この場合、392条２項は適用されず、債務者所有不動産についての後順位抵当権者は物上保証人所有の抵当不動産に代位できない。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅱ　物上保証人所有不動産から先に競売された場合</p>
          <p class="p6 ind4">別人所有であるため、392条2項の適用はない。しかし、この場合、物上保証人は求償権に基づいて債権者に代位することができる（499条、501条）。</p>
          <p class="p6 ind4">そして、物上保証人が他の不動産について代位する権利の上に、後順位抵当権者は物上代位と同様の優先権を持つ（大判昭11.12.９、最判昭53.７.４）。</p>
        </div>
