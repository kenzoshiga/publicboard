          <p class="p6 ind1">元本について確定期日の定めがない場合においては、確定期日の新設を、元本について確定期日の定めがある場合においては、確定期日の変更（延長・繰上げ・廃止）を設定当事者間の合意ですることができる（398条の６第１項）。この場合、利害関係を有する第三者の承諾を要しない（398条の６第２項、398条の４第２項）。ただし、確定期日の新設・変更をした場合、その新設・変更期日は新設・変更した日から５年以内でなければならない（398条の６第３項）。</p>
          <p class="p6 ind1">確定期日の変更は、変更前の期日より前に登記をしなければ元本は変更前の期日で確定する（398条の６第４項）。</p>
