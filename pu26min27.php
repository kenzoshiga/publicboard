          <p class="h6 ind0">1.　根抵当権の譲渡</p>
          <p class="p6 ind1">根抵当権は通常の抵当権と異なり被担保債権から切断された独立性を有する権利であるため、通常の抵当権とは異なった処分方法（全部譲渡、分割譲渡、一部譲渡）が定められている。</p>
        <div class="p-card">
          <p class="h6 ind1">(1) 全部譲渡</p>
          <p class="p6 ind2">元本確定前においては、譲渡人たる根抵当権者と譲受人の合意のほか、根抵当権設定者の承諾を得てその根抵当権を譲渡（全部譲渡）することができる（398条の12第１項）。この全部譲渡は元本確定前の根抵当権を被担保債権とは独立に譲受人に移転するものである。なお、対抗要件として登記が必要である。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(2) 分割譲渡</p>
          <p class="p6 ind2">根抵当権の分割譲渡とは、元本確定前の根抵当権を２個の根抵当権に分割してその１つを譲渡することをいう（398条の12第２項前段）。分割譲渡をするには、全部譲渡の場合と同様、譲渡人たる根抵当権者と譲受人の合意のほか、根抵当権設定者の承諾を得る必要がある（398条の12第２項前段、１項）。対抗要件として登記が必要である。</p>
          <p class="p6 ind2">分割譲渡は２つの抵当権が生じ、その１つが譲渡されて他が譲渡人のもとに残ることから、全部譲渡と異なる点がある。根抵当権の分割譲渡が行われると、その根抵当権を目的とする権利（転抵当等）は、譲渡した根抵当権について消滅することになる（398条の12第２項後段）。従って、分割譲渡をするには、根抵当権を目的とする権利を有する者の承諾が必要とされる（同条３項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind1">(3) 一部譲渡</p>
          <p class="p6 ind2">一部譲渡とは、譲渡人と譲受人との間で、元本確定前の根抵当権を準共有する関係を成立させる処分方法をいう（398条の13）。一部譲渡をするには、全部譲渡の場合と同様に、譲渡人たる根抵当権者と譲受人の合意のほか、根抵当権設定者の承諾を得る必要がある。対抗要件として登記が必要であることも同様である。</p>
          <p class="p6 ind2">一部譲渡がなされると、譲渡人及び譲受人両当事者が極度額を共同利用することになる。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　転抵当</p>
          <p class="p6 ind1">元本確定の前後を問わず、することができる。</p>
