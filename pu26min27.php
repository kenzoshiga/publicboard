      <p id="A03-02-06-2" class="h6 ind0">Ⅱ　根抵当権の変更・処分等</p>
    <div class="blk outer">
      <div class="outer-title" id="A03-02-06-2-01">１　元本の確定前における内容の変更</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　被担保債権の範囲の変更</p>
          <p class="p6 ind1">元本確定前においては、設定当事者間の合意で根抵当権の担保すべき債権の範囲を変更することができる（398条の４第１項前段）。この変更には後順位抵当権者・転抵当権者等第三者の承諾を得ることを要しない（同条２項）。ただし、元本確定前に変更の登記をしなければ変更をしなかったものとみなされる（同条３項）。そこで、変更登記は事実上の効力発生要件と考えられている。</p>
          <p class="p6 ind1">なお、根抵当権が共有である場合には、被担保債権の範囲及び債務者は、各共有者間で異なってもよいが、この変更は、根抵当権の変更としてすべての共有者が当事者とならなければならない。</p>
