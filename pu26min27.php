          <p class="p6 ind1">被担保債権の範囲の変更と同様、元本確定前においては、設定当事者間の合意で債務者を変更することができる。第三者の承諾を得る必要がなく、元本確定前に変更の登記をしなければ変更をしなかったものとみなされる点も同様である（398条の４第１項後段、２項、３項）。</p>
