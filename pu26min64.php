        <div class="p-card">
          <p class="h6 ind0">2.　被保佐人は、保佐人の同意を得ずに完全に有効な遺言をすることができる（962条、13条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　成年被後見人も、事理を弁識する能力を一時回復している時において、２人以上の医師の立会いがあれば遺言をすることができる（973条１項）。</p>
          <p class="p6 ind1">※　遺言は、意思表示の一種であるから、意思能力がなければ遺言は無効となり、詐欺・強迫による遺言は取り消すことができる（これは遺言の撤回とは異なる）。</p>
