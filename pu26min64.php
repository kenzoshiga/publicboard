    <div class="p-card sec-card">
      <h3 id="A09-07-02">第２節　遺言の方式</h3>
    <div class="blk outer">
      <div class="outer-title" id="A09-07-02-0-01">１　総説</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　遺言方式の厳格性</p>
          <p class="p6 ind1">遺言は、民法に定める方式に従わなければ、遺言としての効力を生じない（960条）。</p>
          <p class="p6 ind1">遺言は遺言者の死亡後に効力が生じるので、遺言者に真意を確かめる機会はなく、異議を述べることもできず、遺言書の偽造・変造の危険も少なくない。そこで、遺言者の真意を確保し、併せて遺言書の偽造・変造の防止を図るため遺言の方式を規定した。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　証人・立会人及びその欠格</p>
          <p class="p6 ind1">自筆証書遺言以外の遺言は、証人、立会人を要する。これらの者は遺言を公正確実にするため必要とされるのであるから、以下の者は一般的能力を欠き、あるいは利害関係を有するため、証人・立会人になることができない（974条）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①未成年者</p>
          <p class="h6 ind3 nobold">②推定相続人及び受遺者並びにこれらの配偶者・直系血族</p>
          <p class="h6 ind3 nobold">③公証人の配偶者・４親等内の親族、書記・使用人</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　共同遺言の禁止</p>
          <p class="p6 ind1">遺言は、２人以上の者が同一の証書ですることはできない（975条）。</p>
          <p class="p6 ind1">共同遺言では、各自が自由に撤回できないおそれが生じるからである。</p>
          <p class="p6 ind1">もっとも、１通の証書に２人以上の遺言が記載されていても、証書が容易に切り離すことができる場合は、975条の共同遺言に当たらない（最判平５.10.19）。</p>
        </div>
