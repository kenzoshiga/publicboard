    <h2 id="A09-07">第７章　遺言</h2>
    <div class="p-card sec-card">
      <h3 id="A09-07-01">第１節　遺言制度</h3>
    <div class="blk outer">
      <div class="outer-title" id="A09-07-01-0-01">１　遺言の意義</div>
      <p>遺言とは、人の生前における最終の意思に法律的効果を認め、死後にその実現を図る制度をいう（960条以下）。</p>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A09-07-01-0-02">２　遺言の法的性質</div>
      <p>遺言者の自由な最終意思の確保という趣旨から、通常の法律行為とは異なる以下の性質を有する。</p>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　相手方のない単独行為</p>
          <p class="p6 ind1">相手方の承諾等は不要である。ただし、遺贈の放棄（986条１項）、成年の子の認知（782条）のように、効果を受ける者の意思によって、遺言者の意思が実現しない場合もある。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　死後行為（985条１項）</p>
          <p class="p6 ind1">遺言は、遺言者の死亡の時からその効力を生ずる。遺言者の最終意思の実現という趣旨からは、遺言者の生存中は受遺者において何らの権利、期待権を有していない（最判昭31.10.４）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">3.　要式行為</p>
          <p class="p6 ind1">遺言は、民法に定める方式に従わなければ、することができない（960条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">4.　代理は許されず、同意も不要である。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">ⅰ　遺言は人の最終意思を尊重するために認められるものであるから、遺言者の真意実現のためには性質上代理は親しまない。</p>
          <p class="h6 ind3 nobold">ⅱ　行為能力は不要であり、遺言能力があれば足りる（961条、962条、973条）。</p>
        </div>
        </div>
        <div class="p-card">
          <p class="h6 ind0">5.　遺言事項の法定</p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">6.　遺言撤回の自由（1022条～1027条）</p>
          <p class="p6 ind1">遺言者の最終意思を確保するためである。</p>
        </div>
      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A09-07-01-0-03">３　遺言事項</div>
      <p>遺言者の最終意思たる遺言を尊重する観点から、法律上遺言によってのみすることができる行為を定める反面、生存利害関係人等の利益にも配慮して、遺言によってすることができる行為を限定した（法定遺言事項）。</p>
