      </div>
    </div>
    <div class="blk outer">
      <div class="outer-title" id="A09-07-02-0-02">２　遺言の種類</div>
      <div class="blk inner">
        <div class="p-card">
          <p class="h6 ind0">1.　種類</p>
          <p class="p6 ind1">遺言には以下のような種類がある。原則として普通の方式によることを必要とするが、それが不可能又は困難という特殊事情がある場合に限って簡易な方式として特別の方式による遺言が認められる。</p>
          <p class="p6 ind1"><img class="embed-img" src="img/min64-02.png" alt="img/min64-02.png"></p>
        </div>
        <div class="p-card">
          <p class="h6 ind0">2.　普通の方式の遺言（967条～975条）</p>
        <div class="p-card" id="min64-q9">
          <p class="h6 ind1">(1) 自筆証書遺言</p>
<div class="jobun">
（自筆証書遺言）
第968条　自筆証書によって遺言をするには、遺言者が、その全文、日付及び氏名を自書し、これに印を押さなければならない。
２ 前項の規定にかかわらず、自筆証書にこれと一体のものとして相続財産（第997条第１項に規定する場合における同項に規定する権利を含む。）の全部又は一部の目録を添付する場合には、その目録については、自書することを要しない。この場合において、遺言者は、その目録の毎葉（自書によらない記載がその両面にある場合にあっては、その両面）に署名し、印を押さなければならない。
３ 自筆証書（前項の目録を含む。）中の加除その他の変更は、遺言者が、その場所を指示し、これを変更した旨を付記して特にこれに署名し、かつ、その変更の場所に印を押さなければ、その効力を生じない。
</div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 自書性について</p>
          <p class="p6 ind3">自筆証書遺言とは、遺言者が、遺言書の全文、日付及び氏名を自署し、これに押印することにより成立する遺言をいう（968条１項）。</p>
          <p class="p6 ind3">財産目録は自筆でなくてもよい。</p>
          <p class="p6 ind3">ex．パソコンによる目録の作成、通帳のコピー、登記事項証明書</p>
          <p class="p6 ind3">この場合、遺言者は、その目録の毎葉ごと（目録の記載が両面にある場合はその両面）に署名し、押印しなければならない（968条２項）。</p>
          <p class="p6 ind3">上記目録も含め、自筆証書中の加除その他の変更は遺言者がその場所を指示し、これを変更した旨を付記して署名し、かつ、その変更の場所に押印しなければその効力を生じない（968条３項）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 日付について</p>
          <p class="p6 ind3">年月日まで確定できる程度の日付の記載が必要である。例えば○年○月吉日は無効である（最判昭54.５.31）が、「長野オリンピック開会式当日」等、正確に年月日を知り得る日であれば差し支えない。</p>
          <p class="p6 ind3">自筆証書遺言によって遺言をするには、遺言が成立した日の日付を記載しなければならないと解される（最判昭52.4.19）。遺言を自書し、押印が後日となった場合、当該遺言には遺言が完成した日である押印した日の日付を記載するべきである。</p>
          <p class="p6 ind3">もっとも、自筆証書遺言に遺言が成立した日と相違する日の日付が記載されているからといって、直ちに当該遺言が無効となるものではない（最判令3.1.18）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(c) 押印について</p>
          <p class="p6 ind3">押印は本人の印鑑によってしなければならないが、認印でも拇印（指印）でもよい（大判大15.11.30）。</p>
          <p class="p6 ind3">なお、花押は不可である（最判平28.6.3）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(d) 保管と検認について</p>
          <p class="p6 ind3">自筆証書遺言による遺言書は、法務局での遺言書の保管が可能である。法務局で遺言書の保管をする場合、検認は不要である（「法務局における遺言書の保管等に関する法律（遺言書保管法）」、2020年7月10日施行）。</p>
          <p class="p6 ind3">その趣旨は、自筆証書遺言の保管場所を提供し、かつ検認手続を不要とすることで、自筆証書遺言の利用を促進することにある。</p>
        </div>
<div class="kakomon"><a href="pu26min64q.php#q-h20-23-o" title="H20-23-オ">過去問</a></div>
        </div>
        <div class="p-card" id="min64-q15">
          <p class="h6 ind1">(2) 公正証書遺言</p>
          <p class="p6 ind2">公正証書遺言とは、２人以上の証人の立会いを得て、遺言者が公証人に遺言の趣旨を口授し、公証人がこれを筆記して遺言者及び証人に読み聞かせ又は閲覧させ、遺言者及び証人が筆記の正確なことを承認した後に各自が署名押印し、公証人が法定の方式に従って作成された旨を付記して署名押印する方式をとる遺言をいう（969条）。</p>
        </div>
<div class="kakomon"><a href="pu26min64q.php#q-h01-23-3" title="H1-23-3">過去問</a></div>
        <div class="p-card" id="min64-q16">
          <p class="h6 ind1">(3) 秘密証書遺言</p>
        <div class="p-card">
          <p class="h6 ind2 nobold">(a) 意義</p>
          <p class="p6 ind3">秘密証書遺言とは、遺言の内容を自己の生存中は秘密にし、その形式的存在を明確にする目的の下に行われる遺言をいう（970条）。</p>
        </div>
        <div class="p-card">
          <p class="h6 ind2 nobold">(b) 方式</p>
          <p class="p6 ind3">秘密証書遺言は、以下の方式でなされる（970条）。</p>
        <div class="p-card">
          <p class="h6 ind3 nobold">①遺言者が秘密証書に署名し、押印する。</p>
          <p class="h6 ind3 nobold">②遺言者が、その証書を封じ、証書に用いた印章で封印する。</p>
          <p class="h6 ind3 nobold">③遺言者が公証人１人及び証人２人以上の前に封書を提出して、自己の遺言書である旨及びその筆者の氏名・住所を申述する〔聴覚、言語機能障害者は手話通訳も可（972条）〕。</p>
          <p class="h6 ind3 nobold">④公証人がその証書を提出した日付及び遺言者の申述を封紙に記載した後、遺言者及び証人とともに署名し、押印する。</p>
          <p class="p6 ind4">この要件を欠く場合、無効となるが、自筆証書の方式を満たしていれば自筆証書として有効とされることもある（無効行為の転換）。</p>
          <p class="p6 ind4">※　この方法は、署名押印を要件とするので、署名のできない者はすることができない。</p>
        </div>
<div class="kakomon"><a href="pu26min64q.php#q-r05-23-e" title="R5-23-エ">過去問</a></div>
        </div>
        </div>
        </div>
        <div class="p-card" id="min64-q17">
          <p class="h6 ind0">3.　特別の方式の遺言（976条～984条）</p>
          <p class="p6 ind1">普通の方式による遺言を利用できない非日常的状況下に置かれた者のため、その状況の相違に応じた特別の遺言方式が用意されている。</p>
<div class="jobun">
（死亡の危急に迫った者の遺言）
第976条　疾病その他の事由によって死亡の危急に迫った者が遺言をしようとするときは、証人３人以上の立会いをもって、その１人に遺言の趣旨を口授して、これをすることができる。この場合においては、その口授を受けた者が、これを筆記して、遺言者及び他の証人に読み聞かせ、又は閲覧させ、各証人がその筆記の正確なことを承認した後、これに署名し、印を押さなければならない。
２　口がきけない者が前項の規定により遺言をする場合には、遺言者は、証人の前で、遺言の趣旨を通訳人の通訳により申述して、同項の口授に代えなければならない。
３　第一項後段の遺言者又は他の証人が耳が聞こえない者である場合には、遺言の趣旨の口授又は申述を受けた者は、同項後段に規定する筆記した内容を通訳人の通訳によりその遺言者又は他の証人に伝えて、同項後段の読み聞かせに代えることができる。
４　前３項の規定によりした遺言は、遺言の日から２０日以内に、証人の１人又は利害関係人から家庭裁判所に請求してその確認を得なければ、その効力を生じない。
５　家庭裁判所は、前項の遺言が遺言者の真意に出たものであるとの心証を得なければ、これを確認することができない。
（伝染病隔離者の遺言）
第977条　伝染病のため行政処分によって交通を断たれた場所に在る者は、警察官１人及び証人１人以上の立会いをもって遺言書を作ることができる。
（在船者の遺言）
第978条　船舶中に在る者は、船長又は事務員１人及び証人２人以上の立会いをもって遺言書を作ることができる。
（船舶遭難者の遺言）
第979条　船舶が遭難した場合において、当該船舶中に在って死亡の危急に迫った者は、証人２人以上の立会いをもって口頭で遺言をすることができる。
２　口がきけない者が前項の規定により遺言をする場合には、遺言者は、通訳人の通訳によりこれをしなければならない。
３　前２項の規定に従ってした遺言は、証人が、その趣旨を筆記して、これに署名し、印を押し、かつ、証人の１人又は利害関係人から遅滞なく家庭裁判所に請求してその確認を得なければ、その効力を生じない。
４　第976条第５項の規定は、前項の場合について準用する。
（遺言関係者の署名及び押印）
第980条　第977条及び第978条の場合には、遺言者、筆者、立会人及び証人は、各自遺言書に署名し、印を押さなければならない。
